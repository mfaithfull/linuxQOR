#include <iostream>

#include "errorhandler.h"
#include "logginghandler.h"

bool ErrorHandler::Handle(const Error& error)
{
    auto severity = error.what().GetSeverity();
    auto severityString = SeverityNames[(int)severity];
    std::cerr << severityString << ": " << error.what().Content() << std::endl;
    
    switch (error.what().GetSeverity())
    {
    case Severity::Note:
        {            
            return true;
        }
        break;
    case Severity::Warning:
        {
            log::inform("{0}: {1}",severityString, error.what().Content());
            return true;
        }
        break;
    case Severity::Continuable_Error:
        {
            log::inform("{0}: {1}",severityString, error.what().Content());
            return true;
        }
        break;
    case Severity::Serious_Error:
        {
            log::impact("{0}: {1}",severityString, error.what().Content());
            return false;
        }
        break;
    case Severity::Fatal_Error:
        {
            log::imperative("{0}: {1}",severityString, error.what().Content());            
            return false;
        }
        break;
    }
    
    return false;
}
