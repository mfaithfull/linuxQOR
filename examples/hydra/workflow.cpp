#include <iostream>
#include <semaphore>

#include "workflow.h"
#include "echoworkflow.h"

HydraWorkflow::HydraWorkflow() : 
    setup(new_ref<State>(this)),
    freakout(new_ref<State>(this)),
    report(new_ref<State>(this))
{    
    qor_pp_ofcontext;    

    setup->Enter = [this]()->void
    {
        qor_pp_ofcontext;
        SetState(freakout);
    };

    freakout->Enter = [this]()->void
    {        
        qor_pp_ofcontext;
        auto application = AppBuilder().TheApplication();
        auto threadPool = application(qor_shared).GetRole()->GetFeature<ThreadPool>();
        const std::size_t num_tasks = 6;//threadPool->GetThreadCount() * 2000;        
        for (std::size_t i = 0; i < num_tasks; ++i)
        {
            threadPool->PostTask(
                [i]()
                {   
                    CurrentThread::GetCurrent().SetName(std::format("Echo thread {0}",i));
                    EchoWorkflow echoWorkflow;
                    int workflowResult = echoWorkflow.Run();                    
                }
            );
        }        
        SetState(report);
    };

    report->Enter = [this]()->void
    {        
        qor_pp_ofcontext;
        sleep(1);
        SetResult(EXIT_SUCCESS);            
        SetComplete();
    };

    SetInitialState(setup);
}
