#include <iostream>
#include "echoworkflow.h"

EchoWorkflow::EchoWorkflow() : 
    connect(new_ref<State>(this)),
    send(new_ref<State>(this)),
    receive(new_ref<State>(this)),
    m_logHandler(log::Level::Debug)
{    
    qor_pp_ofcontext;    

    m_totalSent = 0;
    m_totalReceived = 0;
    m_messageCount = 500;

    auto application = AppBuilder().TheApplication();
    auto logAggregator = application(qor_shared).GetRole()->GetFeature<LogAggregatorService>();
    qor::connect(m_logHandler, &qor::components::LogHandler::forward, 
        logAggregator(qor_shared).Receiver(), &qor::components::LogReceiver::ReceiveLog, 
        qor::ConnectionKind::QueuedConnection);

    connect->Enter = [this]()->void
    {
        qor_pp_ofcontext;
        
        m_connector = new_ref<SocketClientConnector>();
        m_requestPipeline = new_ref<RequestPipeline>(m_connector);
        m_responsePipeline = new_ref<ResponsePipeline>(m_connector);

        if( m_connector->ConnectToAddress("localhost", "127.0.0.1", 12345, qor::network::sockets::eAddressFamily::AF_INet, 0, false, false) )
        { 
            log::inform("Connected to server on 12345 @ localhost");
            SetState(send);
        }
        else
        {
            log::impact("Failed to connect to server on 12345 @ localhost: {0}", strerror(errno));
            SetResult(EXIT_FAILURE);
            SetComplete();
        }
    };

    send->Enter = [this]()->void
    {        
        qor_pp_ofcontext;
        size_t result = m_requestPipeline->PumpSome();
        m_totalSent += result;
        if(result == maxEchoSize)
        {
            log::important("Total sent {0}", m_totalSent);
            SetState(receive);
        }
        else
        {
            log::imperative("Failed to send to server: {0}", strerror(errno));
            SetResult(EXIT_FAILURE);            
            SetComplete();
        }       
    };

    receive->Enter = [this]()->void
    {
        qor_pp_ofcontext;
        size_t result = m_responsePipeline->PumpSome();
        m_totalReceived += result;
        if( result == maxEchoSize)
        {            
            log::important("Total received {0}", m_totalReceived );
         
            if(--m_messageCount > 0)
            {
                SetState(send);
            }
            else
            {
                log::important("Total sent and received {0}:{1}", m_totalSent, m_totalReceived);
                SetResult(EXIT_SUCCESS);
                SetComplete();
            }

        }
        else
        {
            log::imperative("Failed to receive from server: {0}", strerror(errno));
            SetResult(EXIT_FAILURE);
            SetComplete();      
        }
    };

    SetInitialState(connect);
}

EchoWorkflow::~EchoWorkflow()
{
    auto application = AppBuilder().TheApplication();
    auto logAggregator = application(qor_shared).GetRole()->GetFeature<LogAggregatorService>();
    qor::disconnect(m_logHandler, &qor::components::LogHandler::forward,
        logAggregator(qor_shared).Receiver(), &qor::components::LogReceiver::ReceiveLog);
    CurrentThread::GetCurrent().SetName("pool");
}