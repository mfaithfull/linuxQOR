//Replace the default framework Application singleton class with HTTPServerApp
SingletonRedirector<Application, HTTPServerApp> HTTPServerApp::m_sRedirect;

//Entry point
int main(const int argc, const char** argv, char** env)
{
    qor_pp_fcontext;
    return AppBuilder().Build<HTTPServerApp>(HTTPServerApp::Name,
        [argc,argv,env](ref_of<HTTPServerApp>::type server)
        {
            ThePlatform(qor_shared)->AddSubsystem<FileSystem>();
            ThePlatform(qor_shared)->AddSubsystem<Sockets>();

            app::OptionGetter options(argc, argv, server(qor_shared));

            server->SetRole<Role>(
                [](ref_of<IRole>::type role)
                {
                    role->AddFeature<thread::ThreadPool>(
                        [](ref_of<thread::ThreadPool>::type threadPool)->void
                        {
                            threadPool->SetThreadCount(8);
                            CurrentThread::Get().SetName("Main");
                        }
                    );
                    role->AddFeature<async::AsyncIOService>(
                        [](ref_of<async::AsyncIOService>::type ioService)->void
                        {
                            PoolInstancer::SetPoolSize<async::AsyncIOContext>(3);
                        }
                    );
                    role->AddFeature<LogAggregatorService>();
                }
            );

            server->UseAggregatedLogging();
        }
    ).As<HTTPServerApp>()->Run();
}

int HTTPServerApp::Run()
{
    qor_pp_ofcontext;
    log::inform("httpserver starting up");
    int result = RunWorkflow(new_ref<NetworkServer>(5080, new_ref<http::HTTPProtocol>()));
    log::inform("httpserver shutting down");
    return result;
}

