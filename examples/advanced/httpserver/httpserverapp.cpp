HTTPServerApp& HTTPServerApp::UseAggregatedLogging()
{
    if(!m_useAggregatedLogging)
    {
        SetupAggregatedLogging();
        m_useAggregatedLogging = true;
    }
    return *this;
}

const DefaultErrorHandler& HTTPServerApp::GetErrorHandler() const
{
    return m_errorHandler;
}

const DefaultLogHandler& HTTPServerApp::GetLogHandler() const
{
    return m_logHandler;
}

void HTTPServerApp::SetupAggregatedLogging() const
{
    auto logPath = ThePlatform(qor_shared)->GetSubsystem<FileSystem>()->ApplicationLogPath() / logTag;
    const DefaultLogHandler& logHandler = GetLogHandler();
    ref_of<LogAggregatorService>::type logAggregator = GetRole(qor_shared)->GetFeature<LogAggregatorService>();
    connect(logHandler, logHandler.GetForwardSignal(), logAggregator(qor_shared).Receiver(), &LogReceiver::ReceiveLog, ConnectionKind::QueuedConnection);
    logAggregator(qor_shared).Receiver().WriteToFileSystem(logPath, logTag);
    logAggregator(qor_shared).Receiver().WriteToStandardOutput(false);
}

void HTTPServerApp::ShutdownAggregatedLogging() const
{
    const qor::DefaultLogHandler& logHandler = GetLogHandler();
    disconnect(logHandler, logHandler.GetForwardSignal(), GetRole(qor_shared)->GetFeature<LogAggregatorService>(qor_shared)->Receiver(), &LogReceiver::ReceiveLog);
}
