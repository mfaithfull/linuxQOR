#include "sdk/using_framework.h"

#include <string>
#include <iostream>

/*To create a custom feature derive from IFeature*/
class CustomFeature : public IFeature
{
public:

    CustomFeature() = default;
    virtual ~CustomFeature() = default;

    /*Override Setup and shutdown for your feature
    these will be called by the Role*/

    virtual void Setup() 
    {
        //Do one time setup for CustomRole here
    }

    virtual void Shutdown() 
    {
        //Shutdown and cleanup CustomRole here
    }

    /*Add whatever else your feature requires*/
    void DoThatConfigurationThing(const std::string& message)
    {
        m_message = message;
    }

    void SayHello()
    {
        std::cout << "Hello world." << std::endl;
    }

private:

    std::string m_message;
};

/*Provide a unique identifier for your feature. This is used as the key in the Role's feature container.*/

constexpr GUID CustomFeatureGUID = { 0x0b8962d2, 0x9b64, 0x4e86, {0xb4, 0x66, 0x9d, 0x64, 0x5c, 0x8a, 0x24, 0xe5}};
namespace qor { qor_pp_declare_guid_of(CustomFeature, CustomFeatureGUID); }//qor

/*The GUID can be used to access your custom feature from the Role with Role::GetFeature(const GUID* id)*/