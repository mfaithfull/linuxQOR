using namespace qor;
using namespace qor::win;
using namespace qor::platform::win;
using namespace qor::platform::win::gui::view;
using namespace std;

class TestMenuHandler : public MenuHandler
{
public:
    bool OnMenuSelect(Window& Wnd, Menu& hMenu, unsigned short index, unsigned short flags)
    {
        //TODO: when we get here with a non zero index we need to issue an actual command related to the menu item
        return false;
    }
};

class WithMenuHandlerFactory : public HandlerFactory<TopLevelWindowHandler>
{
    virtual ref_of<qor::platform::win::gui::view::AbstractWindowHandler>::type Create()
    {
        auto topLevel    =  new_ref<TopLevelWindowHandler>();
        auto menuHandler =  new_ref<TestMenuHandler>();
        topLevel->m_menu = menuHandler;
        return topLevel;
    }
};

int main()
{
    WindowFactory factory(GetInstance());    
    MessageHandler messageHandler;

                        //Create the main window
    auto windowClass =  factory.AddWindowClass(L"WithMenu");    
                        factory.RegisterClass(windowClass, new_ref<WithMenuHandlerFactory>());
    auto controller  =  factory.Create(windowClass, L"Menu Testbed");

                        //Build and attach the content of the window
    auto menuBar     =  new_ref<Menu>();
    auto fileMenu    =  new_ref<PopupMenu>();
    auto itemIdNew   =  fileMenu->AppendTextItem(L"New");
    auto itemIdOpen  =  fileMenu->AppendTextItem(L"Open");
    auto itemIdSave  =  fileMenu->AppendTextItem(L"Save");
    auto itemIdHelp  =  menuBar->AppendTextItem(L"Help");
                        menuBar->AppendSubMenu(fileMenu, L"File");
                        controller->SetMenu(menuBar);
                        //Show the window and run the message loop
                        controller->Show();
    return              messageHandler.MessageLoop();
}
