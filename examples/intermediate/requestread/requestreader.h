#include "sdk/using_framework.h"
#include "sdk/using_platform.h"
#include "sdk/components/framework.h"
#include "src/components/protocols/http/request/parser/requesttoken.h"
#include "src/components/protocols/http/request/parser/requestnode.h"

class RequestReader
{
public:

    RequestReader() : m_byteBuffer(2048), m_sink(m_byteBuffer)
    {        
    }

    ref_of<components::protocols::http::HTTPRequest>::type operator()(const pipeline::Plug& sourceConnector)
    {        
        Pipeline(
            sourceConnector,
            m_sink,
            Element::Push).Connect().PumpAll();
      
        m_sink.Parser().FinalParse();
        auto finalNode = m_sink.Parser().PopNode();
        return finalNode.AsRef<components::protocols::http::RequestNode>()->GetObject();
    }

    const pipeline::Buffer& Buffer()
    {
        return m_byteBuffer;
    }

private:

    pipeline::PODBuffer<byte> m_byteBuffer;
    ParserSink<components::protocols::http::request> m_sink;
};
