using namespace qor;
using namespace qor::test;
using namespace qor::components::threadmemory;

struct SmallObjectBucketTestSuite{};

struct TestSmallItemAgain
{
    long pole;
    long ladder;
    long handle;
};

qor_pp_test_suite_case(SmallObjectBucketTestSuite, canAllocateAndFreeFromSmallObjectBucket)
{
    SmallObjectBucket* bucket = nullptr;
    bucket = new SmallObjectBucket(sizeof(TestSmallItemAgain) & 0x3F);
    qor_pp_assert_that(bucket).isNotNull();
    delete bucket;
}

qor_pp_test_suite_case(SmallObjectBucketTestSuite, canAllocateAndFreeAnItemFromASmallObjectBucket)
{
    SmallObjectBucket* bucket = nullptr;
    bucket = new SmallObjectBucket(sizeof(TestSmallItemAgain) & 0x3F);    
    byte* memory = bucket->Allocate();
    qor_pp_assert_that(bucket->AllocatedItems()).isEqualTo(1);
    bucket->Free(memory);    
    qor_pp_assert_that(bucket->AllocatedItems()).isEqualTo(0);
    delete bucket;
}

qor_pp_test_suite_case(SmallObjectBucketTestSuite, canAllocateAndFreeMultipleItemsFromASmallObjectBucket)
{
    SmallObjectBucket* bucket = nullptr;
    bucket = new SmallObjectBucket(sizeof(TestSmallItemAgain) & 0x3F);
    
    byte* memory1 = bucket->Allocate();
    byte* memory2 = bucket->Allocate();
    byte* memory3 = bucket->Allocate();
    byte* memory4 = bucket->Allocate();
    byte* memory5 = bucket->Allocate();
    qor_pp_assert_that(bucket->AllocatedItems()).isEqualTo(5);
    bucket->Free(memory3);
    bucket->Free(memory4);
    bucket->Free(memory5);
    qor_pp_assert_that(bucket->AllocatedItems()).isEqualTo(2);
    bucket->Free(memory1);
    bucket->Free(memory2);
    qor_pp_assert_that(bucket->AllocatedItems()).isEqualTo(0);
    delete bucket;
}

