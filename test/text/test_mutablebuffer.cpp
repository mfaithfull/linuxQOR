qor_pp_test_case(canUseMutableBufferWithRangesViewsAndAlgorithms)
{
    auto text = MutableBuffer<char>{"     Hello World"};    
    auto conv = std::views::transform(std::views::drop_while(text, ::isspace),::toupper);
    std::string temp(conv.begin(), conv.end());
    qor_pp_assert_that(temp).isEqualTo("HELLO WORLD");
}

qor_pp_test_case(canGetDataOfMutableBuffer)
{
    auto text = MutableBuffer<char8_t>{u8"Hello World"};
    {
        auto ptr = text.data();
        char8_t* p = ptr;
        size_t len = 0;
        while(p != nullptr && *p++ != 0)
        {
            ++len;        
        }
        ptr.Validate(len);
    }//View expires here and validates length of buffer
    qor_pp_assert_that(text.Length()).isEqualTo(11);
}