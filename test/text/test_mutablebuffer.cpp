qor_pp_test_case(canUseRangeBasedForLoopWithMutableBuffer)
{
    auto mb = MutableBuffer<char>("Hello World");

    std::string s;
    for (char c : mb)
    {
        s += c;
    }
    qor_pp_assert_that(s).isEqualTo("Hello World");
}

qor_pp_test_case(canUseReverseIteratorsWithMutableBuffer)
{
    auto mb = MutableBuffer<char>("Hello World");

    std::string s;
    for (auto it = mb.rbegin(); it != mb.rend(); ++it)
    {
        s += *it;
    }
    std::reverse(s.begin(), s.end());
    qor_pp_assert_that(s).isEqualTo("Hello World");
}

