qor_pp_test_case(canInstanceUTF16String)
{
    UTF16String utf16str(u"Hello World");
    qor_pp_assert_that(utf16str.Length()).isEqualTo(11);
    qor_pp_assert_that(utf16str[0]).isEqualTo('H');
    qor_pp_assert_that(utf16str.At(10)).isEqualTo('d');
}

qor_pp_test_case(canGetLeftCharsFromUTF16String)
{
    UTF16String utf16str(u"Hello World");
    auto result = utf16str.Left(5);
    qor_pp_assert_that(result.Length()).isEqualTo(5);
    qor_pp_assert_that(result[0]).isEqualTo('H');
    qor_pp_assert_that(result.At(4)).isEqualTo('o');
}

qor_pp_test_case(canGetRightCharsFromUTF16String)
{
    UTF16String utf16str(u"Hello World");
    auto result = utf16str.Right(5);
    qor_pp_assert_that(result.Length()).isEqualTo(5);
    qor_pp_assert_that(result[0]).isEqualTo('W');
    qor_pp_assert_that(result.At(4)).isEqualTo('d');
}

qor_pp_test_case(canHoldMultiByteCharsInUTF16String)
{
    UTF16String korean(u"﻿중앙일보 - 사건/사회 - 극지탐험　협회결");
    size_t length = korean.Length();
    qor_pp_assert_that(korean.IsEmpty()).isFalse();
    qor_pp_assert_that(length).isGreaterThan(10);
}

qor_pp_test_case(canGetRightCharsInUTF16String)
{
    UTF16String korean16(u"﻿중앙일보 - 사건/사회 - 극지탐험　협회결");    
    auto result = korean16.Right(3);
    qor_pp_assert_that(result.Length()).isEqualTo(3);
    UTF16String oneglyph(u"결");
    qor_pp_assert_that(result[2]).isEqualTo(oneglyph[0]);
}

qor_pp_test_case(canUseUTF16StringWithRangesViewsAndAlgorithms)
{
    UTF16String test{u"    Something easy."};
    auto conv = std::views::drop_while(test, ::isspace);
    std::u16string temp(conv.begin(), conv.end());
    std::u16string cmp(u"Something easy.");
    bool check = (temp == cmp) ? true : false;
    qor_pp_assert_that(check).isTrue();
}

qor_pp_test_case(canEraseFromUTF16String)
{
    UTF16String input{u"Something NOT good"};
    auto output = input.Erase(10,4);
    qor_pp_assert_that(output.ToStdString() == std::u16string(u"Something good")).isTrue();
}
