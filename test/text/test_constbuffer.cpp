qor_pp_test_case(canUseRangeBasedForLoop)
{
    constexpr auto cb = ConstBuffer<char>("Hello World");

    std::string s;
    for (char c : cb)
    {
        s += c;
    }
    qor_pp_assert_that(s).isEqualTo("Hello World");
}

qor_pp_test_case(canUseReverseIterators)
{
    constexpr auto cb = ConstBuffer<char>("Hello World");

    std::string s;
    for (auto it = cb.rbegin(); it != cb.rend(); ++it)
    {
        s += *it;
    }
    std::reverse(s.begin(), s.end());
    qor_pp_assert_that(s).isEqualTo("Hello World");
}

