struct TTFReaderTestSuite{};

qor_pp_test_suite_case(TTFReaderTestSuite, canOpenTTFFileAndReadHeader)
{
    auto filesystem = ThePlatform(qor_shared)->GetSubsystem<FileSystem>();
    filesystem::Index fontFile(filesystem::Path("F:/Develop/linuxQOR/cmakebuild/test/data"), "NOTO-SANS-KR.ttf");
    {
        FileConnector connector(fontFile, io::OpenFor::ReadOnly, io::WithFlags::None);
        qor::res::ttf::Reader reader(connector, 65536);
        auto rawFont = reader.ReadFont("NOTO-SANS-KR.ttf");
        rawFont->Init();
        size_t glyphCount = rawFont->GetGlyphCount();
        uint32_t indexK = rawFont->GetGlyphIndex(CodePoint((uint32_t)'g'));
        uint32_t offsetK = rawFont->GetGlyphOffset(indexK);

        qor::res::ttf::glyph_header header = rawFont->m_glyfTable->ReadGlyphHeader(offsetK);
        if(header.contours > 0)
        {
            rawFont->m_glyfTable->ReadSimpleOutline(offsetK, header);
        }
        //reader.ReadGlyph(indexA);
    }
}
