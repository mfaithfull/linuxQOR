qor_pp_test_suite_case(PipelineTestSuite, testPipelineReaderNetworkByteOrder)
{
    auto filesystem = ThePlatform(qor_shared)->GetSubsystem<FileSystem>();
    filesystem::Index inFile(filesystem::Path("F:/Develop/linuxQOR/cmakebuild/test/data"), "test_output.bin");
    FileConnector connector(inFile, io::OpenFor::ReadOnly, io::WithFlags::None);
    Reader reader(connector, 64);
    reader.SetByteOrder(arch::Endian::network);

    unsigned long long ullValue = reader.Read<unsigned long long>();
    qor_pp_assert_that(ullValue).isEqualTo(784000001);
    byte byteValue = reader.Read<byte>();
    qor_pp_assert_that(byteValue).isEqualTo(0xFE);
    short shortValue = reader.Read<short>();
    qor_pp_assert_that(shortValue).isEqualTo(13001);
    double doubleValue = reader.Read<double>();
    qor_pp_assert_that(doubleValue).isEqualTo(123.456);
}

struct Biscuit
{
    int sizes[2]{27,47};
    float price{0.04f};
    double weightKg{0.01};
    char categoryLabel{'g'};
};

qor_pp_test_suite_case(PipelineTestSuite, testPipelineWriteStruct)
{
    Biscuit biscuit4[4];
    auto filesystem = ThePlatform(qor_shared)->GetSubsystem<FileSystem>();
    filesystem::Index biscuitFile(filesystem::Path("F:/Develop/linuxQOR/cmakebuild/test/data"), "biscuits.bin");
    {
        FileConnector connector(biscuitFile, io::OpenFor::WriteOnly, io::WithFlags::None);
        Writer writer(connector, 64);
        writer << biscuit4[0] << biscuit4[1] << biscuit4[2] << biscuit4[3];
    }
    {
        FileConnector connector(biscuitFile, io::OpenFor::ReadOnly, io::WithFlags::None);
        Reader reader(connector, 64);
        reader >> biscuit4[0] >> biscuit4[1] >> biscuit4[2] >> biscuit4[3];
    }
}