struct AsyncMutexTestSuite{};

qor_pp_test_suite_case(AsyncMutexTestSuite, try_lock)
{
	AsyncMutex mutex;

	qor_pp_assert_that(mutex.try_lock());
	qor_pp_assert_that(mutex.try_lock()).isFalse();
	mutex.unlock();
	qor_pp_assert_that(mutex.try_lock());
}
