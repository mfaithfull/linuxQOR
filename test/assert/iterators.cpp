#include "../../src/configuration/configuration.h"

#include <vector>

#include "../../src/qor/test/test.h"
#include "../../src/qor/assert/assert.h"

namespace test_iterators {

    using namespace qor;
    using namespace qor::test;

    struct IteratorsTests {};

    qor_pp_test_suite_case(IteratorsTests, IsEmpty) 
    {
        std::vector<int> v0;
        qor_pp_assert_that(v0).isEmpty();
        std::list<int> l0;
        qor_pp_assert_that(l0).isEmpty();
        std::array<int, 0> a0;
        qor_pp_assert_that(a0).isEmpty();
        std::deque<int> d0{};
        qor_pp_assert_that(a0).isEmpty();
        std::forward_list<int> fl{};
        qor_pp_assert_that(fl).isEmpty();
    }

    qor_pp_test_suite_case(IteratorsTests, IsExpectIsEmpty) 
    {
        std::vector<int> v0;
        qor_pp_expect_that(v0).isEmpty();
        std::list<int> l0;
        qor_pp_expect_that(l0).isEmpty();
        std::array<int, 0> a0;
        qor_pp_expect_that(a0).isEmpty();
        std::deque<int> d0{};
        qor_pp_expect_that(a0).isEmpty();
        std::forward_list<int> fl{};
        qor_pp_expect_that(fl).isEmpty();
    }

    qor_pp_test_suite_case(IteratorsTests, IsNotEmpty) 
    {
        std::vector<int> v0{1, 2};
        qor_pp_assert_that(v0).isNotEmpty();
        std::list<int> l0{3, 4};
        qor_pp_assert_that(l0).isNotEmpty();
        std::array<int, 10> a0;
        qor_pp_assert_that(a0).isNotEmpty();
        std::deque<int> d0{1, 2, 3};
        qor_pp_assert_that(d0).isNotEmpty();
        std::forward_list<int> fl{1, 2, 3};
        qor_pp_assert_that(fl).isNotEmpty();
    }

    qor_pp_test_suite_case(IteratorsTests, IsNotEqualTo) 
    {
        std::vector<int> v0{1, 2};
        std::vector<int> v1{1, 3};
        qor_pp_assert_that(v0).isNotEqualTo(v1.begin(), v1.end());
    }

    qor_pp_test_suite_case(IteratorsTests, IsEqualTo) 
    {
        std::vector<int> v0{1, 2};
        std::vector<int> v1{1, 2};
        qor_pp_assert_that(v0).isEqualTo(v1.begin(), v1.end());
    }

    qor_pp_test_suite_case(IteratorsTests, Contains) 
    {
        std::vector<int> v0{1, 2};
        qor_pp_assert_that(v0).contains(2);
    }

    qor_pp_test_suite_case(IteratorsTests, HasSize) 
    {
        std::vector<int> v0{1, 2};
        qor_pp_assert_that(v0).hasSizeThat().isEqualTo(2);
    }

}//test_iterators
