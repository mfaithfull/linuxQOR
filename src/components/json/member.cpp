namespace qor { namespace components { namespace json { namespace parser {

    using namespace qor::components::parser;

    //member = string name-separator value
    member::member(Parser* parser) : 
        Sequence(parser,
            new_ref<string>(parser).AsRef<ParserState>(),
            new_ref<Sequence>(parser,
                new_ref<name_separator>(parser).AsRef<ParserState>(),
                new_ref<value>(parser).AsRef<ParserState>()
            ).AsRef<ParserState>(),
        static_cast<uint64_t>(jsonToken::member))
    {
    }

    void member::Prepare()
    {
        //Push a node onto the stack in expectation of an array
        GetParser()->PushNode(new_ref<Member>().AsRef<Node>());
    }

    void member::Emit()
    {
        std::string name;
        ref_of<Value>::type value;
        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = jsonTokenNames.find(token);
            std::string tokenName;
            if(f != jsonTokenNames.end())
            {
                tokenName = f->second;
            }

            if(token == static_cast<uint64_t>(jsonToken::value))
            {
                auto valueNode = node.AsRef<Value>();
                value = valueNode;

                node = GetParser()->PopNode();

                uint64_t token2 = node->GetToken();
                auto f2 = jsonTokenNames.find(token2);
                std::string tokenName2;
                if(f2 != jsonTokenNames.end())
                {
                    tokenName2 = f2->second;
                }

                if(token2 == static_cast<uint64_t>(jsonToken::string))
                {
                    auto stringNode = node.AsRef<String>();
                    name = stringNode->GetValue();
                }
                else
                {
                    std::cout << "Emitting a member - name: " << std::endl;
                    std::cout << "unexpected:" << tokenName2 << std::endl;
                }
            }
            else
            {
                std::cout << "Emitting a member: " << std::endl;
                std::cout << "unexpected:" << tokenName << std::endl;
            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            auto memberNode = node.As<Member>();
            memberNode->SetName(name);
            memberNode->GetValue() = value;
            GetParser()->PushNode(node);
        }
    }

    void member::Fail()
    {
        ref_of<Node>::type node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }
}}}}