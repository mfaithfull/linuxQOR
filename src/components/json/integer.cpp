namespace qor { namespace components { namespace json { namespace parser {

    using namespace qor::components::parser;

    void integer::Prepare()
    {
        GetParser()->PushNode(new_ref<Integer>().AsRef<Node>());
    }

    void integer::Emit()
    {
        int value = 0;
        int magnitude = 1;
        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = jsonTokenNames.find(token);
            std::string tokenName;
            if(f != jsonTokenNames.end())
            {
                tokenName = f->second;
            }
            
            if(token == static_cast<uint64_t>(components::parser::eToken::Digit))
            {
                auto digitNode = node.AsRef<components::parser::Digit>();
                unsigned int digitVal = digitNode->GetValue();
                value += digitVal * magnitude;
                magnitude *= 10;
            }
            else
            {
                std::cout << "Emitting an integer: " << std::endl;
                std::cout << "unexpected:" << tokenName << std::endl;

            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            auto integerNode = node.AsRef<Integer>();
            integerNode->SetValue(value);
            GetParser()->PushNode(node);
        }
    }

    void integer::Fail()
    {
        ref_of<Node>::type node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

}}}}//qor::components::json::parser
