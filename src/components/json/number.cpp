    using namespace qor::components::parser;

    void number::Prepare()
    {
        GetParser()->PushNode(new_ref<Number>().AsRef<Node>());
    }

    //number = [ minus ] int [ frac ] [ exp ]
    void number::Emit()
    {
        double value = 0.0;        
        int sign = 1;
        double frac;
        long exp;

        auto node = GetParser()->PopNode();
        do{
            if(node.IsNotNull() && node->GetToken() != m_token)
            {
                uint64_t token = node->GetToken();

                if(token == static_cast<uint64_t>(jsonToken::minus))
                {
                    sign = -1;
                    node = GetParser()->PopNode();
                    token = node->GetToken();
                    if(node.IsNull() || node->GetToken() == m_token)
                    {
                        break;
                    }
                }
                
                if(token == static_cast<uint64_t>(jsonToken::integer))
                {
                    auto integerNode = node.AsRef<Integer>();
                    value = integerNode->GetValue();
                    node = GetParser()->PopNode();
                    token = node->GetToken();
                    if(node.IsNull() || node->GetToken() == m_token)
                    {
                        break;
                    }
                }
                else
                {
                    auto f = jsonTokenNames.find(token);
                    std::string tokenName;
                    if(f != jsonTokenNames.end())
                    {
                        tokenName = f->second;
                    }

                    std::cout << "Emitting a number: " << std::endl;
                    std::cout << "unexpected:" << tokenName << std::endl;
                }

                if(token == static_cast<uint64_t>(jsonToken::fraction))
                {
                    auto fractionNode = node.AsRef<Fraction>();
                    //TODO: Sort out fractions
                    node = GetParser()->PopNode();
                    token = node->GetToken();
                    if(node.IsNull() || node->GetToken() == m_token)
                    {
                        break;
                    }
                }

                if(token == static_cast<uint64_t>(jsonToken::exp))
                {
                    auto expNode = node.AsRef<Exp>();
                    //TODO: Sort out exponent
                    node = GetParser()->PopNode();
                    token = node->GetToken();
                }
                
            }
        }while(node.IsNotNull() && node->GetToken() != m_token);

        if(node.IsNotNull())
        {
            auto numberNode = node.AsRef<Number>();
            numberNode->SetValue(value);
            //std::cout << value << std::endl;
            GetParser()->PushNode(node);
        }
    }

    void number::Fail()
    {
        ref_of<Node>::type node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

