    using namespace qor::components::parser;

    //value = false / null / true / object / array / number / string
    value::value(Parser* parser) : 
        AnyOneOf(parser,
            new_ref<_false>(parser).AsRef<ParserState>(),
            new_ref<AnyOneOf>(parser,
                new_ref<_null>(parser).AsRef<ParserState>(),
                new_ref<AnyOneOf>(parser,
                    new_ref<_true>(parser).AsRef<ParserState>(),
                    new_ref<AnyOneOf>(parser,
                        new_ref<deferred<object>>(parser).AsRef<ParserState>(),
                        new_ref<AnyOneOf>(parser,
                            new_ref<deferred<array>>(parser).AsRef<ParserState>(),
                            new_ref<AnyOneOf>(parser,
                                new_ref<number>(parser).AsRef<ParserState>(),
                                new_ref<string>(parser).AsRef<ParserState>()
                            ).AsRef<ParserState>()
                        ).AsRef<ParserState>()
                    ).AsRef<ParserState>()
                ).AsRef<ParserState>()
            ).AsRef<ParserState>(),
        static_cast<uint64_t>(jsonToken::value))
    {
    }

    void value::Prepare()
    {
        //Push a node onto the stack in expectation of an object
        GetParser()->PushNode(new_ref<Value>().AsRef<Node>());
    }

    void value::Emit()
    {
        ValueType vt;
        ref_of<Node>::type valueNode;
        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = jsonTokenNames.find(token);
            std::string tokenName;
            if(f != jsonTokenNames.end())
            {
                tokenName = f->second;
            }

            switch(token)
            {
            case static_cast<uint64_t>(jsonToken::_false):
                vt = ValueType::_false;
                valueNode = node;
                break;
            case static_cast<uint64_t>(jsonToken::_true):
                vt = ValueType::_true;
                valueNode = node;
                break;
            case static_cast<uint64_t>(jsonToken::_null):
                vt = ValueType::_null;
                valueNode = node;
                break;
            case static_cast<uint64_t>(jsonToken::object):
                vt = ValueType::object;
                valueNode = node;
                break;
            case static_cast<uint64_t>(jsonToken::array):
                vt = ValueType::array;
                valueNode = node;
                break;
            case static_cast<uint64_t>(jsonToken::number):
                vt = ValueType::number;
                valueNode = node;
                break;
            case static_cast<uint64_t>(jsonToken::string):
                vt = ValueType::string;
                valueNode = node;
                break;
            default:
                std::cout << "Emitting a value: " << std::endl;
                std::cout << "TODO:" << tokenName << std::endl;
            }

            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            if( valueNode.IsNotNull())
            {
                auto thisNode = node.AsRef<Value>();
                thisNode->SetValue(valueNode);
                thisNode->SetType(vt);
            }
            GetParser()->PushNode(node);
        }
    }

    void value::Fail()
    {
        ref_of<Node>::type node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

