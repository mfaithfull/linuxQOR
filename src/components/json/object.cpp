namespace qor { namespace components { namespace json { namespace parser {

    using namespace qor::components::parser;

    //object = begin-object [ member *( value-separator member ) ] end-object
    object::object(Parser* parser) :
        Sequence(parser,
            new_ref<begin_object>(parser).AsRef<ParserState>(),
            new_ref<Sequence>(parser,
                new_ref<Optional>(parser,
                    new_ref<Sequence>(parser,
                        new_ref<member>(parser).AsRef<ParserState>(),
                        new_ref<ZeroOrMore>(parser,
                            new_ref<Sequence>(parser,
                                new_ref<value_separator>(parser).AsRef<ParserState>(),
                                new_ref<member>(parser).AsRef<ParserState>()
                            ).AsRef<ParserState>()
                        ).AsRef<ParserState>()
                    ).AsRef<ParserState>()
                ).AsRef<ParserState>(),
                new_ref<end_object>(parser).AsRef<ParserState>()
            ).AsRef<ParserState>(),
        static_cast<uint64_t>(jsonToken::object))
    {
    }

    void object::Prepare()
    {
        //Push a node onto the stack in expectation of an object
        GetParser()->PushNode(new_ref<Object>().AsRef<Node>());
    }

    void object::Emit()
    {
        std::vector<ref_of<Member>::type> members;
        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = jsonTokenNames.find(token);
            std::string tokenName;
            if(f != jsonTokenNames.end())
            {
                tokenName = f->second;
            }

            if(token == static_cast<uint64_t>(jsonToken::member))
            {
                auto memberNode = node.AsRef<Member>();
                members.push_back(memberNode);
            }
            else
            {
                std::cout << "Emitting an object: " << std::endl;
                std::cout << "unexpected:" << tokenName << std::endl;
            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            for(auto it = members.rbegin(); it != members.rend(); ++it)
            {
                node.AsRef<Object>()->m_members.push_back(*it);
            }
            GetParser()->PushNode(node);
        }
    }

    void object::Fail()
    {
        ref_of<Node>::type node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

}}}}