namespace qor { namespace components { namespace serial {

    DeserializerState::DeserializerState(Deserializer* deserializer, size_t size, arch::Endian endian) : workflow::State(deserializer), m_size(size), m_endian(endian)
    {
        m_result = UNINITIALIZED;                
        if(m_endian == arch::endian)
        {
            m_index = 0;
        }
        else
        {
            m_index = m_size - 1;
        }

        Enter = [this]()
        {            
            byte* data = nullptr;
            if(GetContext()->GetOctet(data))
            {
                m_data[m_index] = *data;                    
                GetContext()->ConsumeOctet();

                if(m_endian == arch::endian)
                {
                    ++m_index;
                }
                else
                {
                    --m_index;
                }
            }
            else
            {
                m_result = MORE_DATA;                
            }
        };

        Leave = [this]()
        {
            if(m_result == FAILURE)
            {
                //Fail();
            }
            else if(m_result == SUCCESS)
            {
                //Emit();
            }
            else if(m_result == MORE_DATA)
            {
                //std::cout << "Ran out of data before we could decide. Reenter with more data to try again." << std::endl;
            }
        };

    }

    Context* DeserializerState::GetContext()
    {
        return dynamic_cast<Deserializer*>(m_Workflow)->GetContext();
    }

    workflow::Workflow* DeserializerState::Workflow()
    {
        return dynamic_cast<workflow::Workflow*>(m_Workflow);
    }

    Deserializer* DeserializerState::GetDeserializer()
    {
        return dynamic_cast<Deserializer*>(m_Workflow);
    }
    
}}}//qor::components::serial
