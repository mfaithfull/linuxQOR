    template<typename T>
    class PrimitiveState : public workflow::State
    {
    public:

        PrimitiveState(Deserializer* deserializer, arch::Endian endian, T* pt) :  workflow::State(deserializer), m_endian(endian), m_t(pt)
        {
            m_size = sizeof(T);
            m_data = reinterpret_cast<byte*>(m_t);
            m_result = UNINITIALIZED;                
            m_index = (m_endian == arch::endian) ? 0 : m_size - 1;//index to begining or end depending if data source endian(ness) is same as host.

            Enter = [this]()
            {            
                byte* data = nullptr;
                if(GetContext()->GetOctet(data))
                {
                    m_data[m_index] = *data;                    
                    GetContext()->ConsumeOctet();

                    if(m_endian == arch::endian)
                    {                    
                        if(m_index == m_size)//We just wrote the last byte
                        {
                            m_result = SUCCESS;
                            Workflow()->PopState();
                        }
                        else                //More to write
                        {
                            ++m_index;
                        }
                    }
                    else
                    {
                        if(m_index == 0)//We just wrote the last byte in reverse order
                        {
                            m_result = SUCCESS;
                            Workflow()->PopState();
                        }
                        else            //More to write
                        {
                            --m_index;
                        }
                    }
                }
                else
                {
                    m_result = MORE_DATA;//Pipeline ran out of data. Read more data and re-Enter to continue.       
                }
            };
        }

        virtual ~PrimitiveState()
        {
            m_result = m_result;
        }          

    protected:

        class Context* GetContext()
        {
            return dynamic_cast<Deserializer*>(m_Workflow)->GetContext();
        }

        workflow::Workflow* Workflow()
        {
            return dynamic_cast<workflow::Workflow*>(m_Workflow);
        }

        class Deserializer* GetDeserializer()
        {
            return dynamic_cast<Deserializer*>(m_Workflow);
        }

        arch::Endian m_endian;//endianness of the source
        byte* m_data;
        size_t m_index;
        size_t m_size;
        ResultCode m_result;
        T* m_t;
    };

    template<class struct_T, std::size_t I, std::size_t N> class StructState;

    template <std::size_t I, std::size_t N>
    struct StateSelector
    {
        template<class T, class R>
        static bool Select(Deserializer* deserializer, arch::Endian endian, const T& value, R* whole_value, bool recurse = true)
        {
            if(!recurse)
            {
                return false;
            }
            
            using element_t = decltype(qor_reflection::detail::sequence_tuple::get<I>(value));
            constexpr std::size_t subFieldCount = qor_reflection::detail::fields_count<std::remove_cvref_t<element_t>>();

            if(subFieldCount == 1)
            {                
                deserializer->PushState(new_ref<PrimitiveState<std::remove_cvref_t<element_t>>>(deserializer, endian, (std::remove_cvref_t<element_t>*)(&qor_reflection::detail::sequence_tuple::get<I>(value))));
            }
            else if(subFieldCount > 1)
            {
                deserializer->PushState(new_ref<StructState<element_t, 0, subFieldCount>>(deserializer, endian, (std::remove_cvref_t<element_t>*)(&qor_reflection::detail::sequence_tuple::get<I>(value))));  
            }
            else
            {
                //weird error, maybe terminal state
            }
            return StateSelector<I + 1, N>::Select(deserializer, endian, value, whole_value, false);
        }
    };

    template <std::size_t I>
    struct StateSelector<I, I>
    {
        template <class T, class R> static bool Select(Deserializer* deserializer, arch::Endian endian, const T& value, R* whole_value, bool recurse = true) noexcept { return false; }
    };

    template<class struct_T, std::size_t I, std::size_t N>
    class StructState : public workflow::State
    {
    public:

        StructState(Deserializer* deserializer, arch::Endian endian, std::remove_cvref_t<struct_T>* pt) : workflow::State(deserializer), m_endian(endian), m_Object(pt)
        {
            m_result = UNINITIALIZED;                  
            m_data = reinterpret_cast<byte*>(m_Object);
            constexpr std::size_t fieldCount = qor_reflection::detail::fields_count<std::remove_cvref_t<struct_T>>();

            Enter = [this]()
            {            
                if(I < fieldCount)
                {
                    StateSelector<I, fieldCount>::Select(GetDeserializer(),m_endian,qor_reflection::detail::tie_as_tuple(*m_Object), m_Object);
                }
                else
                {
                    m_result = SUCCESS;
                    Workflow()->PopState();
                }
            };

            Resume = [this]()
            {
                Workflow()->SetState(new_ref<StructState<struct_T, I + 1, N>>(GetDeserializer(), m_endian, m_Object));
            };

        }

        virtual ~StructState()
        {
            m_result = m_result;
        }            

    protected:

        class Context* GetContext()
        {
            return dynamic_cast<Deserializer*>(m_Workflow)->GetContext();
        }

        workflow::Workflow* Workflow()
        {
            return dynamic_cast<workflow::Workflow*>(m_Workflow);
        }

        class Deserializer* GetDeserializer()
        {
            return dynamic_cast<Deserializer*>(m_Workflow);
        }

        arch::Endian m_endian;//endianness of the source
        byte* m_data;
        size_t m_index;        
        ResultCode m_result;
        std::remove_cvref_t<struct_T>* m_Object;
    };

    template<class struct_T, std::size_t I>
    class StructState<struct_T, I, I> : public workflow::State    
    {
    public:

        StructState(Deserializer* deserializer, arch::Endian endian, std::remove_cvref_t<struct_T>* pt) : workflow::State(deserializer)
        {
            m_result = UNINITIALIZED;                  

            Enter = [this]()
            {            
                m_result = SUCCESS;
                Workflow()->PopState();
            };
        }

        virtual ~StructState()
        {
            m_result = m_result;
        }            

    protected:

        class Context* GetContext()
        {
            return dynamic_cast<Deserializer*>(m_Workflow)->GetContext();
        }

        workflow::Workflow* Workflow()
        {
            return dynamic_cast<workflow::Workflow*>(m_Workflow);
        }

        class Deserializer* GetDeserializer()
        {
            return dynamic_cast<Deserializer*>(m_Workflow);
        }

        ResultCode m_result;
    };

    template<class t>
    struct StructureDeserializer
    {
        typedef StructState<t, 0, qor_reflection::detail::fields_count<std::remove_cvref_t<t>>()> base;
    };

