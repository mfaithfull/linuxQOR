    ResponseGenState::ResponseGenState(HTTPResponseGenerator* responseGenerator) : State(responseGenerator)
    {
    }

    parser::Context* ResponseGenState::GetContext()
    {
        return dynamic_cast<HTTPResponseGenerator*>(m_Workflow)->GetContext();
    }

    workflow::Workflow* ResponseGenState::Workflow()
    {
        return dynamic_cast<workflow::Workflow*>(m_Workflow);
    }

    HTTPResponseGenerator* ResponseGenState::GetResponseGenerator()
    {
        return dynamic_cast<HTTPResponseGenerator*>(m_Workflow);
    }
    

    ResponseGenInitial::ResponseGenInitial(HTTPResponseGenerator* generator) : ResponseGenState(generator)
    {
        Enter = [this]()
        {            
            Workflow()->PopState();
        };

        Resume = [this]()
        {
            Workflow()->PopState();
        };
    }

    ResponseGenChar::ResponseGenChar(HTTPResponseGenerator* generator, byte character) : ResponseGenState(generator)
    {
        m_EnteredAtLeastOnce = false;

        Enter = [this, character]()
        {
            m_EnteredAtLeastOnce = true;
            std::cout << (char)character;
            GetContext()->PutOctet(character);
            Workflow()->PopState();
        };

        Resume = [this]()
        {
            if(m_EnteredAtLeastOnce)
            {
                Workflow()->PopState();
            }
        };
    }

    ResponseGenString::ResponseGenString(HTTPResponseGenerator* generator, std::string str) : ResponseGenState(generator), m_str(str)
    {        
        m_it = m_str.begin();
        m_EnteredAtLeastOnce = false;

        Enter = [this, str]()
        {
            m_EnteredAtLeastOnce = true;
            if(m_it != m_str.end())
            {                
                Workflow()->PushState(new_ref<ResponseGenChar>(GetResponseGenerator(), (byte)(*m_it)));
            }
            else
            {
                Workflow()->PopState();
            }
        };

        Resume = [this]()
        {
            if(m_EnteredAtLeastOnce == true)
            {
                ++m_it;
            }
        };
    }


    ResponseGenCRLF::ResponseGenCRLF(HTTPResponseGenerator* generator) : ResponseGenState(generator)
    {
        m_EnteredAtLeastOnce = false;
        
        Enter = [this]()
        {            
            m_EnteredAtLeastOnce = true;            
            Workflow()->PushState(new_ref<ResponseGenChar>(GetResponseGenerator(), 0x0A));//LF
            Workflow()->PushState(new_ref<ResponseGenChar>(GetResponseGenerator(), 0x0D));//CR
        };

        Resume = [this]()
        {
            if(m_EnteredAtLeastOnce)
            {
                Workflow()->PopState();
            }
        };
    };

