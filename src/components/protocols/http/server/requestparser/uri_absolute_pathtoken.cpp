#include "src/configuration/configuration.h"

#include "uri_absolute_pathtoken.h"

namespace qor { namespace components { namespace protocols { namespace http {

    void uri_absolute_path::Prepare()
    {
        std::cout << "Looking for a uri_absolute_path." << std::endl;
        GetParser()->PushNode(new_ref<UriAbsolutePathNode>());
    }

    void uri_absolute_path::Emit()
    {
        std::cout << "Emitting a uri_absolute_path." << std::endl;
        std::vector<std::string> segments;
        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = httpRequestTokenNames.find(token);
            std::string tokenName;
            if(f != httpRequestTokenNames.end())
            {
                tokenName = f->second;
            }
            
            //std::cout << tokenName << std::endl;

            if(token == static_cast<uint64_t>(httpRequestToken::segment) || 
                token == static_cast<uint64_t>(httpRequestToken::segment_nz))
            {
                auto segmentNode = node.AsRef<SegmentNode>();
                if(segmentNode.IsNotNull())
                {
                    segments.push_back(segmentNode->GetObject()->m_segment);
                }
            }
            else
            {
                continuable("Unexpected token.");
            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            auto uriNode = node.AsRef<UriAbsolutePathNode>();
            if(uriNode.IsNotNull())
            {
                uriNode->GetObject()->m_segments = segments;
                std::cout << "UriAbsolutePathNode " << segments.size() << " elements." << std::endl;
            }
            GetParser()->PushNode(node);
        }
    }

    void uri_absolute_path::Fail()
    {
        auto node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

}}}}//qor::components::protocols::http