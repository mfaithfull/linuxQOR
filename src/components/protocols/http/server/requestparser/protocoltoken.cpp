#include "src/configuration/configuration.h"

#include <string>

#include "protocoltoken.h"

namespace qor { namespace components { namespace protocols { namespace http {

    void protocol::Prepare()
    {
        std::cout << "Looking for a protocol." << std::endl;
        GetParser()->PushNode(new_ref<ProtocolNode>());
    }

    void protocol::Emit()
    {
        std::cout << "Emitting a protocol." << std::endl;
        std::string name;
        std::string protocolVersion;

        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = httpRequestTokenNames.find(token);
            std::string tokenName;
            if(f != httpRequestTokenNames.end())
            {
                tokenName = f->second;
            }
            
            if(token == static_cast<uint64_t>(httpRequestToken::protocol_name))
            {
                auto nameNode = node.AsRef<ProtocolNameNode>();
                if(nameNode.IsNotNull())
                {
                    name = nameNode->GetObject()->m_name;
                }
            }
            else if(token == static_cast<uint64_t>(httpRequestToken::protocol_version))
            {
                auto protocolVersionNode = node.AsRef<ProtocolVersionNode>();
                if(protocolVersionNode.IsNotNull())
                {
                    protocolVersion = protocolVersionNode->GetObject()->m_version;
                }
            }
            else
            {
                continuable("Unexpected token.");
            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            auto requestLineNode = node.AsRef<ProtocolNode>();
            if(requestLineNode.IsNotNull())
            {
                requestLineNode->GetObject()->m_protocolName = name;
                requestLineNode->GetObject()->m_protocolVersion = protocolVersion;
            }
            GetParser()->PushNode(node);
        }
    }

    void protocol::Fail()
    {
        auto node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

}}}}//qor::components::protocols::http