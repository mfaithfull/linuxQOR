#ifndef QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUEST
#define QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUEST

#include <string>
#include <vector>
#include <chrono>

#include <cstdint>

#include "params.h"
#include "../headers/headers.h"
#include "progress.h"
#include "content.h"

namespace qor { namespace components { namespace protocols { namespace http {
    
    class HTTPRequest
    {
    public:

        HTTPRequest() = default;
        virtual ~HTTPRequest() = default;

        void SetMethod(const std::string& method)
        {
            m_method = method;
        }

        const std::string& GetMethod() const
        {
            return m_method;
        }

        void SetPath(const std::string& path)
        {
            m_path = path;            
        }

        const std::string& GetPath() const
        {
            return m_path;
        }

        std::string GetURI() const
        {
            return m_path;//TODO: URI object ?
        }

        void SetParams(const Params& params)
        {
            m_params = params;
        }

        const Params& GetParams()
        {
            return m_params;
        }

        void SetHeaders(const Headers& headers)
        {
            m_headers = headers;
        }

        const Headers& GetHeaders()
        {
            return m_headers;
        }

        void SetTrailers(const Headers& trailers)
        {
            m_trailers = trailers;
        }

        const Headers& GetTrailers()
        {
            return m_trailers;
        }

        void SetBody(const std::string& body)
        {
            m_body = body;
        }

        const std::string& GetBody()
        {
            return m_body;
        }

        void AddAcceptContentType(const std::string& acceptContentType)
        {
            m_acceptContentTypes.push_back(acceptContentType);
        }

        bool HasHeader(const std::string &key) const
        {
            return m_headers.find(key) != m_headers.end();
        }
        
        bool HasTrailer(const std::string &key) const
        {
            return m_trailers.find(key) != m_trailers.end();
        }

        bool HasParam(const std::string &key) const 
        {
            return m_params.find(key) != m_params.end();
        }

        std::string GetHeaderValue(const std::string &key, const char* def = "", size_t id = 0) const
        {
            auto rng = m_headers.equal_range(key);
            auto it = rng.first;
            std::advance(it, static_cast<int64_t>(id));
            if (it != rng.second) 
            { 
                return it->second.c_str(); 
            }
            return def;
        }

        std::string GetTrailerValue(const std::string &key, const char* def = "", size_t id = 0) const
        {
            auto rng = m_trailers.equal_range(key);
            auto it = rng.first;
            std::advance(it, static_cast<int64_t>(id));
            if (it != rng.second) 
            { 
                return it->second.c_str(); 
            }
            return def;
        }

        std::string GetParamValue(const std::string &key, size_t id) const 
        {
            auto rng = m_params.equal_range(key);
            auto it = rng.first;
            std::advance(it, static_cast<int64_t>(id));
            if (it != rng.second) 
            { 
                return it->second; 
            }
            return std::string();
        }

        void AddHeader(const std::string &key, const std::string &value)
        {
            m_headers.emplace(key,value);
        }

        void AddTrailer(const std::string &key, const std::string &value)
        {
            m_trailers.emplace(key,value);
        }

        size_t GetHeaderValueCount(const std::string &key) const 
        {
            auto r = m_headers.equal_range(key);
            return static_cast<size_t>(std::distance(r.first, r.second));
        }

        size_t GetTrailerValueCount(const std::string &key) const 
        {
            auto r = m_trailers.equal_range(key);
            return static_cast<size_t>(std::distance(r.first, r.second));
        }

        size_t GetParamValueCount(const std::string &key) const 
        {
            auto r = m_params.equal_range(key);
            return static_cast<size_t>(std::distance(r.first, r.second));
        }

        bool IsMultipartFormData() const 
        {
            const auto &content_type = GetHeaderValue("Content-Type");
            return !content_type.rfind("multipart/form-data", 0);
        }

        static std::string AppendQueryParams(const std::string &path, const qor::components::protocols::http::Params &params);
        static std::string ParamsToQueryStr(const qor::components::protocols::http::Params &params);
        static std::string EncodeQueryComponent(const std::string &component, bool space_as_plus = true);

    private:
        
        std::string m_method;
        //TODO: Add a proper URI object for the Request-URI
        std::string m_path;
        Params m_params;
        Headers m_headers;
        Headers m_trailers;
        std::string m_body;
        std::vector<std::string> m_acceptContentTypes;
        //ResponseHandler response_handler;
        ContentReceiverWithProgress content_receiver;
        DownloadProgress m_downloadProgress;
        
        UploadProgress m_uploadProgress;
        size_t m_maxRedirectCount = 1;
        size_t m_contentLength = 0;
        ContentProvider m_contentProvider;
        size_t m_authorizationCount = 0;
        std::chrono::time_point<std::chrono::steady_clock> m_startTime = (std::chrono::steady_clock::time_point::min)();

    };

}}}}//qor::components::protocols::http

#endif//QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUEST