#ifndef QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUESTPARSER_REQUESTCHARTOKEN
#define QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUESTPARSER_REQUESTCHARTOKEN

#include "src/components/parser/state.h"
#include "src/components/parser/rfc5234.h"
#include "tokendefs.h
#include "src/components/parser/oneormore.h"
#include "src/components/parser/nodes/char.h"

namespace qor { namespace components { namespace protocols { namespace http {

    class requestChar : public qor::components::parser::OneOfARange
    {
    public: requestChar(qor::components::parser::Parser* parser) :
                qor::components::parser::OneOfARange(parser,
                0x00,
                0xFF,
                static_cast<uint64_t>(httpRequestToken::requestChar))
            {}
        
        virtual void Emit()
        {
            char charValue = m_result.first;
            GetParser()->PushNode(
                qor::new_ref<qor::components::parser::Char>(
                    charValue,static_cast<uint64_t>(httpRequestToken::requestChar)
                ).template AsRef<qor::components::parser::Node>()
            );
        }
    };
}}}}//qor::components::protocols::http

#endif//QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUESTPARSER_REQUESTCHARTOKEN