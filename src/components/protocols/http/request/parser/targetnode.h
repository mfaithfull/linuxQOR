#ifndef QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUESTPARSER_TARGETNODE
#define QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUESTPARSER_TARGETNODE

#include "src/components/parser/state.h"
#include "src/components/parser/rfc5234.h"
#include "tokendefs.h"
#include "src/components/parser/oneormore.h"
#include "src/components/parser/nodes/char.h"
#include "uri_origin_formnode.h"
#include "uri_absolute_formnode.h"
#include "uri_authority_formnode.h"

namespace qor { namespace components { namespace protocols { namespace http {

    enum uriForm
    {
        form_origin,
        form_absolute,
        form_authority,
        form_asterisk
    };

    struct Target
    {         
        uriForm m_form;
        UriOriginForm m_originURI;
        UriAbsoluteForm m_absoluteURI;
        UriAuthorityForm m_authorityURI;        
    };

    class TargetNode : public parser::NodeAdapter<Target>
    {
    public:

        TargetNode() : parser::NodeAdapter<Target>(static_cast<uint64_t>(httpRequestToken::target))
        {
        }

        TargetNode(ref_of<Target>::type target) : parser::NodeAdapter<Target>(target, static_cast<uint64_t>(httpRequestToken::target))
        {
        }

        virtual ~TargetNode() = default;

    };

}}}}

#endif//QOR_PP_H_COMPONENTS_PROTOCOLS_HTTP_REQUESTPARSER_TARGETNODE