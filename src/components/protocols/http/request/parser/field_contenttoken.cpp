#include "src/configuration/configuration.h"

#include "field_contenttoken.h"

namespace qor { namespace components { namespace protocols { namespace http {

    void field_content::Prepare()
    {
        //std::cout << "Looking for a field_content." << std::endl;
        GetParser()->PushNode(new_ref<FieldContentNode>());
    }

    void field_content::Emit()
    {
        //std::cout << "Emitting a field_content." << std::endl;
        std::string content;
        auto node = GetParser()->PopNode();
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = httpRequestTokenNames.find(token);
            std::string tokenName;
            if(f != httpRequestTokenNames.end())
            {
                tokenName = f->second;
            }
            
            //std::cout << tokenName << std::endl;

            if(token == static_cast<uint64_t>(httpRequestToken::field_vchar))
            {
                auto charNode = node.AsRef<FieldVCharNode>();
                char c = charNode->GetObject()->m_value;
                content = std::string(&c,1) + content;
            }
            else
            {
                char c = '?';
                content = std::string(&c,1) + content;
            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            auto contentNode = node.AsRef<FieldContentNode>();
            if(contentNode.IsNotNull())
            {
                contentNode->GetObject()->m_content = content;
                //std::cout << "Content: " << content << std::endl;
            }
            GetParser()->PushNode(node);
        }                        
    }

    void field_content::Fail()
    {
        auto node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

}}}}//qor::components::protocols::http