#include "src/configuration/configuration.h"

#include <string>

#include "protocolnametoken.h"

namespace qor { namespace components { namespace protocols { namespace http {

    void protocol_name::Prepare()
    {
        //std::cout << "Looking for a protocol name." << std::endl;
        GetParser()->PushNode(new_ref<ProtocolNameNode>());
    }

    void protocol_name::Emit()
    {
        //std::cout << "Emitting a protocol name." << std::endl;
        auto node = GetParser()->PopNode();
        std::string name;
        while(node.IsNotNull() && node->GetToken() != m_token)
        {
            uint64_t token = node->GetToken();
            auto f = httpRequestTokenNames.find(token);
            std::string tokenName;
            if(f != httpRequestTokenNames.end())
            {
                tokenName = f->second;
            }
            
            //std::cout << tokenName << std::endl;

            if(token == static_cast<uint64_t>(httpRequestToken::tchar))
            {
                auto tcharNode = node.AsRef<TCharNode>();
                char c = tcharNode->GetObject()->m_char;
                name = std::string(&c,1) + name;
            }
            node = GetParser()->PopNode();
        }

        if(node.IsNotNull())
        {
            auto nameNode = node.AsRef<ProtocolNameNode>();
            if(nameNode.IsNotNull())
            {
                nameNode->GetObject()->m_name = name;
                std::cout << "Protocol Name: " << name << std::endl;
            }
            GetParser()->PushNode(node);
        }
    }

    void protocol_name::Fail()
    {
        ref_of<parser::Node>::type node = GetParser()->PopNode();
        if(node.IsNotNull() && node->GetToken() != m_token)
        {
            GetParser()->PushNode(node);
        }
    }

}}}}//qor::components::protocols::http