    bool HTTPSource::Push(size_t& unitsRead, size_t unitsToRead)
    {
        if( GetFlowMode() == FlowMode::Push )
        {
            return ActualSink()->Write(unitsRead, unitsToRead) && (unitsRead > 0) ? true : false;
        }
        return true;
    }

