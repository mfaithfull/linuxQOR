    GeneratorState::GeneratorState(Generator* generator) : workflow::State(generator)
    {
        m_result.code = Result::UNINITIALIZED;

        Leave = [this]()
        {
            if(m_result.code == Result::FAILURE)
            {
                Fail();
            }
            else if(m_result.code == Result::SUCCESS)
            {
                Emit();
            }
            else if(m_result.code == Result::MORE_DATA)
            {
                std::cout << "Ran out of space before we could generate. Reenter with more space to try again." << std::endl;
            }
        };
    }

    void GeneratorState::Reset()
    {
        m_result.code = Result::MORE_DATA;
    }

    Context* GeneratorState::GetContext()
    {
        return dynamic_cast<Generator*>(m_Workflow)->GetContext();
    }

    workflow::Workflow* GeneratorState::Workflow()
    {
        return dynamic_cast<workflow::Workflow*>(m_Workflow);
    }

    Generator* GeneratorState::GetGenerator()
    {
        return dynamic_cast<Generator*>(m_Workflow);
    }
    
    void GeneratorState::Prepare()
    {
    }
    
    void GeneratorState::Emit()
    {
    }

    void GeneratorState::Fail()
    {        
        //Must undo anything done in Prepare
    }
    
    ASCIIChar::ASCIIChar(Generator* generator, byte asciiChar) : GeneratorState(generator),
        m_acsiiChar(asciiChar)
    {
        Enter = [this]()
        {
            Prepare();

            if(GetContext()->PutOctet(m_acsiiChar))
            {
                m_result.code = Result::SUCCESS;
                Workflow()->PopState();
            }
            else
            {
                Fail();
                m_result.code = Result::MORE_DATA;
                return;//Need more space to continue
            }            
        };
    }

