#ifndef QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_HEADTABLE
#define QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_HEADTABLE

#include <assert.h>
#include <type_traits>
#include "../blobbuffer.h"
#include "../ttfstructures.h"

namespace qor{ namespace res { namespace ttf {

    class HeadTable
    {
    public:

        HeadTable(const BlobBuffer& blob) : m_blob(blob)
        {
            Init();
        }

        HeadTable(BlobBuffer&& blob) : m_blob(std::move(blob))
        {
            Init();
        }

        HeadTable(const HeadTable& src) : m_blob(src.m_blob)
        {
            m_table = src.m_table;
        }

        HeadTable(HeadTable&& src) : m_blob(std::move(src.m_blob)), m_table(std::move(src.m_table)){ }

        ~HeadTable() = default;

        int16_t IndexToLocFormat()
        {
            return m_table.indexToLocFormat;
        }

        uint16_t GetFlags()
        {
            return m_table.flags;
        }

        uint16_t GetUnitsPerEm()
        {
            return m_table.unitsPerEm;
        }

    private:

        void Init()
        {
            m_blob.SetCursor(0);
            m_table = m_blob.ReadStructure<head_table>();
            assert(m_table.magicNumber == 0x5f0f3cf5);
            m_blob.Reset(0);
        }

        head_table m_table;
        BlobBuffer m_blob;
    };

}}}//qor::res::ttf

#endif//QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_BLOBBUFFER