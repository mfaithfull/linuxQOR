#ifndef QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_CMAPTABLE
#define QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_CMAPTABLE

#include <assert.h>
#include <type_traits>
#include "blobbuffer.h"
#include "ttfstructures.h"

namespace qor{ namespace res { namespace ttf {

    class CmapTable
    {
    public:

        CmapTable(const BlobBuffer& blob) : m_blob(blob)
        {
            Init();
        }

        CmapTable(BlobBuffer&& blob) : m_blob(std::move(blob))
        {
            Init();
        }

        CmapTable(const CmapTable& src) = delete;

        CmapTable(CmapTable&& src) : 
            m_blob(std::move(src.m_blob)), 
            m_subTables(std::move(src.m_subTables)), 
            m_format(std::move(src.m_format)),
            m_format4(src.m_format4),
            m_endCode(src.m_endCode),
            m_startCode(src.m_startCode),
            m_idDelta(src.m_idDelta),
            m_idRangeOffset(src.m_idRangeOffset),
            m_glyphIdArray(src.m_glyphIdArray)
        { 
            src.m_endCode = nullptr;
            src.m_startCode = nullptr;
            src.m_idDelta = nullptr;
            src.m_idRangeOffset = nullptr;
            src.m_glyphIdArray = nullptr;
            src.m_format4.Dispose();
        }

        ~CmapTable()
        {
            delete [] m_endCode;
            delete [] m_startCode;
            delete [] m_idDelta;
            delete [] m_idRangeOffset;
            delete [] m_glyphIdArray;
        }

        uint32_t GetGlyphIndex(uint32_t codePoint)
        {
            if(m_format == 4)
            {                        
                int index = -1;
                const uint16_t* ptr = nullptr;
                for(int i = 0; i < m_format4->segCountX2/2; i++) 
                {
                    if(m_endCode[i] > codePoint) {index = i; break;};
                }

                if(index == -1) return 0;

                if(m_startCode[index] < codePoint) 
                {
                    if(m_idRangeOffset[index] != 0) 
                    {
                        ptr = m_idRangeOffset + index + m_idRangeOffset[index]/2;
                        ptr += codePoint - m_startCode[index];
                        if(*ptr == 0) return 0;
                        return (*ptr + m_idDelta[index]) % 65536;
                    } 
                    else 
                    {
                        return (codePoint + m_idDelta[index]) % 65536;
                    }
                }
            }
            return 0;
        }        

    private:

        void AllocateIndexes(uint16_t size, uint16_t remaining)
        {
            m_endCode = new uint16_t[size];
            m_startCode = new uint16_t[size];
            m_idDelta = new uint16_t[size];
            m_idRangeOffset = new uint16_t[size];        
            m_glyphIdArray = new uint16_t[remaining];
        }

        void InitFormat4(uint16_t length, uint16_t remaining)
        {       
            int segIndex = 0;
            for(segIndex = 0; segIndex < m_format4->segCountX2 / 2; segIndex++)
            {
                m_endCode[segIndex] = m_blob.Read<uint16_t>();
            }            
            for(segIndex = 0; segIndex < m_format4->segCountX2 / 2; segIndex++)
            {
                m_startCode[segIndex] = m_blob.Read<uint16_t>();
            }
            for(segIndex = 0; segIndex < m_format4->segCountX2 / 2; segIndex++)
            {
                m_idDelta[segIndex] = m_blob.Read<uint16_t>();
            }
            for(segIndex = 0; segIndex < m_format4->segCountX2 / 2; segIndex++)
            {
                m_idRangeOffset[segIndex] = m_blob.Read<uint16_t>();
            }
                        
            for(segIndex = 0; segIndex < remaining; segIndex++)
            {
                m_glyphIdArray[segIndex] = m_blob.Read<uint16_t>();
            }
        }

        void InitUnicodeSubTable(cmap_sub_table subTable)
        {
            switch( subTable.platformSpecificId )
            {
                case 0://	Version 1.0 semantics
                break;
                case 1://	Version 1.1 semantics
                break;
                case 2://	ISO 10646 1993 semantics (deprecated)
                break;
                case 3://	Unicode 2.0 or later semantics (BMP only)
                break;
                case 4://	Unicode 2.0 or later semantics (non-BMP characters allowed)
                break;
                case 5://	Unicode Variation Sequences
                break;
                case 6://	Last Resort
                break;
                default:
                break;
            }

            m_blob.SetCursor(subTable.offset);
            auto subTableHeader = m_blob.ReadStructure<cmap_sub_table_header>();
            switch(subTableHeader.format)
            {
                case 0:
                break;
                case 1:
                break;
                case 2:
                break;
                case 3:
                break;
                case 4:
                {
                    m_format = 4;                    
                    m_format4 = new_ref<cmap_format4>(m_blob.ReadStructure<cmap_format4>());

                    int remaining = (subTableHeader.length/2) - ( 
                                        ( sizeof(cmap_sub_table_header) / 2) +
                                        ( sizeof(cmap_format4) / 2 ) + 
                                        ( m_format4->segCountX2 * 2 ) );

                    AllocateIndexes(m_format4->segCountX2 / 2, remaining);                    
                    InitFormat4(subTableHeader.length, remaining);
                }
                break;
                case 5:
                break;
                case 6:
                break;
                case 7:
                break;
                case 8:
                break;
                default:
                break;
            }
        }

        void Init()
        {
            m_blob.SetCursor(0);
            auto header = m_blob.ReadStructure<cmap_table_header>();
            for( int index = 0; index < header.subtables; index ++ )
            {
                auto subTableEntry = m_blob.ReadStructure<cmap_sub_table>();
                m_subTables.push_back(subTableEntry);
            }

            for( auto subTable : m_subTables )
            {
                switch(subTable.platformId)
                {
                    case 0:
                        InitUnicodeSubTable(subTable);
                    break;
                    case 1:
                        //InitMacSubTable(subTable);
                    break;
                    case 3:
                        //InitMicrosoftSubTable(subTable);
                    break;
                    default:
                    break;
                }
            }
            m_blob.Reset(0);
        }

        
        BlobBuffer m_blob;
        std::vector<cmap_sub_table> m_subTables;
        uint16_t m_format;
        ref_of<cmap_format4>::type m_format4;
        uint16_t* m_endCode{nullptr};
        uint16_t* m_startCode{nullptr};
        uint16_t* m_idDelta{nullptr};
        uint16_t* m_idRangeOffset{nullptr};
        uint16_t* m_glyphIdArray{nullptr};
    };

}}}//qor::res::ttf

#endif//QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_BLOBBUFFER