#ifndef QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_LOCATABLE
#define QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_LOCATABLE

#include <assert.h>
#include <type_traits>
#include "../blobbuffer.h"
#include "../ttfstructures.h"

namespace qor{ namespace res { namespace ttf {

    class LocaTable
    {
    public:

        LocaTable(const BlobBuffer& blob, int16_t format) : m_blob(blob), m_format(format)
        {
            Init();
        }

        LocaTable(BlobBuffer&& blob, int16_t&& format) : m_blob(std::move(blob)), m_format(format)
        {
            Init();
        }

        LocaTable(const LocaTable& src) :
            m_blob(src.m_blob),
            m_format(src.m_format),
            m_glyphCount(src.m_glyphCount)
            { }

        LocaTable(LocaTable&& src) :
            m_blob(std::move(src.m_blob)),
            m_format(std::move(src.m_format)),
            m_glyphCount(std::move(src.m_glyphCount))
            { }

        ~LocaTable() = default;

        uint32_t GetGlyphOffset(size_t index)
        {
            if(m_format == 1)
            {
                uint32_t result;
                m_blob.SetCursor( index * 4 );
                m_blob >> result;
                return result;
            }
            else
            {
                uint16_t result;
                m_blob.SetCursor( index * 2);
                m_blob >> result;
                return result * 2;
            }
        }

        size_t GlyphCount()
        {
            return m_glyphCount;
        }

    private:

        void Init()
        {
            m_glyphCount = (m_format == 1) ? m_blob.Size() / 4 : m_blob.Size() / 2;
        }

        BlobBuffer m_blob;
        int16_t m_format{0};
        size_t m_glyphCount;
    };

}}}//qor::res::ttf

#endif//QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_LOCATABLE