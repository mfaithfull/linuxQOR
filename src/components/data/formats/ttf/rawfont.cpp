#include "src/configuration/configuration.h"
#include "rawfont.h"

namespace qor{ namespace res { namespace ttf {

    RawFont::RawFont(const std::string& fileName) : m_fileName (fileName){ }

    BlobBuffer RawFont::GetTable(uint32_t tag)
    {
        auto it = m_tables.find(tag);
        if(it != m_tables.end())
        {
            return it->second;
        }
        return BlobBuffer();
    }

    void RawFont::Init()
    {
        auto blob = GetTable(head_tag);
        if(blob.Size())
        {
            m_headTable = new_ref<HeadTable>(blob);
        }
        blob = GetTable(cmap_tag);
        if(blob.Size())
        {
            m_cmapTable = new_ref<CmapTable>(blob);
        }
        blob = GetTable(loca_tag);
        if(blob.Size())
        {
            m_locaTable = new_ref<LocaTable>(blob, m_headTable->IndexToLocFormat());
        }
        blob = GetTable(glyf_tag);
        if(blob.Size())
        {
            m_glyfTable = new_ref<GlyfTable>(blob);
        }
        blob = GetTable(hhea_tag);
        if(blob.Size())
        {
            m_hheaTable = new_ref<HheaTable>(blob);
        }
        blob = GetTable(hmtx_tag);
        if(blob.Size())
        {
            m_hmtxTable = new_ref<HmtxTable>(blob);
        }
        blob = GetTable(kern_tag);
        if(blob.Size())
        {
            m_kernTable = new_ref<KernTable>(blob);
        }
        blob = GetTable(gpos_tag);
        if(blob.Size())
        {
            m_gposTable = new_ref<GposTable>(blob);
        }
    }
    
}}}//qor::res::ttf
