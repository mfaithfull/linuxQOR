    constexpr inline uint32_t make_tag(unsigned char c1, unsigned char c2, unsigned char c3, unsigned char c4){ 
        return ((uint32_t)((((uint32_t)(c1)&0xFF)<<24)|(((uint32_t)(c2)&0xFF)<<16)|(((uint32_t)(c3)&0xFF)<<8)|((uint32_t)(c4)&0xFF)));
    }

    inline String untag(uint32_t t)
    { 
        char8_t a[4]{(char8_t)(((t)>>24)&0xFF),(char8_t)(((t)>>16)&0xFF),(char8_t)(((t)>>8)&0xFF),(char8_t)((t)&0xFF)}; 
        String s(a); 
        return s;
    }

    constexpr uint32_t head_tag = make_tag('h','e','a','d');
    constexpr uint32_t cmap_tag = make_tag('c','m','a','p');
    constexpr uint32_t loca_tag = make_tag('l','o','c','a');
    constexpr uint32_t hhea_tag = make_tag('h','h','e','a');
    constexpr uint32_t hmtx_tag = make_tag('h','m','t','x');
    constexpr uint32_t glyf_tag = make_tag('g','l','y','f');
    constexpr uint32_t kern_tag = make_tag('k','e','r','n');
    constexpr uint32_t gpos_tag = make_tag('g','p','o','s');

    struct offset_subtable
    {
        uint32_t	scaler_type; //0x74727565 ('true' as chars) or 0x00010000 for TTF   0x4F54544F ('OTTO' as chars) means its an OpenType with PostScript outlines  0x74797031 ('typ1' as chars) is type 1 postscript font files which was made by adobe in the 80s
        uint16_t	numTables;
        uint16_t	searchRange;
        uint16_t	entrySelector;
        uint16_t	rangeShift;
    };

    struct table_directory
    {
        uint32_t    tag;
        uint32_t	checkSum;
        uint32_t	offset;
        uint32_t	length;
    };

    struct cmap_header
    {
	    uint16_t version;
	    uint16_t numberSubtables;
    };


    struct font_directory
    {
        offset_subtable off_sub;
        table_directory* tbl_dir;
    };

    union fixed_t
    {
        uint32_t uintval;
    };

    union longDateTime
    {
        int64_t intval;
    };

    union fword
    {
        int16_t intval;
    };

    struct head_table
    {
        fixed_t version;
        fixed_t fontRevision;
        uint32_t checkSumAdjustment;
        uint32_t magicNumber;
        uint16_t flags;
        uint16_t unitsPerEm;
        longDateTime created;
        longDateTime modified;
        fword xMin;
        fword yMin;
        fword xMax;
        fword yMax;
        uint16_t macStyle;
        uint16_t lowestRecPPEM;
        int16_t fontDirectionHint;
        int16_t indexToLocFormat;
        int16_t glyphDataFormat;
    };

    struct glyph
    {
        int16_t contours;
        fword xMin;
        fword yMin;
        fword xMax;
        fword yMax;        
    };

    struct glyph_header
    {
        int16_t contours;
        fword xMin;
        fword yMin;
        fword xMax;
        fword yMax;        
    };

    struct cmap_table_header
    {
        uint16_t version;
        uint16_t subtables;
    };

    struct cmap_sub_table
    {
        uint16_t platformId;
        uint16_t platformSpecificId;
        uint32_t offset;
    };

    struct cmap_sub_table_header
    {
        uint16_t format;
        uint16_t length;
        uint16_t language;
    };

    struct cmap_format4
    {
        uint16_t  segCountX2;
        uint16_t  searchRange;
        uint16_t  entrySelector;
        uint16_t  rangeShift;
        uint16_t  reservedPad;
    };
