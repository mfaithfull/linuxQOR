#ifndef QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_BLOBBUFFER
#define QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_BLOBBUFFER

#include <cstdint>
#include <string.h>
#include <stdexcept>
#include "src/platform/compiler/compiler.h"
#include "src/qor/reflection/reflection.h"

namespace qor{ namespace res { namespace ttf {

    class BlobBuffer
    {
    public:

        BlobBuffer(size_t byteCount = 0) : m_size(byteCount)
        {
            Allocate(m_size);
        }

        BlobBuffer(const BlobBuffer& src)
        {
            m_size = src.Size();
            Allocate(m_size);
            memcpy(m_memory, src(), m_size);
            m_contentByteOrder = src.m_contentByteOrder;
        }

        BlobBuffer& operator = (const BlobBuffer& src)
        {
            if(&src != this)
            {
                m_size = src.Size();
                Allocate(m_size);
                memcpy(m_memory, src(), m_size);
                m_contentByteOrder = src.m_contentByteOrder;
            }
            return *this;
        }

        BlobBuffer(BlobBuffer&& src)
        {
            Free();
            m_size = src.m_size;
            m_memory = src.m_memory;
            m_contentByteOrder = src.m_contentByteOrder;
            src.m_memory = nullptr;
            src.m_size = 0;
        }

        BlobBuffer& operator = (BlobBuffer&& src)
        {
            if(&src != this)
            {
                Free();
                m_size = src.m_size;
                m_memory = src.m_memory;
                src.m_memory = nullptr;
                src.m_size = 0;
                m_contentByteOrder = src.m_contentByteOrder;
            }
            return *this;
        }

        ~BlobBuffer()
        {
            if(Overrun())
            {
                std::terminate();
            }
            Free();
        }

        inline const byte* operator()() const
        {
            return m_memory;
        }

        inline byte* data()
        {
            return m_memory;
        }

        inline size_t Size() const
        {
            return m_size;
        }

        template<class T>
        T& As(size_t position = 0)
        {
            SetCursor(position);
            if(sizeof(T) <= (m_size - m_cursor))
            {
                return *(reinterpret_cast<T*>(m_memory + m_cursor));
            }
            throw std::logic_error("T is larger than BlobBuffer. Can't cast a BlobBuffer to a type larger than the buffer.");
        }

        template <typename T> requires std::is_arithmetic_v<T>
        T Read()
        {
            T t{0};
            if((m_size > m_cursor) && (m_size - m_cursor >= sizeof(T)))
            {
                if(arch::endian != m_contentByteOrder)
                {   
                    byte* space = reinterpret_cast<byte*>(&t);
                    //Write the bytes from memory in reverse to flip endianness (e.g. Big/Network to Little/Host on x86)
                    for(int index = 0; index < sizeof(T); ++index)
                    {
                        space[index] = m_memory[ m_cursor + sizeof(T) - (index + 1) ];
                    }
                }
                else
                {                    
                    memcpy(&t, m_memory + m_cursor, sizeof(T));
                }
                
                m_cursor += sizeof(T);
            }
            return t;
        }

        bool Read(byte* data, size_t byteCount)
        {
            if((m_size > m_cursor) && (m_size - m_cursor >= byteCount))
            {
                memcpy(data, m_memory + m_cursor, byteCount);
                m_cursor += byteCount;
                return true;
            }
            return false;
        }

        template <typename T> requires std::is_standard_layout_v<T> && std::is_trivially_copyable_v<T>
        inline BlobBuffer& operator >> (T& item)
        {
            ExtractValue(sizeof(T), reinterpret_cast<byte*>(&item));
            return *this;
        }

        template <typename T> requires std::is_standard_layout_v<T> && std::is_trivially_copyable_v<T>
        inline T ReadStructure()
        {
			T result{0};
			qor_reflection::for_each_field(result, [this](auto& value, std::size_t /*i*/)
				{
                    *this >> (value);
				}
			);
            return result;
        }

        template <typename T> requires std::is_standard_layout_v<T> && std::is_trivially_copyable_v<T>
        inline T& RewriteEndianInPlaceAs(size_t position)
        {
            if(m_contentByteOrder != arch::endian)
            {
                SetCursor(position);
                auto t = ReadStructure<T>();
                SetCursor(position);
                memcpy(m_memory + m_cursor, &t, sizeof(T));
            }
            m_contentByteOrder = arch::Endian::host;   
            return As<T>(position);
        }        

        bool SetCursor(size_t position)
        {
            if(position < m_size)
            {
                m_cursor = position;
                return true;
            }
            return false;
        }

        size_t GetCursor()
        {
            return m_cursor;
        }

        void Reset(size_t byteCount)
        {
            m_contentByteOrder = arch::Endian::network;
            Allocate(byteCount);
        }

    private:

        void ExtractValue(size_t size, byte* space)
        {
            if(arch::endian != m_contentByteOrder)
            {   
                //Write the bytes from data in reverse to flip endianness (e.g. Big/Network to Little/Host on x86)
                for(int index = 0; index < size; ++index)
                {
                    space[index] = m_memory[ m_cursor + size - (index + 1) ];
                }
            }
            else
            {                
                memcpy(space, m_memory + m_cursor, size);            
            }
            m_cursor += size;
        }

        inline bool Overrun()
        {
            return (m_memory) && (*(reinterpret_cast<uint64_t*>(m_memory + m_size)) != 0xAAAAAAAAAAAAAAAA);
        }

        void Allocate(size_t size)
        {
            Free();
            m_size = size;
            if(m_size)
            {
                m_memory = new byte[m_size + 8];
                memset(m_memory, 0xAA, m_size + 8);
            }
        }

        void Free()
        {
            if(m_memory)
            {
                delete [] m_memory;
                m_memory = nullptr;
            }
            m_size = 0;
            m_cursor = 0;
        }

        byte* m_memory{nullptr};
        size_t m_size{0};
        size_t m_cursor{0};
        arch::Endian m_contentByteOrder{arch::Endian::network};
    };

}}}//qor::res::ttf

#endif//QOR_PP_H_COMPONENTS_DATA_FORMATS_TTF_BLOBBUFFER