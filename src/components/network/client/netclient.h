namespace qor{ namespace components {

    class BaseClient
    {
    public:

        BaseClient(qor::ref_of<qor::pipeline::Plug>::type connector) : m_connector(connector)
        {
            m_requestPipeline = new_ref<qor::pipeline::Pipeline>();
            m_responsePipeline = new_ref<qor::pipeline::Pipeline>();
        }

        bool Connect()
        {
            qor_pp_ofcontext;
            //m_requestPipeline->SetSinkConnector(m_connector);  
            //m_responsePipeline->SetSourceConnector(m_connector);
            return m_connector->Connect();
        }

        bool Send()
        {
            qor_pp_ofcontext;
            m_requestPipeline->ResetStream();
            return m_requestPipeline->PumpSome() > 0 ? true : false;
        }

        bool Receive()
        {
            qor_pp_ofcontext;
            m_responsePipeline->ResetStream();
            return m_responsePipeline->PumpSome() > 0 ? true : false;
        }

    protected:
            
        qor::ref_of<qor::pipeline::Pipeline>::type m_requestPipeline;
        qor::ref_of<qor::pipeline::Pipeline>::type m_responsePipeline;

    private:

        qor::pipeline::Plug* m_connector;

    };

    class NetworkClient : public BaseClient
    {
    public:

        NetworkClient() :
            BaseClient(new_ref<qor::components::SocketClientConnector>())
        {                        
        }

        NetworkClient(qor::ref_of<pipeline::Protocol>::type protocol) : 
            BaseClient(new_ref<qor::components::SocketClientConnector>()),
            m_protocol(protocol)
        {
        }


        void Configure(const std::string &host, const std::string &ip, int port, qor::network::sockets::eAddressFamily address_family, qor::network::addrinfo_flags socket_flags, bool tcp_nodelay, bool ipv6_v6only, time_t timeout_sec)
        {
            m_socketClientConnector->Configure(host, ip, port, address_family, socket_flags, tcp_nodelay, ipv6_v6only, timeout_sec);
        }

        bool Connect(const std::string &host, const std::string &ip, int port, qor::network::sockets::eAddressFamily address_family, qor::network::addrinfo_flags socket_flags, bool tcp_nodelay, bool ipv6_v6only, time_t timeout_sec)
        {
            qor_pp_ofcontext;
            m_requestPipeline->SetSink(m_socketClientConnector->GetSink());        
            m_responsePipeline->SetSource(m_socketClientConnector->GetSource());
            m_socketClientConnector->Configure(host, ip, port, address_family, socket_flags, tcp_nodelay, ipv6_v6only, timeout_sec);
            return BaseClient::Connect();
        }

        bool Connect()
        {
            qor_pp_ofcontext;
            if(m_protocol.IsNull())
            {
                serious("No protocol set for NetworkClient connection.");
                return false;
            }
            else
            {
                m_requestPipeline->SetSource(m_protocol->GetSource());
                m_requestPipeline->SetSink(m_socketClientConnector->GetSink());
                m_responsePipeline->SetSink(m_protocol->GetSink());
                m_responsePipeline->SetSource(m_socketClientConnector->GetSource());
                return BaseClient::Connect();
            }
        }

        void SetProtocol(qor::ref_of<qor::pipeline::Protocol>::type protocol)
        {
            m_protocol = protocol;
        }

        bool Send()
        {
            qor_pp_ofcontext;
            m_requestPipeline->ResetStream();
            return m_requestPipeline->PumpSome() > 0 ? true : false;
        }

        bool Receive()
        {
            qor_pp_ofcontext;
            m_responsePipeline->ResetStream();
            return m_responsePipeline->PumpSome() > 0 ? true : false;
        }

    protected:
    
        qor::ref_of<qor::components::SocketClientConnector>::type m_socketClientConnector;
        qor::ref_of<qor::pipeline::Protocol>::type m_protocol;

    };

}}//qor::components

