#ifndef QOR_PP_H_COMPONENTS_NETWORK_CLIENTS_HTTP_ITRANSPORTCLIENT
#define QOR_PP_H_COMPONENTS_NETWORK_CLIENTS_HTTP_ITRANSPORTCLIENT

#include <chrono>
#include "src/framework/pipeline/iosink.h"

namespace qor{ namespace components{ namespace network{

    class ITransportClient
    {
        public:

        virtual time_t GetMaxTimeout_msec(void) = 0;
        virtual bool PrepareToSend(void) = 0;
        virtual bool Process(std::chrono::time_point<std::chrono::steady_clock> start_time, std::function<bool(void*)> callback) = 0;
        virtual void Shutdown(void) = 0;
        virtual bool FollowLocation(void) = 0;
        virtual bool IsUnixSocket(void) = 0;
        virtual unsigned short Port(void) const = 0;
        virtual bool IsSSL(void) = 0;
        virtual std::string GetHost(void) = 0;
        virtual std::string GetHostAndPort(void) = 0;
        virtual qor::pipeline::Sink* GetSink(void) = 0;
        virtual qor::pipeline::Source* GetSource(void) = 0;
        virtual qor::pipeline::Buffer* GetOutputBuffer(void) = 0;
        virtual qor::pipeline::Buffer* GetInputBuffer(void) = 0;
    };

}}}//qor::components::network

#endif//QOR_PP_H_COMPONENTS_NETWORK_CLIENTS_HTTP_ITRANSPORTCLIENT