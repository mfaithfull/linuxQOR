#include "src/framework/pipeline/pipeline.h"
#include "sessionpipeline.h"

namespace qor{ namespace components {

    class NetworkSession : public workflow::Workflow
    {
    public:

        NetworkSession(ref_of<qor::network::Socket>::type socket, ref_of<qor::pipeline::Protocol>::type protocol) :
            connected(new_ref<workflow::State>(this)),
            running(new_ref<workflow::State>(this)),
            disconnect(new_ref<workflow::State>(this)),
            m_socket(socket)
        {

            connected->Enter = [this,protocol]()->void
            {
                qor_pp_ofcontext;
                log::inform("Servicing a connected client {0}", m_socket->m_fd);
                auto application = framework::AppBuilder().TheApplication();
                auto ioService = application(qor_shared).GetRole(qor_shared)->GetFeature<framework::AsyncIOService>();                
                m_ioSession = ioService(qor_shared).GetSession();

                m_pipeline = new_ref<SessionPipeline>(m_socket, m_ioSession, protocol);
                SetState(running);
            };

            running->Enter = [this]()->void
            {
                qor_pp_ofcontext;  
                size_t unitsPumped = 0;                
                if(!m_pipeline->PumpSome(unitsPumped,0) || unitsPumped == 0)
                {
                    SetState(disconnect);
                }
            };

            disconnect->Enter = [this]()->void
            {
                qor_pp_ofcontext;
                log::inform("Disconnecting client {0}", m_socket->m_fd);                
                m_socket->Shutdown(network::sockets::ShutdownReadWrite);
                SetComplete(EXIT_SUCCESS);
                PopState();
            };

            SetInitialState(connected);
        }

        ~NetworkSession()
        {
            framework::CurrentThread::GetCurrent().SetName("pool");//reset the thread name as we're about to hand it back to the pool
        }

    private:
    
        //The 3 states of a per-client session
        ref_of<workflow::State>::type connected;
        ref_of<workflow::State>::type running;
        ref_of<workflow::State>::type disconnect;

        //session data
        ref_of<network::Socket>::type m_socket;        
        ref_of<framework::AsyncIOContext::Session>::type m_ioSession;
        ref_of<SessionPipeline>::type m_pipeline;

    };

}}//qor::components

#endif//QOR_PP_H_COMPONENTS_NETWORK_SESSION
