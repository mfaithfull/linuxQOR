#include "src/configuration/configuration.h"
#include "context.h"

namespace qor { namespace components{

    EGLContext::EGLContext(void* ctx, EGLDisplay& display) : m_display(&display), m_ctx(ctx)
    {        
    }

    EGLContext::EGLContext(EGLDisplay* display, void* config, void* share_context, const int32_t *attrib_list) : m_display(display)
    {
        m_ctx = m_display->InternalCreateContext(config, share_context, attrib_list);
    }
    
    EGLContext::~EGLContext()
    {
        m_display->InternalDestroyContext(m_ctx);
    }

    bool EGLContext::MakeCurrent(void* draw, void* read)
    {
        return m_display->InternalMakeCurrent(draw, read, m_ctx);
    }

    bool EGLContext::Query(int32_t attribute, int32_t* value)
    {
        return m_display->InternalQueryContext(m_ctx, attribute, value);
    }

    void* EGLContext::CreateImage(unsigned int target, void* buffer, const intptr_t* attrib_list)
    {
        return m_display->InternalCreateImage(m_ctx, target, buffer, attrib_list);
    }

}}//qor::components
