#include "src/configuration/configuration.h"

#include "src/framework/application/application_builder.h"
#include "src/components/framework/ui/opengles/opengles.h"
#include "src/components/framework/ui/egl/window.h"
#include "canvas.h"

namespace qor{ namespace components{ 

    using namespace qor::framework;

    Canvas::Canvas()
    {
        auto opengles = AppBuilder().TheApplication()->
        GetRole(qor_shared)->
        GetFeature(&OpenGLESFeatureGUID).
        AsRef<qor::components::OpenGLESFeature>();

        m_eglContext = opengles->Context();
        //m_window = opengles->CreateWindow("Canvas", 1280, 800);
        //m_window->Show();

        //Create a Canvas
        m_canvas = ui::renderer::GlCanvas::gen(opengles);
        if (!m_canvas) {
            std::cout << "GlCanvas is not supported. Did you enable the GlEngine?" << std::endl;
        }

        width = 1200;
        height = 800;
        Resize();
    }

    bool Canvas::Draw()
    {
        //Draw the contents to the Canvas
        m_canvas->draw(clearBuffer);
        m_canvas->sync();
        return true;
    }

    bool Canvas::Prepare()
    {
        if (!m_canvas) return false;
        return Draw();
    }

    void Canvas::Show()
    {
        m_window->Present();
    }

    void Canvas::Resize()
    {
        //Set the canvas target and draw on it.
        static_cast<ui::renderer::GlCanvas*>(m_canvas)->target(m_eglContext->Use(), 0, width, height, ui::renderer::ColorSpace::ABGR8888S);
    }

    void Canvas::Refresh()
    {
        m_window->Present();
    }

    ui::renderer::Result Canvas::Push(ui::renderer::Paint* target, ui::renderer::Paint* at)
    {
        return m_canvas->push(target, at);
    }

    ui::renderer::Result Canvas::Remove(ui::renderer::Paint* paint)
    {
        return m_canvas->remove(paint);
    }

    ui::renderer::Result Canvas::Update()
    {
        return m_canvas->update();
    }

    ui::renderer::Result Canvas::Viewport(int32_t x, int32_t y, int32_t w, int32_t h)
    {
        return m_canvas->viewport(x, y, w, h);
    }

}}//qor::components
