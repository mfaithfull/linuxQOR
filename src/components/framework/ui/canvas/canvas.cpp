#include "src/configuration/configuration.h"

#include "src/framework/application/application_builder.h"
#include "src/components/framework/ui/opengles/opengles.h"
#include "src/components/framework/ui/opengles/glwindow.h"
#include "canvas.h"

namespace qor{ namespace components{ 

    using namespace qor::framework;

    Canvas::Canvas()
    {
        auto opengles = AppBuilder().TheApplication()->
        GetRole(qor_shared)->
        GetFeature(&OpenGLESFeatureGUID).
        AsRef<qor::components::OpenGLESFeature>();

        m_eglContext = opengles->Context();
        m_window = opengles->CreateWindow();//SDL_CreateWindow("ThorVG Example (OpenGL/ES)", SDL_WINDOWPOS_UNDEFINED, SDL_WINDOWPOS_UNDEFINED, width, height, SDL_WINDOW_OPENGL | SDL_WINDOW_HIDDEN | SDL_WINDOW_RESIZABLE);
        m_window->Show();
        //Create a Canvas
        m_canvas = ui::renderer::GlCanvas::gen(opengles);
        if (!m_canvas) {
            std::cout << "GlCanvas is not supported. Did you enable the GlEngine?" << std::endl;
            //return;
        }

        width = 1200;
        height = 800;
        Resize();
    }

    bool Canvas::Draw()
    {
        //Draw the contents to the Canvas
        m_canvas->draw(clearBuffer);
        m_canvas->sync();
        return true;
    }

    bool Canvas::Ready()
    {
        if (!m_canvas) return false;

//
        //Prepare a Composite Shape (Rectangle + Rectangle + Circle + Circle)
        auto shape4 = ui::renderer::Shape::gen();
        shape4->appendRect(0, 0, 300, 300, 50, 50);     //x, y, w, h, rx, ry
        shape4->appendCircle(400, 150, 150, 150);       //cx, cy, radiusW, radiusH
        shape4->appendCircle(600, 150, 150, 100);       //cx, cy, radiusW, radiusH
        shape4->fill(255, 255, 0);                      //r, g, b
        m_canvas->push(shape4);

        //Prepare Round Rectangle
        auto shape1 = ui::renderer::Shape::gen();
        shape1->appendRect(0, 450, 300, 300, 50, 50);  //x, y, w, h, rx, ry
        shape1->fill(0, 255, 0);                       //r, g, b
        m_canvas->push(shape1);

        //Prepare Circle
        auto shape2 = ui::renderer::Shape::gen();
        shape2->appendCircle(400, 600, 150, 150);    //cx, cy, radiusW, radiusH
        shape2->fill(255, 255, 0);                   //r, g, b
        m_canvas->push(shape2);

        //Prepare Ellipse
        auto shape3 = ui::renderer::Shape::gen();
        shape3->appendCircle(600, 600, 150, 100);    //cx, cy, radiusW, radiusH
        shape3->fill(0, 255, 255);                   //r, g, b
        m_canvas->push(shape3);

//
        //initiate the first rendering before window pop-up.
        m_canvas->draw();
        m_canvas->sync();

        return true;
    }

    void Canvas::Show()
    {
        m_window->Refresh();
    }

    void Canvas::Resize()
    {
        //Set the canvas target and draw on it.
        static_cast<ui::renderer::GlCanvas*>(m_canvas)->target(m_eglContext->Use(), 0, width, height, ui::renderer::ColorSpace::ABGR8888S);
    }

    void Canvas::Refresh()
    {
        m_window->Refresh();
    }

}}//qor::components
