#ifndef QOR_PP_H_COMPONENTS_FRAMEWORK_UI_RENDERER_GL_RENDERPASS
#define QOR_PP_H_COMPONENTS_FRAMEWORK_UI_RENDERER_GL_RENDERPASS

#include "common.h"
#include "rendertask.h"
#include "rendertarget.h"

namespace qor{ namespace components{ namespace ui{ namespace renderer{
    
    class GlProgram;

    class GlRenderPass
    {
    public:
        GlRenderPass(GlRenderTarget* fbo);
        GlRenderPass(GlRenderPass&& other);

        ~GlRenderPass();

        bool isEmpty() const { return mFbo == nullptr; }

        void addRenderTask(GlRenderTask* task);

        GLuint getFboId() { return mFbo->getFboId(); }

        GLuint getTextureId() { return mFbo->getColorTexture(); }

        const RenderRegion& getViewport() const { return mFbo->getViewport(); }

        uint32_t getFboWidth() const { return mFbo->getWidth(); }

        uint32_t getFboHeight() const { return mFbo->getHeight(); }

        void getMatrix(float dst[16], const Matrix& matrix) const;

        template <class T>
        T* endRenderPass(qor::ref_of<qor::components::OpenGLESFeature>::type openGLES, GlProgram* program, GLuint targetFbo) {
            int32_t maxDepth = mDrawDepth + 1;

            for (uint32_t i = 0; i < mTasks.count; i++) {
                mTasks[i]->normalizeDrawDepth(maxDepth);
            }

            auto task = new T(openGLES, program, targetFbo, mFbo, std::move(mTasks));
            task->setRenderSize(mFbo->getViewport().w(),  mFbo->getViewport().h());

            return task;
        }

        int nextDrawDepth() { return ++mDrawDepth; }

        void setDrawDepth(int32_t depth) { mDrawDepth = depth; }

        GlRenderTarget* getFbo() { return mFbo; }
    private:
        GlRenderTarget* mFbo;
        Array<GlRenderTask*> mTasks = {};
        int32_t mDrawDepth = 0;
    };

}}}}//qor::components::ui::renderer

#endif//QOR_PP_H_COMPONENTS_FRAMEWORK_UI_RENDERER_GL_RENDERPASS