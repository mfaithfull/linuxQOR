#ifndef QOR_PP_H_COMPONENTS_FRAMEWORK_UI_RENDERER_GL_GPUBUFFER
#define QOR_PP_H_COMPONENTS_FRAMEWORK_UI_RENDERER_GL_GPUBUFFER

#include "common.h"

namespace qor{ namespace components{ namespace ui{ namespace renderer{
    
    class GlGpuBuffer
    {
    public:
        enum class Target
        {
            ARRAY_BUFFER = GL_ARRAY_BUFFER,
            ELEMENT_ARRAY_BUFFER = GL_ELEMENT_ARRAY_BUFFER,
            UNIFORM_BUFFER = GL_UNIFORM_BUFFER,
        };

        GlGpuBuffer();
        ~GlGpuBuffer();
        void updateBufferData(Target target, uint32_t size, const void* data);
        void bind(Target target);
        void unbind(Target target);
        uint32_t getBufferId() { return mGlBufferId; }

    private:
        uint32_t    mGlBufferId = 0;

    };

    class GlStageBuffer {
    public:
        GlStageBuffer();
        ~GlStageBuffer();

        uint32_t push(void* data, uint32_t size, bool alignGpuOffset = false);
        uint32_t pushIndex(void* data, uint32_t size);
        bool flushToGPU();
        void bind();
        void unbind();
        GLuint getBufferId();

    private:
        void alignOffset(uint32_t size);

        GLuint mVao = 0;
        GlGpuBuffer mGpuBuffer = {};
        GlGpuBuffer mGpuIndexBuffer = {};
        Array<uint8_t> mStageBuffer = {};
        Array<uint8_t> mIndexBuffer = {};
    };


}}}}//qor::components::ui::renderer

#endif//QOR_PP_H_COMPONENTS_FRAMEWORK_UI_RENDERER_GL_GPUBUFFER