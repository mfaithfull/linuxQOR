    // Accumulate the values of a list U[n] into v[n]. So that:
    // V[0] = 0;
    // V[n+1] = v[n] + U[n]
    // return the sum of U[n].
    int Integrate(std::vector<int>& elements) 
    {
        int accu = 0;
        for (auto& i : elements) 
        {
            const int old_accu = accu;
            accu += i;
            i = old_accu;
        }
        return accu;
    }

