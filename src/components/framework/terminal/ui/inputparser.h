#ifndef QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_UI_INPUTPARSER
#define QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_UI_INPUTPARSER

#include <functional>
#include <string>
#include <vector>

#include "mouse.h"

namespace qor{ namespace components{ namespace tui {

    struct Event;

    // Parse a sequence of |char| accross |time|. Produces |Event|.
    class TerminalInputParser 
    {
    public:
        explicit TerminalInputParser(std::function<void(Event)> out);
        void Timeout(int time);
        void Add(char c);

        private:
        unsigned char Current();
        bool Eat();

        enum Type 
        {
            UNCOMPLETED,
            DROP,
            CHARACTER,
            MOUSE,
            CURSOR_POSITION,
            CURSOR_SHAPE,
            SPECIAL,
        };

        struct CursorPosition 
        {
            int x;
            int y;
        };

        struct Output 
        {
            Type type;
            union 
            {
                Mouse mouse;
                CursorPosition cursor{};
                int cursor_shape;
            };

            Output(Type t) : type(t) {}
        };

        void Send(Output output);
        Output Parse();
        Output ParseUTF8();
        Output ParseESC();
        Output ParseDCS();
        Output ParseCSI();
        Output ParseOSC();
        Output ParseMouse(bool altered, bool pressed, std::vector<int> arguments);
        Output ParseCursorPosition(std::vector<int> arguments);

        std::function<void(Event)> out_;
        int position_ = -1;
        int timeout_ = 0;
        std::string pending_;
    };

}}}//qor::components::tui

#endif//QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_UI_INPUTPARSER

