#ifndef QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_UI_MOUSE
#define QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_UI_MOUSE

namespace qor{ namespace components{ namespace tui {

    //A mouse event. It contains the coordinate of the mouse, the button pressed and the modifier (shift, ctrl, meta).

    struct Mouse 
    {
        enum Button 
        {
            Left = 0,
            Middle = 1,
            Right = 2,
            None = 3,
            WheelUp = 4,
            WheelDown = 5,
            WheelLeft = 6,   /// Supported terminal only.
            WheelRight = 7,  /// Supported terminal only.
        };

        enum Motion 
        {
            Released = 0,
            Pressed = 1,
            Moved = 2,
        };

        Button button = Button::None;

        Motion motion = Motion::Pressed;

        // Modifiers:
        bool shift = false;
        bool meta = false;
        bool control = false;

        // Coordinates:
        int x = 0;
        int y = 0;
    };

}}}//qor::components::tui

#endif//QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_UI_MOUSE

