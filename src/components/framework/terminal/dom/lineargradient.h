#ifndef QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_DOM_LINEARGRADIENT
#define QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_DOM_LINEARGRADIENT

#include <vector>
#include <optional>
#include "../screen/colour.h"

namespace qor{ namespace components{ namespace tui {

    //A class representing the settings for linear-gradient color effect.
    ///
    /// Example:
    /// ```cpp
    /// LinearGradient()
    ///    .Angle(45)
    ///    .Stop(Color::Red, 0.0)
    ///    .Stop(Color::Green, 0.5)
    ///    .Stop(Color::Blue, 1.0);
    /// ```
    ///
    /// There are also shorthand constructors:
    /// ```cpp
    /// LinearGradient(Color::Red, Color::Blue);
    /// LinearGradient(45, Color::Red, Color::Blue);
    /// ```

    struct LinearGradient 
    {
        float angle = 0.f;

        /// A stop is a color at a specific position in the gradient.  The position is a value between 0.0 and 1.0,
        /// where 0.0 is the start of the gradient  and 1.0 is the end of the gradient.
        struct Stop 
        {
            Colour colour = Colour::Default;
            std::optional<float> position;
        };
        std::vector<Stop> stops;

        // Simple constructor
        LinearGradient();
        LinearGradient(Colour begin, Colour end);
        LinearGradient(float angle, Colour begin, Colour end);

        // Modifier using the builder pattern.
        LinearGradient& Angle(float angle);
        LinearGradient& Stop(Colour colour, float position);
        LinearGradient& Stop(Colour colour);
    };

}}}//qor::components::tui

#endif//QOR_PP_H_COMPONENTS_FRAMEWORK_TUI_DOM_LINEARGRADIENT

