    template<class T>
    class deferred : public ParserState
    {
        public:
        deferred(Parser* parser) : ParserState(parser)
        {

            Enter = [this]()
            {
                m_p = new T(GetParser());
                m_p->Enter();
                if(m_p)
                {
                    m_result = m_p->m_result;
                }
            };

            Resume = [this]()
            {
                if(m_p)
                {
                    m_result = m_p->m_result;
                    if(m_p)
                    {
                        m_p->Resume();
                        if(m_p)
                        {
                            m_result = m_p->m_result;
                        }
                    }
                }
            };

            Leave = [this]()
            {
                if(m_p)
                {
                    m_p->Leave();
                    m_result = m_p->m_result;
                    delete m_p;
                    m_p = nullptr;
                }
            };
        }

    private:

        T* m_p;
    };

