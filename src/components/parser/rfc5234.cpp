#include "src/configuration/configuration.h"
#include "src/platform/compiler/compiler.h"
#include "src/qor/error/error.h"
#include "parser.h"
#include "rfc5234.h"
#include "nodes/digit.h"
#include "nodes/char.h"

namespace qor { namespace components { namespace parser {

    void DIGIT::Emit()
    {
        int digitVal = m_result.first - '0';
        GetParser()->PushNode(new_ref<Digit>(digitVal).AsRef<Node>());
    }

    void VCHAR::Emit()
    {
        char charVal = m_result.first;
        GetParser()->PushNode(new_ref<Char>(charVal, static_cast<uint64_t>(m_token)).AsRef<Node>());
    }

    void CTL::Emit()
    {
        char ctlVal = m_result.first;
        GetParser()->PushNode(new_ref<Char>(ctlVal, static_cast<uint64_t>(m_token)).AsRef<Node>());
    }
    
    void HEXDIGIT::Emit()
    {
        unsigned int hexVal = (m_result.first >= '0' && m_result.first <= '9') ? m_result.first - '0' :
        ((m_result.first >= 'a' && m_result.first <= 'f') ? m_result.first - 'a' + 10 :
            ((m_result.first >= 'A' && m_result.first <= 'F') ? m_result.first - 'A' + 10 : 0));
        GetParser()->PushNode(new_ref<Digit>(hexVal).AsRef<Node>());
    }

    void BIT::Emit()
    {
        unsigned int bitVal = m_result.first == '0' ? 0 : 
        (m_result.first == '1' ? 1 : 0);
    }

    void CHAR::Emit()
    {
        char charVal = m_result.first;
        GetParser()->PushNode(new_ref<Char>(charVal, static_cast<uint64_t>(m_token)).AsRef<Node>());
    }

    void ALPHA::Emit()
    {
        char charVal = m_result.first;
        GetParser()->PushNode(new_ref<Char>(charVal, static_cast<uint64_t>(m_token)).AsRef<Node>());
    }

}}}//qor::components::parser
