#ifndef QOR_PP_H_INSTANCE_SINGLETON
#define QOR_PP_H_INSTANCE_SINGLETON

#include <stddef.h>

#include "src/qor/reference/reference.h"
#include "src/qor/factory/factory.h"
#include "src/qor/sync/recursive_mutex.h"
#include "src/qor/sync/lock.h"

//The singleton instancer gives out the same instance each time unless it it destroyed by there being no more references to it. Then it gives a new one

namespace qor{

    namespace detail {

        template <class T>    
        class SingletonInstanceHolder final
        {
        public:
            SingletonInstanceHolder() : bInitialised(false){}

            ~SingletonInstanceHolder()
            {
                Lock lock(m);
                if(bInitialised)
                {
                    theRef.Dispose();
                    bInitialised = false;
                }
            }

            typename ref_of<T>::type Instance()
            {
                Lock lock(m);
                if( !bInitialised )
                {                
                    theRef = factory_of<T>::type::Construct(count);                    
                    bInitialised = true;
                }
                else if(theRef.IsNull())
                {
                    theRef.Attach(factory_of<T>::type::Construct(count));
                }
                return theRef;
            }

            template<typename... _p >
            typename ref_of<T>::type Instance(size_t, _p&&... p1)
            {
                Lock lock(m);
                if( !bInitialised )
                {                                    
                    theRef = factory_of<T>::type::Construct(count, std::forward<_p>(p1)...);
                    bInitialised = true;
                }
                else if(theRef.IsNull())
                {
                    theRef.Attach(factory_of<T>::type::Construct(count, std::forward<_p>(p1)...));
                }
                return theRef;
            }

            void Release()
            {
                Lock lock(m);
                if(bInitialised)
                {
                    factory_of<T>::type::Destruct(theRef);
                    theRef.Reset();
                }
            }

        private:
            static constexpr size_t count = 1;
            ref_of<T>::type theRef;
            bool bInitialised;
            RecursiveMutex m;
        };

    }//detail
    
	class SingletonInstancer final
	{
	public:

		template< class T >
		static inline void Release(T* pt, size_t count = ignored_count)
		{
			Holder<T>().Release();
		}

		template< class T >
		static inline auto Instance(size_t count = ignored_count)
		{
            return Holder<T>().Instance();
		}

        template< class T, typename... _p >
		static inline auto Instance(size_t count, _p&&... p1)
		{
			return Holder<T>().Instance(count, std::forward<_p>(p1)...);
		}
		
        static constexpr size_t ignored_count = 1;
	private:

        template< class T>
        static detail::SingletonInstanceHolder<T>& Holder()
        {
            static detail::SingletonInstanceHolder<T> holder;
            return holder;
        }

        SingletonInstancer() = delete;
		~SingletonInstancer() = delete;
	};

}//qor

#endif//QOR_PP_H_INSTANCE_SINGLETON
