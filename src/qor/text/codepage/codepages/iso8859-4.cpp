	ISO8859_4CodePage::ISO8859_4CodePage() : CodePage("ISO_8859-4:1988", "ISO-8859-4", aliases, 6)
	{
	}
	
	bool ISO8859_4CodePage::Encode(const CodePoint & codePoint, char8_t*& space, size_t& available) const
	{
		if(available == 0 || space == nullptr)
		{
			return false;
		}

        char8_t c = 255;
        auto it = codePointToChar.find(codePoint.Value());
        if(it != codePointToChar.end())
        {
            c = it->second;
        }
		*space++ = c;
        --available;
		return true;    	
	}

	CodePoint ISO8859_4CodePage::Decode(const char8_t*& chars, size_t& available) const
	{
        uint32_t cp = 255;
        if (chars != nullptr && available-- > 0)
        {
            cp = charToCodePoint[*chars++];
        }
		return CodePoint(cp);
	}
