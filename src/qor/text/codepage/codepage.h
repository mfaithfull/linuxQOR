#ifndef QOR_PP_H_TEXT_CODEPAGE_CODEPAGE
#define QOR_PP_H_TEXT_CODEPAGE_CODEPAGE

#include <vector>
#include <string>
#include "codepagebase.h"

namespace qor{

	template< typename C, Mib M = Mib::C >
	class CodePage : public CodePageBase< C, M >
	{
	public:

		typedef C charT;

		CodePage(const std::string& name, const std::string& MIMEName, const std::string* aliases = nullptr, uint32_t aliasCount = 0) : CodePageBase< C, M >(), m_name(name), m_MIMEName(MIMEName)
		{
			while (aliasCount--)
			{
				std::string alias(aliases[aliasCount]);
				m_aliases.push_back(alias);
			}
		}

		virtual ~CodePage() = default;

		virtual inline charT Encode(const CodePoint & codePoint) const = 0;
		virtual inline CodePoint Decode(charT character) const = 0;

	protected:

		std::string m_name;
		std::string m_MIMEName;
		std::vector< std::string > m_aliases;

    };

	template<Mib M>
	struct charset_of
	{
		typedef CodePage<char, M> type;
	};

}//qor

#endif//QOR_PP_H_TEXT_CODEPAGE_CODEPAGE