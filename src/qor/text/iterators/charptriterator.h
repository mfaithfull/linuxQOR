#ifndef QOR_PP_H_TEXT_CHARPTRITERATOR
#define QOR_PP_H_TEXT_CHARPTRITERATOR

#include <string>

namespace qor
{
    template<typename charptrT>
    class charptr_iterator
    {
    protected:

        charptrT m_p;

        typedef std::iterator_traits<charptrT>		traitsT;

        template<typename _Iter>
        using __convertible_from = std::__enable_if_t<std::is_convertible<_Iter, charptrT>::value>;

    public:

        typedef charptrT					iterator_type;
        typedef typename traitsT::iterator_category iterator_category;
        typedef typename traitsT::value_type  	value_type;
        typedef typename traitsT::difference_type 	difference_type;
        typedef typename traitsT::reference 	reference;
        typedef typename traitsT::pointer   	pointer;

        using iterator_concept = std::__detail::__iter_concept<charptrT>;

        constexpr charptr_iterator() noexcept : m_p(charptrT()) 
        {            
        }

        explicit constexpr charptr_iterator(const charptrT& __i) noexcept : m_p(__i) 
        {
        }

        // Allow iterator to const_iterator conversion
        template<typename _Iter, typename = __convertible_from<_Iter>>
        constexpr charptr_iterator(const charptr_iterator<_Iter>& __i) noexcept : m_p(__i.base()) 
        {             
        }

        // Forward iterator requirements
        constexpr reference operator*() const noexcept
        { 
            return *m_p; 
        }

        constexpr pointer operator->() const noexcept
        { 
            return m_p; 
        }

        constexpr charptr_iterator& operator++() noexcept
        {
            ++m_p;
            return *this;
        }

        constexpr charptr_iterator operator++(int) noexcept
        { 
            return charptr_iterator(m_p++); 
        }

        // Bidirectional iterator requirements
        constexpr charptr_iterator& operator--() noexcept
        {
            --m_p;
            return *this;
        }

        constexpr charptr_iterator operator--(int) noexcept
        { 
            return charptr_iterator(m_p--); 
        }

        // Random access iterator requirements
        constexpr reference operator[](difference_type __n) const noexcept
        { 
            return m_p[__n]; 
        }

        constexpr charptr_iterator& operator+=(difference_type __n) noexcept
        { 
            m_p += __n; 
            return *this; 
        }

        constexpr charptr_iterator operator+(difference_type __n) const noexcept
        { 
            return charptr_iterator(m_p + __n); 
        }

        constexpr charptr_iterator& operator-=(difference_type __n) noexcept
        { 
            m_p -= __n; return *this; 
        }

        constexpr charptr_iterator operator-(difference_type __n) const noexcept
        { 
            return charptr_iterator(m_p - __n); 
        }

        constexpr const charptrT& base() const noexcept
        { 
            return m_p; 
        }
    };

    template<typename _IteratorL, typename _IteratorR>
    [[nodiscard]] constexpr bool operator == (const charptr_iterator<_IteratorL>& __lhs, const charptr_iterator<_IteratorR>& __rhs) 
    noexcept(noexcept(__lhs.base() == __rhs.base()))
    requires requires 
    {
        { __lhs.base() == __rhs.base() } -> std::convertible_to<bool>;
    }
    { 
        return __lhs.base() == __rhs.base(); 
    }

    template<typename _IteratorL, typename _IteratorR>
    [[nodiscard]] constexpr std::__detail::__synth3way_t<_IteratorR, _IteratorL> operator<=>(const charptr_iterator<_IteratorL>& __lhs, const charptr_iterator<_IteratorR>& __rhs)
    noexcept(noexcept(std::__detail::__synth3way(__lhs.base(), __rhs.base())))
    { 
        return std::__detail::__synth3way(__lhs.base(), __rhs.base()); 
    }

    template<typename _Iterator>
    [[nodiscard]] constexpr bool operator==(const charptr_iterator<_Iterator>& __lhs, const charptr_iterator<_Iterator>& __rhs)
    noexcept(noexcept(__lhs.base() == __rhs.base()))
    requires requires 
    {
        { __lhs.base() == __rhs.base() } -> std::convertible_to<bool>;
    }
    { 
        return __lhs.base() == __rhs.base(); 
    }

    template<typename _Iterator>
    [[nodiscard]] constexpr std::__detail::__synth3way_t<_Iterator> operator<=>(const charptr_iterator<_Iterator>& __lhs, const charptr_iterator<_Iterator>& __rhs)
    noexcept(noexcept(std::__detail::__synth3way(__lhs.base(), __rhs.base())))
    { 
        return std::__detail::__synth3way(__lhs.base(), __rhs.base()); 
    }

    template<typename _IteratorL, typename _IteratorR>
    [[nodiscard]] constexpr inline auto operator-(const charptr_iterator<_IteratorL>& __lhs, const charptr_iterator<_IteratorR>& __rhs) noexcept -> decltype(__lhs.base() - __rhs.base())
    { 
        return __lhs.base() - __rhs.base(); 
    }

    template<typename _Iterator>
    [[nodiscard]] constexpr inline typename charptr_iterator<_Iterator>::difference_type operator-(const charptr_iterator<_Iterator>& __lhs, const charptr_iterator<_Iterator>& __rhs) noexcept
    { 
        return __lhs.base() - __rhs.base(); 
    }

    template<typename _Iterator>
    [[nodiscard]] constexpr inline charptr_iterator<_Iterator> operator+(typename charptr_iterator<_Iterator>::difference_type __n, const charptr_iterator<_Iterator>& __i) noexcept
    { 
        return charptr_iterator<_Iterator>(__i.base() + __n); 
    }

}//qor
#endif//QOR_PP_H_TEXT_CHARPTRITERATOR