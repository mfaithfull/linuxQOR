#ifndef QOR_PP_H_TEXT_ABSTRACTSTRING
#define QOR_PP_H_TEXT_ABSTRACTSTRING

#include <stdexcept>
#include <string>
#include "src/qor/text/codepage/codepages.h"
#include "src/qor/iterators/iterators.h"
#include "src/qor/text/codepage/codepageregistry.h"

namespace qor{

    template< typename C >
    struct encoding_of
    {
        typedef ASCIICodePage CodePageT;
    };

    template<>
    struct encoding_of< char8_t >
    {
        typedef UTF8CodePage CodePageT;
    };

    template<>
    struct encoding_of< char16_t >
    {
        typedef UTF16CodePage CodePageT;
    };

    template<typename IteratorT, typename CharT>
    bool WhileBlank(IteratorT& it, const std::locale& locale)
    {
        CharT c = *it;        
        if(std::isblank<CharT>(c, locale))
        {
            return true;
        }
        return false;
    }

    template<typename ImplT, typename BufferT, 
    typename iterator = ImplT::iterator, 
    typename const_iterator = ImplT::const_iterator,
    typename reverse_iterator = ImplT::reverse_iterator,
    typename const_reverse_iterator = ImplT::const_reverse_iterator>
    class AbstractString
    {
    public:
        
        typedef typename BufferT::iterator::value_type CharT;

        //All string classes must implement and expose

        virtual size_t Length() const = 0;
        virtual bool IsEmpty() const = 0;
        virtual void Reset() = 0;
        virtual typename BufferT::View view() = 0;
        virtual CharT At(size_t index) const = 0;
        virtual ImplT Clone() const = 0;
        virtual BufferT CloneBuffer() const = 0;        
        virtual std::basic_string<typename std::remove_cv<CharT>::type> ToStdString() const = 0;
        virtual Mib GetEncoding() const = 0;
        virtual size_t size() const = 0;
        virtual iterator begin() const = 0;
        virtual const_iterator cbegin() const = 0;
        virtual iterator end() const = 0;
        virtual const_iterator cend() const = 0;
        virtual reverse_iterator rbegin() const = 0;
        virtual const_reverse_iterator crbegin() const = 0;
        virtual reverse_iterator rend() const = 0;
        virtual const_reverse_iterator crend() const = 0;

        operator std::basic_string<typename std::remove_cv<CharT>::type>() const
        {
            return ToStdString();
        }

        //All string classes inherit and expose

        ImplT Left(size_t charCount) const
        {
            if(charCount == 0)
            {
                return BufferT();
            }
            if(charCount >= Length())
            {
                return CloneBuffer();
            }
            BufferT output;            
            const_iterator it = cbegin();
            WriteUpToCount(it, charCount, output);
            return ImplT(output);
        }

        ImplT Right(size_t charCount) const
        {
            if( charCount == 0)
            {
                return BufferT();
            }
            if (charCount >= Length())
            {
                return CloneBuffer();
            }
            BufferT output;            
            const_iterator it = cbegin();            
            size_t offsetCount = Length() - charCount;
            OffsetIterator(it, offsetCount);
            WriteUpToCount(it, charCount, output);
            return ImplT(output);
        }

        ImplT Mid(size_t from, size_t charCount) const
        {
            if(from >= Length() || charCount == 0)
            { 
                return BufferT();
            }
            if(from + charCount > Length())
            {
                charCount = Length() - from;
            }
            BufferT output;            
            const_iterator it = cbegin();     
            OffsetIterator(it, from);
            WriteUpToCount(it, charCount, output);
            return ImplT(output);
        }

        ImplT Erase(size_t from, size_t charCount) const
        {
            if(from >= Length() || charCount == 0)
            {
                return CloneBuffer();
            }
            if(from + charCount > Length())
            {
                charCount = Length() - from;
            }
            BufferT output;
            const_iterator in = cbegin();                        
            WriteUpToCount(in, from, output);
            OffsetIterator(in, charCount);
            WriteRemaining(in, output);
            return ImplT(output);
        }

        ImplT Trim() const
        {
            BufferT output;
            const_iterator start = cbegin();
            OffsetIterator<const_iterator>(start, &WhileBlank<const_iterator, CharT>);
            const_reverse_iterator end = crbegin();
            OffsetIterator<const_reverse_iterator>(end, &WhileBlank<const_reverse_iterator, CharT>);
            if( end.getPtr() - start.getPtr() <= 0 )
            {
                return ImplT();
            }
            WriteUpTo(start, output, end);
            return ImplT(output);
        }

        ImplT TrimLeft() const
        {
            BufferT output;
            const_iterator start = cbegin();
            OffsetIterator<const_iterator>(start, &WhileBlank<const_iterator, CharT>);
            if(start == cend())
            {
                return ImplT();
            }
            WriteRemaining(start, output);
            return ImplT(output);
        }

        ImplT TrimRight() const
        {
            BufferT output;
            const_iterator start = cbegin();
            const_reverse_iterator end = crbegin();
            OffsetIterator<const_reverse_iterator>(end, &WhileBlank<const_reverse_iterator, CharT>);
            if( end.getPtr() - start.getPtr() <= 0 )
            {
                return ImplT();
            }
            WriteUpTo(start, output, end);
            return ImplT(output);
        }

        void Fill(CharT c)
        {            
            FillUpToCount(c, Length());
        }

        void Fill(CharT c, size_t count)
        {
            FillUpToCount(c, count);
        }

        void Fill(const CodePoint& cp)
        {
            FillUpToCount(cp, Length());
        }

        void Fill(const CodePoint& cp, size_t count)
        {
            FillUpToCount(cp, count);
        }

    protected:

        //derived classes must override this if they have a modifiable buffer but shouldn't expose it
        virtual BufferT* GetModifiableBufferObject()
        {
            return nullptr;
        }

        //Optionally string classes may override

        virtual void UpdateLength() const{}
        virtual AbstractCharacterCodec< CharT >* GetCodecCache() const{ return nullptr; }
        virtual void SetCodecCache(AbstractCharacterCodec< CharT >* codec) const{}
        
        //Implementation details used by AbstractString and derived classes

        AbstractCharacterCodec< CharT >* GetCodec() const
        {
            AbstractCharacterCodec< CharT >* Codec = GetCodecCache();
            if(Codec)
            {
                return Codec;
            }
            AnyObject Registration = TheCodePageRegistry()->GetCodePage(GetEncoding());
            Codec = Registration;
            if(Codec == nullptr)
            {
                throw std::logic_error("No CodePage registered for encoding");
            }
            SetCodecCache(Codec);
            return Codec;
        }

    private:

        //Implementation details used only by AbstractString

        static constexpr size_t kMaxCodeUnitsPerCodePoint{6};

        inline void OffsetIterator(const_iterator& it, size_t offsetCount) const
        {
            while(offsetCount > 0)
            {
                --offsetCount;
                ++it;
            }
        }

        template<typename iterator_type>
        inline void OffsetIterator(iterator_type& it, bool(*pred)(iterator_type&, const std::locale&)) const
        {
            auto locale = std::locale("", LC_CTYPE);
            while(pred(it, locale))
            {
                ++it;
            }
        }

        inline void ReverseOffsetIterator(const_iterator& it, size_t offsetCount) const
        {
            while(offsetCount > 0)
            {
                --offsetCount;
                --it;
            }
        }

        inline void WriteRemaining(const_iterator& in, BufferT& output) const
        {
            AbstractCharacterCodec< CharT >* Codec = GetCodec();
            while(in != cend())
            {
                size_t charCount = cend() - in;
                const CharT* input = in++;                
                CodePoint cp = Codec->Decode(input, charCount);
                EncodeIntoOutput(output, Codec, cp);
            }
        }

        inline void WriteUpToCount(const_iterator& it, size_t& charCount, BufferT& output) const
        {
            AbstractCharacterCodec< CharT >* Codec = GetCodec();
            while(it != cend() && charCount > 0)
            {
                const CharT* input = it++;
                CodePoint cp = Codec->Decode(input, charCount);                
                EncodeIntoOutput(output, Codec, cp);
            }
        }

        inline void EncodeIntoOutput(BufferT& output, AbstractCharacterCodec< CharT >* Codec, const CodePoint& cp) const
        {
            CharT interBuffer[kMaxCodeUnitsPerCodePoint]{0};
            size_t capacity = kMaxCodeUnitsPerCodePoint;
            CharT* space = interBuffer;
            Codec->Encode(cp, space, capacity);
            output.Write(interBuffer, space - &interBuffer[0]);
        }

    };
}//qor

#endif//QOR_PP_H_TEXT_ABSTRACTSTRING
