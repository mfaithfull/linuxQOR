#ifndef QOR_PP_H_TEXT_UTF8STRING
#define QOR_PP_H_TEXT_UTF8STRING

#include <stdexcept>
#include <string>
#include "src/qor/text/abstractstring.h"
#include "src/qor/text/buffers/mutablebuffer.h"
#include "src/qor/text/iterators/unitchariterator.h"
#include "src/qor/text/codepage/codepages/utf8.h"

namespace qor{

    class UTF8String : public AbstractString< 
        UTF8String, 
        MutableBuffer<char8_t>,
        UTF8Iterator< MutableBuffer<char8_t> >::Iterator,
        UTF8ConstIterator< MutableBuffer<char8_t> >::Iterator,
        UTF8ReverseIterator< MutableBuffer<char8_t> >::Iterator,
        UTF8ConstReverseIterator< MutableBuffer<char8_t> >::Iterator >
    {
    public:

        typedef AbstractString< 
        UTF8String, 
        MutableBuffer<char8_t>,
        UTF8Iterator< MutableBuffer<char8_t> >::Iterator,
        UTF8ConstIterator< MutableBuffer<char8_t> >::Iterator,
        UTF8ReverseIterator< MutableBuffer<char8_t> >::Iterator,
        UTF8ConstReverseIterator< MutableBuffer<char8_t> >::Iterator > base;

        typedef MutableBuffer<char8_t> BufferT;
        typedef UTF8CodePage defaultEncodingT;
        typedef typename BufferT::View viewT;
        typedef typename UTF8Iterator<BufferT>::Iterator iterator;
        typedef typename UTF8ConstIterator<BufferT>::Iterator const_iterator;
        typedef typename UTF8ReverseIterator<BufferT>::Iterator reverse_iterator;
        typedef typename UTF8ConstReverseIterator<BufferT>::Iterator const_reverse_iterator;

        template<size_t N>
        UTF8String(const char8_t(&str)[N]) : m_buffer(str)        
        {
            UpdateLength();
        }

        UTF8String(const BufferT& buffer) : m_buffer(buffer)
        {
            UpdateLength();
        }

        UTF8String(const UTF8String& src) : m_buffer(src.m_buffer)
        {
            m_cachedLength = src.Length();
        }

        UTF8String(UTF8String&& src) noexcept : m_buffer(std::move(src.m_buffer))
        {
            m_cachedLength = src.Length();
        }

        UTF8String(const char8_t* pBuffer, size_t stCount ) : m_buffer( pBuffer, stCount )
        {
            UpdateLength();            
        }

        virtual ~UTF8String()
        {
        }

        UTF8String& operator = (const UTF8String& src)
        {
            if (this != &src)
            {
                m_buffer = src.m_buffer;
                m_cachedLength = src.Length();
            }
            return *this;
        }

        size_t Length() const
        {
            return m_cachedLength;
        }

        bool IsEmpty() const
        {
            return m_buffer.IsEmpty();
        }

        void Reset(void)
        {
            m_buffer.Reset();
            m_cachedLength = 0;
        }

        virtual viewT GetBuffer() //override
        {
            return m_buffer.GetBuffer();
        }

        const char32_t operator[](size_t index) const
        {
            iterator it = begin();
            while(it != end() && index-- > 0)
            {
                it++;
            }
            if(it != end())
            {
                AbstractCharacterCodec< CharT >* Codec = GetCodec();
                const CharT* input = it;
                size_t charCount = 1;
                CodePoint cp = Codec->Decode(input, charCount);
                return cp.UChar();
            }
            else
            {
                throw std::out_of_range("Index out of range accessing UTF8String At {index}");
            }
        }

        char8_t At(size_t index) const
        {
            return m_buffer.At(index);
        }

        std::basic_string<char8_t> ToStdString() const
        {
            return m_buffer.ToStdString();
        }

        operator std::basic_string<char8_t>() const
        {
            return ToStdString();
        }

        iterator begin() const
        {
            iterator it(m_buffer);
            return it.begin();
        }

        const_iterator cbegin() const
        {
            const_iterator it(m_buffer);
            return it.begin();
        }

        iterator end() const
        {
            iterator it(m_buffer);
            return it.end();
        }

        const_iterator cend() const
        {
            const_iterator it(m_buffer);
            return it.end();
        }

        reverse_iterator rbegin() const
        {
            reverse_iterator it(m_buffer);
            return it.begin();
        }

        const_reverse_iterator crbegin() const
        {
            const_reverse_iterator it(m_buffer);
            return it.begin();
        }

        reverse_iterator rend() const
        {
            reverse_iterator it(m_buffer);
            return it.end();
        }

        const_reverse_iterator crend() const
        {
            const_reverse_iterator it(m_buffer);
            return it.end();
        }

        template<typename func_t>
        void ConstVisit(func_t&& func, size_t startIndex = 0) const
        {
        }

        template<typename func_t>
        void ConstReverseVisit(func_t&& func, size_t startIndex = (size_t)(-1)) const
        {
        }

        virtual Mib GetEncoding() const override
        {
            return defaultEncodingT::GetMib();
        }

        void SetEncoding(Mib charSet)
        {
            //UTF8String has fixed encoding
        }

        UTF8String Left(size_t charCount) const
        {
            return UTF8String(base::Left(charCount));
        }

        UTF8String Right(size_t charCount) const
        {
            return UTF8String(base::Right(charCount));
        }

        UTF8String Mid(size_t from, size_t charCount) const
        {
            return UTF8String(base::Mid(from, charCount));
        }

    protected:

        //Recalculate character count after buffer change
        virtual void UpdateLength() const
        {            
            m_cachedLength = 0;
            auto it = cbegin();
            while(it++ != cend())
            {
                m_cachedLength++;
            }
        }

        virtual AbstractCharacterCodec< CharT >* GetCodecCache() const
        { 
            return m_cachedCodec; 
        }

        virtual void SetCodecCache(AbstractCharacterCodec< CharT >* codec) const
        {
            m_cachedCodec = codec;
        }

        BufferT m_buffer;
        mutable size_t m_cachedLength{0};
        mutable AbstractCharacterCodec< char8_t >* m_cachedCodec{nullptr};
    };

}//qor

#endif//QOR_PP_H_TEXT_UTF8STRING
