#ifndef QOR_PP_H_TEXT_UCS4STRING
#define QOR_PP_H_TEXT_UCS4STRING

#include <stdexcept>
#include <string>
#include "src/qor/text/abstractstring.h"
#include "src/qor/text/buffers/mutablebuffer.h"
#include "src/qor/iterators/iterators.h"

namespace qor{

    class UCS4String : public AbstractString< MutableBuffer<char32_t> >
    {
    public:

        typedef AbstractString< MutableBuffer<char32_t> > base;
        typedef MutableBuffer<char32_t> BufferT;
        typedef typename encoding_of<char32_t>::CodePageT defaultEncodingT;
        typedef typename base::viewT viewT;
        typedef rawiterator<char32_t> iterator;
        typedef rawiterator<const char32_t> const_iterator;
        typedef rawreverseiterator<char32_t> reverse_iterator;
        typedef rawreverseiterator<const char32_t> const_reverse_iterator;

        template<size_t N>
        UCS4String(const char32_t(&str)[N]) : m_buffer(str)        
        {
        }

        UCS4String(const BufferT& buffer) : m_buffer(buffer)
        {
        }

        UCS4String(const UCS4String& src) : m_buffer(src.m_buffer)
        {
        }

        UCS4String(UCS4String&& src) noexcept : m_buffer(std::move(src.m_buffer))
        {
        }

        UCS4String( const char32_t* pBuffer, size_t stCount ) : m_buffer( pBuffer, stCount )
        {
        }

        virtual ~UCS4String()
        {
        }

        UCS4String& operator = (const UCS4String& src)
        {
            if (this != &src)
            {
                m_buffer = src.m_buffer;
            }
            return *this;
        }

        size_t Length() const
        {
            return m_buffer.Length();
        }

        bool IsEmpty() const
        {
            return m_buffer.IsEmpty();
        }

        void Reset(void)
        {
            m_buffer.Reset();
        }

        virtual viewT GetBuffer() override
        {
            return m_buffer.GetBuffer();
        }

        const char32_t& operator[](size_t index) const
        {
            return m_buffer[index];
        }

        char32_t At(size_t index) const
        {
            return m_buffer.At(index);
        }

        std::basic_string<char32_t> ToStdString() const
        {
            return m_buffer.ToStdString();
        }

        operator std::basic_string<char32_t>() const
        {
            return ToStdString();
        }

        iterator begin() const
        {
            return iterator(m_buffer.begin());
        }

        const_iterator cbegin() const
        {
            return const_iterator(m_buffer.cbegin());
        }

        iterator end() const
        {
            return iterator(m_buffer.end());
        }

        const_iterator cend() const
        {
            return const_iterator(m_buffer.cend());
        }

        reverse_iterator rbegin() const
        {
            return reverse_iterator(m_buffer.rbegin());
        }

        const_reverse_iterator crbegin() const
        {
            return const_reverse_iterator(m_buffer.crbegin());
        }

        reverse_iterator rend() const
        {
            return reverse_iterator(m_buffer.rend());
        }

        const_reverse_iterator crend() const
        {
            return const_reverse_iterator(m_buffer.crend());
        }

        template<typename func_t>
        void ConstVisit(func_t&& func, size_t startIndex = 0) const
        {
            m_buffer.ConstVisit(std::forward<func_t>(func), startIndex);
        }

        template<typename func_t>
        void ConstReverseVisit(func_t&& func, size_t startIndex = (size_t)(-1)) const
        {
            m_buffer.ConstReverseVisit(std::forward<func_t>(func), startIndex);
        }

        Mib GetEncoding()
        {
            return defaultEncodingT::GetMib();
        }

        void SetEncoding(Mib charSet)
        {
            //UCS4String has fixed encoding
        }

        UCS4String Left(size_t charCount) const
        {
            return UCS4String(base::Left(charCount));
        }

    protected:
        BufferT m_buffer;
    };

}//qor

#endif//QOR_PP_H_TEXT_UCS4STRING
