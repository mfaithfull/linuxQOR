#ifndef QOR_PP_H_REF
#define QOR_PP_H_REF

#include <atomic>
#include "src/qor/sync/sync.h"
#include "src/qor/instance/instance.h"
#include "src/qor/memory/memory.h"
#include "sharedref.h"

namespace qor{

	//The outer public reference class is an augmented pointer to the inner shared reference
	template< class T > 
	class Ref final
	{
	public:	

		typedef detail::SharedRef<T> _tInternalRef;

		class Access final
		{
		public:
			Access(const Ref& r) : m(r)
			{
				m.Lock();
			}

			T* operator -> () const
			{
				return m.m_p->ptr();
			}

			T& operator ()()
			{
				return *(m.m_p->ptr());
			}

			~Access()
			{
				m.Unlock();
			}
			const Ref& m;
		};	

		constexpr Ref() : m_p(nullptr) {}

		Ref(const detail::SharedRef<T>* pt) : m_p(pt)
		{
			if (m_p)
			{
				m_p->AddRef();
			}
		}

		Ref(const Ref< T >& Src) : m_p(Src.m_p)
		{
			if (m_p)
			{
				m_p->AddRef();
			}
		}

		Ref(Ref< T >&& Src) noexcept
		{
			m_p = Src.m_p;
			Src.m_p = nullptr;
		}

		Ref& operator = (const Ref< T >& Src)
		{
			if (this != &Src)
			{
				Dispose();
				m_p = Src.m_p;
				if (m_p)
				{
					m_p->AddRef();
				}
			}
			return *this;
		}

		Ref& operator = (Ref< T >&& Src) noexcept
		{
			if (this != &Src)
			{
				Dispose();
				m_p = Src.m_p;
				if (m_p)
				{
					m_p->AddRef();
				}
			}
			return *this;
		}

		//Two references are equal if they refer to the same object
		bool operator == (const Ref< T >& Cmp) const
		{
			return (m_p == Cmp.m_p);
		}

		template< class D >
		operator Ref<D>(void) const
		{
			if (m_p && ( dynamic_cast<D*>(m_p->ptr()) != nullptr ))
			{
				return Ref<D>(reinterpret_cast<const detail::SharedRef<D>*>(m_p));
			}
			return Ref<D>();
		}

		//The overloaded operators double dereference through the inner pointer to provide access to the referenced object
		operator T* (void) const
		{
#ifndef NDEBUG			
			if(m_p && m_p->LockIsReal() && !m_p->IsLocked() && m_p->m_ulRefCount != 0)
			{
				throw std::logic_error("Synchronised object being accessed without auto locking. Did you forget a (qor_shared) ?");
			}
#endif
			return m_p ? m_p->ptr() : nullptr;
		}

		//Function operator returns a temporary for locked access to shared objects
		const Access operator()(void) const
		{
			if (m_p == nullptr)
			{
				throw std::logic_error("Null reference exception: A reference must refer to an object in order to be used.");
			}
			return Access(*this);
		}

		//Function operator returns a temporary for locked access to shared objects
		Access operator()(void)
		{
			if (m_p == nullptr)
			{
				throw std::logic_error("Null reference exception: A reference must refer to an object in order to be used.");
			}
			return Access(*this);
		}

		T* operator -> () const
		{
			if (m_p == nullptr)
			{
				throw std::logic_error("Null reference exception: A reference must refer to an object in order to be used.");
			}
#ifndef NDEBUG
			if(m_p->LockIsReal() && !m_p->IsLocked() && m_p->m_ulRefCount != 0)
			{
				throw std::logic_error("Synchronised object being accessed without auto locking. Did you forget a (qor_shared) ?");
			}
#endif
			return m_p->ptr();			
		}

		operator const T& () const
		{
			if (m_p == nullptr)
			{
				throw std::logic_error("Null reference exception: A reference must refer to an object in order to be used.");
			}
#ifndef NDEBUG
			if(m_p->LockIsReal() && !m_p->IsLocked() && m_p->m_ulRefCount != 0)
			{
				throw std::logic_error("Synchronised object being accessed without auto locking. Did you forget a (qor_shared) ?");
			}
#endif
			return const_cast<const T&>(*(m_p->ptr()));
		}

		~Ref()
		{
			Dispose();
		}

		void Dispose(void)
		{
			if (m_p)
			{
				(const_cast<detail::SharedRef<T>*>(m_p))->Release();
			}
			m_p = nullptr;
		}

		Ref<T>& DeOwn()
		{
			if (m_p)
			{
				(const_cast<detail::SharedRef<T>*>(m_p))->Release();
			}
			m_p = nullptr;
			return *this;
		}

		Ref<T>& Reset()
		{
			if (m_p)
			{
				(const_cast<detail::SharedRef<T>*>(m_p))->Reset();
			}
			m_p = nullptr;
			return *this;
		}

		void Attach(T* pt)
		{
			if( m_p && m_p->ptr() == nullptr)
			{
				m_p->Attach(pt);
			}
		}

		template< class TDerived >
		TDerived* As(void) const
		{
			return m_p ? dynamic_cast< TDerived* >(m_p->ptr()) : nullptr;
		}

		template< class TDerived >
		Ref< TDerived > AsRef(void)
		{
			if (m_p && ( dynamic_cast<TDerived*>(m_p->ptr()) != nullptr ))
			{
				return Ref< TDerived >(reinterpret_cast<const detail::SharedRef<TDerived>*>(m_p));
			}
			return Ref< TDerived >();
		}

		Ref<T> Clone()
		{
			if(m_p)
			{
				return Ref<T>(m_p);
			}
			return Ref<T> ();
		}

		bool IsNull(void) const
		{
			return m_p == nullptr || (m_p->IsNull());
		}

		bool IsNotNull(void) const
		{
			return m_p != nullptr && (!m_p->IsNull());
		}

		bool IsOwner(void) const
		{
			return (m_p && m_p->m_ulRefCount == 1) ? true : false;
		}

		template< class TDerived >
		bool Configure()
		{
			return const_cast<detail::SharedRef<T>*>(m_p)->Configure();
		}

		template< class TDerived, typename... _p >
		bool Configure(_p&&... p1)
		{
			return const_cast<detail::SharedRef<T>*>(m_p)->Configure(std::forward<_p>(p1)...);
		}

		void Lock() const
		{
			if (m_p)
			{
				m_p->Lock();
			}
		}

		void Unlock() const
		{
			if (m_p)
			{
				m_p->Unlock();
			}
		}

	protected:

		//A pointer to the shared reference, behaves like a pointer to the object itself.
		const detail::SharedRef<T>* m_p;
	};

}//qor

#endif//QOR_PP_H_REF
