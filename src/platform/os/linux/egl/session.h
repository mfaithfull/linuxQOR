#ifndef QOR_PP_H_LINUX_EGL_SESSION
#define QOR_PP_H_LINUX_EGL_SESSION

#include "src/framework/thread/currentthread.h"
#include "src/qor/reference/newref.h"
#include "display.h"

namespace qor{ namespace platform { namespace nslinux{

    template<class NativeSession>
    class EGLSession : public NativeSession
    {
    public:

        template<class NativeDisplayRef>
        EGLSession(NativeDisplayRef nativeDisplay) : NativeSession(nativeDisplay)
        {
            m_eglDisplay = new_ref<EglDisplay>(NativeSession::GetDisplay()->Use());
            m_eglDisplay->Initialize();
        }

        virtual ~EGLSession()
        {
        }

        virtual ref_of<EglDisplay>::type GetEGLDisplay()
        {
            return m_eglDisplay;
        }

    protected:

        ref_of<EglDisplay>::type m_eglDisplay;        
    };

    template<class NativeWindow>
    class EglWindow : public NativeWindow
    {
    public:

        template<class SessionRef>
        EglWindow(SessionRef sessionRef, const std::vector<int32_t>& contextAttributes) : NativeWindow(sessionRef), m_eglDisplay(sessionRef->GetEGLDisplay())
        {                        
            m_eglContext = new_ref<qor::components::EGLContext>(m_eglDisplay, nullptr, contextAttributes.data());
        }

        virtual ~EglWindow()
        {            
        }

        virtual void DoConfiguration()
        {
            NativeWindow::DoConfiguration();
            int32_t surfaceAttributes[] = { EGL_NONE };
            m_eglSurface = m_eglDisplay->CreateWindowSurface(m_eglDisplay->GetConfig(), NativeWindow::Use(), surfaceAttributes);
            m_eglContext->MakeCurrent(m_eglSurface, m_eglSurface);
        }

        void Present()
        {
            m_eglDisplay->SwapBuffers(m_eglSurface);
        }

    protected:        
        qor::ref_of<EglDisplay>::type m_eglDisplay;
        qor::ref_of<qor::components::EGLContext>::type m_eglContext;
        void* m_eglSurface;
    };

}}}//qor::platform::nslinux

#endif//QOR_PP_H_LINUX_EGL_SESSION
