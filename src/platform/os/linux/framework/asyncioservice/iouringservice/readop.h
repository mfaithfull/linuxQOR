#ifndef QOR_PP_H_OS_LINUX_FRAMEWORK_ASYNCIOSERVICE_IOURING_READOP
#define QOR_PP_H_OS_LINUX_FRAMEWORK_ASYNCIOSERVICE_IOURING_READOP

#include <system_error>
#include <coroutine>
#include "iouringawaiter.h"

namespace qor{ namespace nslinux{ namespace framework{

    class ReadOperation
    {
        public:

        ReadOperation(IOUring &uring, int fd ,byte* buffer, int32_t len) : m_sqe(uring.GetSQE())
        {
            m_sqe.PrepareRead( fd, buffer, len, 0);
        }
        
        auto operator co_await() 
        {
            return IOUringAwaiter{m_sqe};
        }
        
    private:
        IOUring::SQE m_sqe;
    };

}}}//qor::nslinux::framework

#endif//QOR_PP_H_OS_LINUX_FRAMEWORK_ASYNCIOSERVICE_READOP