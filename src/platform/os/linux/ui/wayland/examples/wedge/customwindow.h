#ifndef QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMWINDOW
#define QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMWINDOW

#include "sdk/platform/os/linux/wayland.h"
#include "src/platform/os/linux/ui/wayland/egl/eglwindow.h"

class CustomWindow : public EGLWindowWrapper<wl::WEGLWindow>
{
public:

    CustomWindow(ref_of<EGLSessionWrapper<wl::XDGSession, EglDisplay>>::type session, const std::vector<int32_t>& contextAttributes, int x, int y, unsigned int width, unsigned int height) : 
        qor::components::EGLWindowWrapper<wl::WEGLWindow>(session, contextAttributes, x, y, width, height), m_session(session)
    {
        m_canvas = nullptr;
        m_gl = GetFeature<OpenGLESFeature>();                        
        DoConfiguration();//Need to do this for every XDGWindow to connect up the callbacks to our derived class                        
        m_canvas = ui::renderer::GlCanvas::gen(m_gl);

        DrawRetained();        
        DrawFrame();

        m_positioner = m_session->GetXDGWMBase()->CreatePositioner();
        m_positioner->SetOffset(0,0);
        m_positioner->SetSize(m_width / 2, m_height / 2);
        m_positioner->SetAnchorRect(0,0,m_width, m_height);

    }

    virtual ~CustomWindow()
    {        
        if(m_popup)
        {
            delete m_popup;
            m_popup = nullptr;
        }
        delete m_canvas;
        m_canvas = nullptr;
    }

    virtual void OnResize()//Override to handle resize happening to this window, e.g. from a Window Manager
    {   
        if(m_canvas)
        {
            static_cast<ui::renderer::GlCanvas*>(m_canvas)->target(m_eglContext->Use(), 0, m_width, m_height, ui::renderer::ColorSpace::ABGR8888S);         
        }
    }

    virtual void DrawRetained()
    {
        auto border = ui::renderer::Shape::gen();        
        border->appendRect(6,6,628,468, 32, 24);
        border->strokeWidth(12);
        border->strokeFill(161,161,192, 172);
        m_canvas->push(border);

        auto close = ui::renderer::Shape::gen();
        close->appendCircle(639 - 32, 1 + 24, 32, 24);
        close->fill(192,0,0,172);

        auto max = ui::renderer::Shape::gen();
        max->appendCircle(639 - (32+64), 1 + 24, 32, 24);
        max->fill(192,0,0,172);

        auto min = ui::renderer::Shape::gen();
        min->appendCircle(639 - (32+128), 1 + 24, 32, 24);
        min->fill(192,0,0,172);

        m_canvas->push(close);
        m_canvas->push(max);
        m_canvas->push(min);

        auto fine = ui::renderer::Shape::gen();
        fine->appendRect(320,0,0,240,0,0);
        fine->strokeWidth(1);
        fine->strokeFill(255,255,255);
        m_canvas->push(fine);

        auto shape1 = ui::renderer::Shape::gen();
        shape1->appendCircle(200, 200, 150, 100);    //cx, cy, radiusW, radiusH        
        shape1->fill(192, 255, 255, 172);                 //r, g, b, a
        m_canvas->push(shape1);
    }

    virtual void DrawFrame(uint32_t serial = 0)
    {        
        m_canvas->draw(true);
        m_canvas->sync();
    }

    virtual void OnClose()
    {
        m_session->End();
    }
    
    void Popup()
    {
        if(!m_popup)
        {
            m_popup = new CustomPopup(m_session, this, m_positioner);                       //Create a popup
        }
        else
        {
            delete m_popup;
            m_popup = nullptr;
        }
    }

protected:

    ref_of<wl::XDGPositioner>::type m_positioner;
    CustomPopup* m_popup;

    ref_of<EGLSessionWrapper<wl::XDGSession, EglDisplay>>::type m_session;    
    ref_of<OpenGLESFeature>::type m_gl;
    ui::renderer::Canvas* m_canvas;
};

#endif//QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMWINDOW