    XTest::XTest(Display* dpy) : m_display(dpy)
    {        
        XExtCodes* data = XInitExtension((::Display*)(m_display->Use()), "XTEST");
        m_extCodes = *(reinterpret_cast<ExtCodes*>(data));
        XFree(data);
    }

