    const std::vector<int> Display::ListDepths(int screen_number)
    {
        std::vector<int> result;
        int count = 0;
        int* depths = XListDepths((::Display*)(m_display), screen_number, &count);
        if(depths != nullptr)
        {
            result.resize(count);
            memcpy(result.data(), depths, count);
            XFree(depths);
        }
        return result;
    }
    
