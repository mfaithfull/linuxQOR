#undef BlackPixel
#undef WhitePixel
#undef DefaultDepth
#undef DefaultGC
#undef DefaultVisual
#undef DoesBackingStore
#undef DoesSaveUnders
#undef DisplayOfScreen
#undef RootWindow

#define WITH_THIS (::Screen*)(m_screen)

namespace qor{ namespace platform { namespace nslinux{ namespace x{

    Screen::Screen(void* screen, Display* display) : m_screen(screen), m_display(display)
    {
    }

    Screen::~Screen()
    {        
    }

    unsigned long Screen::BlackPixel()
    {
        return XBlackPixelOfScreen(WITH_THIS);
    }

    unsigned long Screen::WhitePixel()
    {
        return XWhitePixelOfScreen(WITH_THIS);
    }

    int Screen::Cells()
    {
        return XCellsOfScreen(WITH_THIS);
    }

    unsigned long Screen::DefaultColourMap()
    {
        return XDefaultColormapOfScreen(WITH_THIS);
    }

    int Screen::DefaultDepth()
    {
        return XDefaultDepthOfScreen(WITH_THIS);
    }

    GC Screen::DefaultGC()
    {
        return GC(m_display, XDefaultGCOfScreen(WITH_THIS));//default context do not free
    }

    Visual Screen::DefaultVisual()
    {
        return Visual(XDefaultVisualOfScreen(WITH_THIS));
    }

    int Screen::DoesBackingStore()
    {
        return XDoesBackingStore(WITH_THIS);//WhenMapped, NotUseful, Always
    }

    bool Screen::DoesSaveUnders()
    {
        return XDoesSaveUnders(WITH_THIS) ? true : false;
    }

    Display Screen::DisplayOfScreen()
    {
        return XDisplayOfScreen(WITH_THIS);
    }

    long Screen::ScreenNumber()
    {
        return XScreenNumberOfScreen(WITH_THIS);
    }

    long Screen::EventMask()
    {
        return XEventMaskOfScreen(WITH_THIS);
    }

    int Screen::Width()
    {
        return XWidthOfScreen(WITH_THIS);//pixels
    }

    int Screen::Height()
    {
        return XHeightOfScreen(WITH_THIS);//pixels
    }

    int Screen::WidthMM()
    {
        return XWidthMMOfScreen(WITH_THIS);
    }

    int Screen::HeightMM()
    {
        return XHeightMMOfScreen(WITH_THIS);
    }

    int Screen::MaxCmaps()
    {
        return XMaxCmapsOfScreen(WITH_THIS);//max colour maps supported
    }

    int Screen::Planes()
    {
        return XPlanesOfScreen(WITH_THIS);
    }

    Window Screen::RootWindow()
    {
        return Window(XRootWindowOfScreen(WITH_THIS), m_display);
    }

    int Screen::MatchVisualInfo(int depth, int c_class, VisualInfo& visualInfo)
    {
        return XMatchVisualInfo((::Display*)(m_display->Use()), ScreenNumber(), depth, c_class, reinterpret_cast<XVisualInfo*>(&visualInfo));
    }

}}}}//qor::platform::nslinux::x

#undef WITH_THIS