    Provider::Provider() : m_display{0}, m_Id{0}
    {        
    }
    
    Provider::Provider(Display* dpy, unsigned long Id) : m_display(dpy), m_Id(Id)
    {        
    }
    
    Provider::~Provider()
    {        
    }

    int Provider::SetOutputSource(unsigned long sourceProvider)
    {
        return XRRSetProviderOutputSource((::Display*)(m_display->Use()), m_Id, sourceProvider);
    }

    int Provider::SetOffloadSink(unsigned long sinkProvider)
    {
        return XRRSetProviderOffloadSink((::Display*)(m_display->Use()), m_Id, sinkProvider);
    }

    std::vector<unsigned long> Provider::ListProperties()
    {
        std::vector<unsigned long> properties;
        int count = 0;
        Atom* props = XRRListProviderProperties((::Display*)(m_display->Use()), m_Id, &count);
        if(count != 0 && props != nullptr)
        {
            properties.resize(count);
            memcpy(properties.data(), props, sizeof(unsigned long) * count);
            XFree(props);
        }
        return properties;
    }

    PropertyInfo Provider::QueryProperty(unsigned long property)
    {
        XRRPropertyInfo* data = XRRQueryProviderProperty((::Display*)(m_display->Use()), m_Id, property);
        return PropertyInfo(data);
    }

    void Provider::ConfigureProperty(unsigned long property, int pending, int range, std::vector<long>& values)
    {
        XRRConfigureProviderProperty((::Display*)(m_display->Use()), m_Id, property, pending, range, values.size(), values.data());
    }
    
    void Provider::ChangeProperty(unsigned long property, unsigned long type, int format, int mode, const std::vector<unsigned char>& data)
    {
        XRRChangeProviderProperty((::Display*)(m_display->Use()), m_Id, property, type, format, mode, data.data(), data.size());
    }

    void Provider::DeleteProperty(unsigned long property)
    {
        XRRDeleteProviderProperty((::Display*)(m_display->Use()), m_Id, property);
    }
    
    int Provider::GetProperty(unsigned long property, long offset, long length, int _delete, int pending, unsigned long reqType, unsigned long& actualType, int& actualFormat, unsigned long& bytesAfter, std::vector<unsigned char>& value)
    {
        unsigned char* prop = nullptr;
        unsigned long nitems = 0;
        int status = XRRGetProviderProperty((::Display*)(m_display->Use()), m_Id, property, offset, length, _delete, pending, reqType, &actualType, &actualFormat, &nitems, &bytesAfter, &prop);
        if(nitems != 0 && prop != nullptr)
        {
            value.clear();//TODO:
            value.resize(nitems * (actualFormat / 8));
            memcpy(value.data(), prop, nitems * (actualFormat / 8));
            XFree(prop);
        }
        return status;
    }

