    XShm::XShm(Display* dpy) : m_display(dpy)
    {        
        XExtCodes* data = XInitExtension((::Display*)(m_display->Use()), "MIT-SHM");
        m_extCodes = *(reinterpret_cast<ExtCodes*>(data));
        XFree(data);
    }

    XShm::~XShm()
    {
    }

    int XShm::QueryExtension()
    {
        return XShmQueryExtension((::Display*)(m_display->Use()));
    }

    int XShm::GetEventBase()
    {
        return XShmGetEventBase((::Display*)(m_display->Use()));
    }

    int XShm::QueryVersion(int& majorVersion, int& minorVersion, int& sharedPixmaps)
    {
        return XShmQueryVersion((::Display*)(m_display->Use()), &majorVersion, &minorVersion, &sharedPixmaps);
    }

    int XShm::PixmapFormat()
    {
        return XShmPixmapFormat((::Display*)(m_display->Use()));
    }

    int XShm::Attach(ShmSegmentInfo& shminfo )
    {
        return XShmAttach((::Display*)(m_display->Use()), reinterpret_cast<XShmSegmentInfo*>(&shminfo));
    }
    
    int XShm::Detach(ShmSegmentInfo& shminfo )
    {
        return XShmDetach((::Display*)(m_display->Use()), reinterpret_cast<XShmSegmentInfo*>(&shminfo));
    }

    int XShm::PutImage(unsigned long drawable, GC gc, Image& image, int src_x, int src_y, int dst_x, int dst_y, unsigned int src_width, unsigned int src_height, int send_event)
    {
        return XShmPutImage((::Display*)(m_display->Use()), drawable, (::GC)(gc.Use()), (::XImage*)image.GetImage(), src_x, src_y, dst_x, dst_y, src_width, src_height, send_event);
    }

    int XShm::GetImage(unsigned long drawable, Image& image, int x, int y, unsigned long planeMask)
    {
        return XShmGetImage((::Display*)(m_display->Use()), drawable, (::XImage*)image.GetImage(), x, y, planeMask);
    }

    Image XShm::CreateImage(Visual* visual, unsigned int depth, int format, char* data, ShmSegmentInfo&	shminfo, unsigned int width, unsigned int height)
    {
        return Image(m_display, (void*)(XShmCreateImage((::Display*)(m_display->Use()), (::Visual*)(visual->Use()), depth, format, data, reinterpret_cast<XShmSegmentInfo*>(&shminfo), width, height)));
    }

    Pixmap XShm::CreatePixmap(unsigned long drawable, char* data, ShmSegmentInfo& shminfo, unsigned int width, unsigned int height, unsigned int depth)
    {
        return Pixmap(m_display, XShmCreatePixmap((::Display*)(m_display->Use()), drawable, data, reinterpret_cast<XShmSegmentInfo*>(&shminfo), width, height, depth));
    }
    
