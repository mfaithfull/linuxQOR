    {
    }

    EGLSession::~EGLSession()
    {
    }

    void EGLSession::OnXDGSurfaceConfigured()
    {
    }

    void EGLSession::OnXDGTopLevelConfigured(int32_t width, int32_t height, struct wl_array* states)
    {
        if(!width && !height) return;

	    if(m_width != width || m_height != height) 
        {
		    m_width = width;
		    m_height = height;
		    m_window->Resize(m_width, m_height, 0, 0);
            m_window->BaseSurface()->Commit();
    	}        
    }

    qor::ref_of<EGLWindow>::type EGLSession::CreateEGLWindow()
    {
        m_window = new_ref<EGLWindow>(wl_egl_window_create(m_xdgSurface->BaseSurface()->Use(), m_width, m_height), m_xdgSurface->BaseSurface());
        return m_window;
    }
    
