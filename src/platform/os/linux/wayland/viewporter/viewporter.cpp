namespace qor{ namespace platform { namespace nslinux{ namespace wl{

    const char* const Viewporter::TagName = "QOR::PLATFORM::NSLINUX::WL::VIEWPORTER";

    Viewporter* Viewporter::ViewporterFrom(wp_viewporter* vp)
    {
        if(!vp)
        {
            return nullptr;
        }
        Viewporter* result = reinterpret_cast<Viewporter*>(wp_viewporter_get_user_data(vp));
        if(result && result->Tag() == TagName)
        {
            return result;
        }
        else if(result)
        {
            continuable("Wayland wp_viewporter user data tag mismatch");
        }
        return new Viewporter(vp);
    }

    Viewporter::Viewporter(wp_viewporter* vp) : m_vp(vp)
    {
        if(m_vp)
        {
            wp_viewporter_set_user_data(m_vp, this);
        }
        else
        {
            continuable("Viewporter created with null wp_viewporter pointer");
        }
    }

    Viewporter::Viewporter(Viewporter&& rhs) noexcept : m_vp(rhs.m_vp)
    {
        rhs.m_vp = nullptr;
        if (m_vp)
        {
            wp_viewporter_set_user_data(m_vp, this);
        }
    }

    Viewporter& Viewporter::operator=(Viewporter&& rhs) noexcept
    {
        if (this != &rhs)
        {
            if (m_vp)
            {
                wp_viewporter_destroy(m_vp);
            }
            m_vp = rhs.m_vp;
            rhs.m_vp = nullptr;
            if (m_vp)
            {
                wp_viewporter_set_user_data(m_vp, this);
            }
        }
        return *this;
    }

    Viewporter::~Viewporter()
    {
        if(m_vp)
        {
            wp_viewporter_destroy(m_vp);
        }
    }

    wp_viewporter* Viewporter::Use() const
    {
        if(!m_vp)
        {
            warning("Using Viewporter with null wp_viewporter pointer");
        }
        return m_vp;
    }

    uint32_t Viewporter::Version() const
    {
        if(!m_vp)
        {
            warning("Getting version of Viewporter with null wp_viewporter pointer");
            return 0;
        }
        return wp_viewporter_get_version(m_vp);
    }

    qor::ref_of<Viewport>::type Viewporter::GetViewport(Surface* surface)
    {
        return new_ref<Viewport>(wp_viewporter_get_viewport(m_vp, surface ? surface->Use() : nullptr));
    }

}}}}//qor::platform::nslinux::wl
