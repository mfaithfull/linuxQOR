    const char* const XDGWMBase::TagName = "QOR::PLATFORM::NSLINUX::WL::XDGWMBASE";

    XDGWMBase* XDGWMBase::XDGWMBaseFrom(xdg_wm_base* wmbase)
    {
        if(!wmbase)
        {
            return nullptr;
        }
        XDGWMBase* result = reinterpret_cast<XDGWMBase*>(xdg_wm_base_get_user_data(wmbase));
        if(result && result->Tag() == TagName)
        {
            return result;
        }
        else if(result)
        {
            continuable("Wayland xdg_wm_base user data tag mismatch");
        }
        return new XDGWMBase(wmbase);
    }

    XDGWMBase::XDGWMBase(Registry* registry, uint32_t name, uint32_t version)
    {
        m_wmbase = reinterpret_cast<xdg_wm_base*>(registry->Bind(name, version, xdg_wm_base_interface));
        if(m_wmbase)
        {
            xdg_wm_base_set_user_data(m_wmbase, this);
        }
        else
        {
            continuable("XDGWMBase created with null xdg_wm_base pointer");
        }
    }

    XDGWMBase::XDGWMBase(xdg_wm_base* wmbase) : m_wmbase(wmbase)
    {
        if(m_wmbase)
        {
            xdg_wm_base_set_user_data(m_wmbase, this);
        }
        else
        {
            continuable("XDGWMBase created with null xdg_wm_base pointer");
        }
    }

    XDGWMBase::XDGWMBase(XDGWMBase&& rhs) noexcept : m_wmbase(rhs.m_wmbase)
    {
        rhs.m_wmbase = nullptr;
        if (m_wmbase)
        {
            xdg_wm_base_set_user_data(m_wmbase, this);
        }
    }

    XDGWMBase& XDGWMBase::operator=(XDGWMBase&& rhs) noexcept
    {
        if (this != &rhs)
        {
            if (m_wmbase)
            {
                xdg_wm_base_destroy(m_wmbase);
            }
            m_wmbase = rhs.m_wmbase;
            rhs.m_wmbase = nullptr;
            if (m_wmbase)
            {
                xdg_wm_base_set_user_data(m_wmbase, this);
            }
        }
        return *this;
    }

    XDGWMBase::~XDGWMBase()
    {
        if(m_wmbase)
        {
            xdg_wm_base_destroy(m_wmbase);
        }
    }

    xdg_wm_base* XDGWMBase::Use() const
    {
        if(!m_wmbase)
        {
            warning("Using XDGWMBase with null xdg_wm_base pointer");
        }
        return m_wmbase;
    }

    uint32_t XDGWMBase::Version() const
    {
        if(!m_wmbase)
        {
            warning("Getting version of XDGWMBase with null xdg_wm_base pointer");
            return 0;
        }
        return xdg_wm_base_get_version(m_wmbase);
    }

    int XDGWMBase::AddListener(const xdg_wm_base_listener& listener, void* context)
    {
        if(!m_wmbase)
        {
            warning("Adding listener to XDGWMBase with null xdg_wm_base pointer");
            return -1;
        }
        return xdg_wm_base_add_listener(m_wmbase, &listener, context);
    }

    void XDGWMBase::Pong(uint32_t serial)
    {
        if(!m_wmbase)
        {
            warning("Ponging XDGWMBase with null xdg_wm_base pointer");
        }
        else
        {
            xdg_wm_base_pong(m_wmbase, serial);
        }
    }

    qor::ref_of<XDGPositioner>::type XDGWMBase::CreatePositioner()
    {
        if(!m_wmbase)
        {
            warning("Creating positioner on XDGWMBase with null xdg_wm_base pointer");
            return nullptr;
        }
        return new_ref<XDGPositioner>(xdg_wm_base_create_positioner(m_wmbase));
    }

    qor::ref_of<XDGSurface>::type XDGWMBase::GetXDGSurface(qor::ref_of<Surface>::type surface)
    {
        if(!m_wmbase)
        {
            warning("Getting XDGSurface from XDGWMBase with null xdg_wm_base pointer");
            return nullptr;
        }
        return new_ref<XDGSurface>(xdg_wm_base_get_xdg_surface(m_wmbase, surface->Use()), surface);
    }        

    void XDGWMBase::OnPing(void* context, uint32_t serial){
        Pong(serial);
    }

