    const char* const XDGTopLevel::TagName = "QOR::PLATFORM::NSLINUX::WL::XDGTOPLEVEL";

    XDGTopLevel* XDGTopLevel::XDGTopLevelFrom(xdg_toplevel* toplevel)
    {
        if(!toplevel)
        {
            return nullptr;
        }
        XDGTopLevel* result = reinterpret_cast<XDGTopLevel*>(xdg_toplevel_get_user_data(toplevel));
        if(result && result->Tag() == TagName)
        {
            return result;
        }
        else if(result)
        {
            continuable("Wayland xdg_toplevel user data tag mismatch");
        }
        return new XDGTopLevel(toplevel);
    }

    XDGTopLevel::XDGTopLevel(xdg_toplevel* toplevel) : m_toplevel(toplevel)
    {
        if(toplevel)
        {
            xdg_toplevel_set_user_data(m_toplevel, this);
        }
        else
        {
            continuable("Surface created with null xdg_toplevel pointer");
        }        
    }

    XDGTopLevel::XDGTopLevel(XDGTopLevel&& rhs) noexcept : m_toplevel(rhs.m_toplevel)
    {
        rhs.m_toplevel = nullptr;
        if (m_toplevel)
        {
            xdg_toplevel_set_user_data(m_toplevel, this);
        }
    }

    XDGTopLevel& XDGTopLevel::operator=(XDGTopLevel&& rhs) noexcept
    {
        if (this != &rhs)
        {
            if (m_toplevel)
            {
                xdg_toplevel_destroy(m_toplevel);
            }

            m_toplevel = rhs.m_toplevel;
            rhs.m_toplevel = nullptr;

            if (m_toplevel)
            {
                xdg_toplevel_set_user_data(m_toplevel, this);
            }
        }
        return *this;
    }

    XDGTopLevel::~XDGTopLevel()
    {
        if(m_toplevel)
        {
            xdg_toplevel_destroy(m_toplevel);
        }
    }

    xdg_toplevel* XDGTopLevel::Use() const
    {
        if(!m_toplevel)
        {
            warning("Using XDGTopLevel with null xdg_toplevel pointer");
        }
        return m_toplevel;
    }

    uint32_t XDGTopLevel::Version() const
    {
        if(!m_toplevel)
        {
            warning("Getting version of XDGTopLevel with null xdg_toplevel pointer");
            return 0;
        }
        return xdg_toplevel_get_version(m_toplevel);
    }
   
    void XDGTopLevel::AddListener(const xdg_toplevel_listener& listener, void* data)
    {
        if(m_toplevel)
        {
            xdg_toplevel_add_listener(m_toplevel, &listener, data);
        }
    }
    
