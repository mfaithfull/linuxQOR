    const char* const XDGSurface::TagName = "QOR::PLATFORM::NSLINUX::WL::XDGSURFACE";

    XDGSurface* XDGSurface::XDGSurfaceFrom(xdg_surface* surface)
    {
        if(!surface)
        {
            return nullptr;
        }
        XDGSurface* result = reinterpret_cast<XDGSurface*>(xdg_surface_get_user_data(surface));
        if(result && result->Tag() == TagName)
        {
            return result;
        }
        else if(result)
        {
            continuable("Wayland xdg_surface user data tag mismatch");
        }
        return new XDGSurface(surface, qor::ref_of<Surface>::type());
    }

    XDGSurface::XDGSurface(xdg_surface* surface, qor::ref_of<Surface>::type baseSurface) : m_surface(surface), m_baseSurface(baseSurface)
    {
        if(surface)
        {
            xdg_surface_set_user_data(m_surface, this);
        }
        else
        {
            continuable("Surface created with null xdg_surface pointer");
        }        
    }

    XDGSurface::XDGSurface(XDGSurface&& rhs) noexcept : m_surface(rhs.m_surface), m_baseSurface(rhs.m_baseSurface)
    {
        rhs.m_surface = nullptr;
        rhs.m_baseSurface.Dispose();
        if (m_surface)
        {
            xdg_surface_set_user_data(m_surface, this);
        }
    }

    XDGSurface& XDGSurface::operator=(XDGSurface&& rhs) noexcept
    {
        if (this != &rhs)
        {
            if (m_surface)
            {
                xdg_surface_destroy(m_surface);
            }

            if(m_baseSurface.IsNotNull())
            {
                m_baseSurface.Dispose();
            }

            m_surface = rhs.m_surface;
            m_baseSurface = rhs.m_baseSurface;
            rhs.m_surface = nullptr;
            rhs.m_baseSurface.Dispose();

            if (m_surface)
            {
                xdg_surface_set_user_data(m_surface, this);
            }
        }
        return *this;
    }

    XDGSurface::~XDGSurface()
    {
        if(m_surface)
        {
            xdg_surface_destroy(m_surface);
        }
    }

    xdg_surface* XDGSurface::Use() const
    {
        if(!m_surface)
        {
            warning("Using XDGSurface with null xdg_surface pointer");
        }
        return m_surface;
    }

    uint32_t XDGSurface::Version() const
    {
        if(!m_surface)
        {
            warning("Getting version of XDGSurface with null xdg_surface pointer");
            return 0;
        }
        return xdg_surface_get_version(m_surface);
    }

    void XDGSurface::AckConfigure(uint32_t serial)
    {
        if(m_surface)
        {
            xdg_surface_ack_configure(m_surface, serial);
        }
    }

    void XDGSurface::AddListener(const xdg_surface_listener& listener, void* data)
    {
        if(m_surface)
        {
            xdg_surface_add_listener(m_surface, &listener, data);
        }
    }

    qor::ref_of<XDGPopup>::type XDGSurface::GetPopup(XDGSurface* parentSurface, XDGPositioner* positioner)
    {
        if(m_surface)
        {
             return new_ref<XDGPopup>(xdg_surface_get_popup(m_surface, parentSurface ? parentSurface->Use() : nullptr, positioner ? positioner->Use() : nullptr));
        }
        return nullptr;
    }

    qor::ref_of<XDGTopLevel>::type XDGSurface::GetToplevel()
    {
        if(m_surface)
        {
            return new_ref<XDGTopLevel>(xdg_surface_get_toplevel(m_surface));
        }
        return nullptr;
    }

    void XDGSurface::SetWindowGeometry(int32_t x, int32_t y, int32_t width, int32_t height)
    {
        if(m_surface)
        {
            xdg_surface_set_window_geometry(m_surface, x, y, width, height);
        }
    }

    void XDGSurface::OnConfigure(void* context, uint32_t serial)
    {
            /* Override in derived class 
            surface configure
            This event is sent when the compositor wants to change the
            size or state of the surface. The client must respond by
            calling xdg_surface_ack_configure with the serial number
            provided.

            The width and height arguments specify the new size of the
            surface's content area in surface-local coordinates. A value
            of 0 for either dimension means that the surface can choose
            its own size.

            The states argument is an array of uint32_t values taken
            from the enum xdg_surface_state. It describes the states
            that should be applied to the surface.
        */
        AckConfigure(serial);        
        if(m_session)
        {
            m_session->OnXDGSurfaceConfigured();
        }
    }

    void XDGSurface::SetSession(XDGSession* session)
    {
        m_session = session;
    }

    qor::ref_of<Surface>::type XDGSurface::BaseSurface()
    {
        return m_baseSurface;
    }
