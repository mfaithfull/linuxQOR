#include "src/configuration/configuration.h"
#include "src/qor/error/error.h"

#include "datadevicelistener.h"
#include "../datadevice.h"

namespace qor{ namespace platform { namespace nslinux{ namespace wl{

    DataDeviceListener::DataDeviceListener()
    {
        wl_data_device_listener::data_offer = [](void* data, wl_data_device* wl_data_device, wl_data_offer* id)
        {
            DataDevice* device = DataDevice::DataDeviceFrom(wl_data_device);
            if(device)
            {
                device->OnDataOffer(data, id);
            }
        };
        wl_data_device_listener::enter = [](void *data, struct wl_data_device *wl_data_device, uint32_t serial, struct wl_surface *surface, wl_fixed_t x, wl_fixed_t y, struct wl_data_offer *id)
        {
            DataDevice* device = DataDevice::DataDeviceFrom(wl_data_device);
            if(device)
            {
                device->OnDataEnter(data, serial, surface, x, y, id);
            }
        };
        wl_data_device_listener::leave = [](void* data, wl_data_device* wl_data_device)
        {
            DataDevice* device = DataDevice::DataDeviceFrom(wl_data_device);
            if(device)
            {
                device->OnDataLeave(data);
            }
        };
        wl_data_device_listener::motion = [](void* data, wl_data_device* wl_data_device, uint32_t time, int32_t x, int32_t y)
        {
            DataDevice* device = DataDevice::DataDeviceFrom(wl_data_device);
            if(device)
            {
                device->OnDataMotion(data, time, x, y);
            }
        };
        wl_data_device_listener::drop = [](void* data, wl_data_device* wl_data_device)
        {
            DataDevice* device = DataDevice::DataDeviceFrom(wl_data_device);
            if(device)
            {
                device->OnDataDrop(data);
            }
        };
        wl_data_device_listener::selection = [](void* data, wl_data_device* wl_data_device, wl_data_offer* id)
        {
            DataDevice* device = DataDevice::DataDeviceFrom(wl_data_device);
            if(device)
            {
                device->OnDataSelection(data, id);
            }
        };
    }
        
}}}}//qor::platform::nslinux::wl
