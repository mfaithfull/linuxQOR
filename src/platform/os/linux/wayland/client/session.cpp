    const char* const Session::TagName = "QOR::PLATFORM::NSLINUX::WL::SESSION";

    Session::Session(qor::ref_of<Display>::type display) : m_Display(display)
    {        
        m_Registry = m_Display->GetRegistry();//Get the global registry object      
        m_Registry->AddDefaultListener(this);//Attach the registry to our session with a default listener so it tells the Session about the available protocols
        //Make the internals do their thing until all pending events have been processed
        m_Display->Dispatch();
        m_Display->Roundtrip();
    }

    Session::~Session()
    {
    }

    void Session::SetCompositor( qor::ref_of<Compositor>::type compositor )
    {
        m_Compositor = compositor;
    }

    qor::ref_of<Compositor>::type Session::GetCompositor()
    {
        return m_Compositor;
    }

    void Session::SetDataDeviceManager( qor::ref_of<DataDeviceManager>::type dataDeviceManager )
    {
        m_DataDeviceManager = dataDeviceManager;
    }

    qor::ref_of<DataDeviceManager>::type Session::GetDataDeviceManager()
    {
        return m_DataDeviceManager;
    }

    void Session::SetShell( qor::ref_of<Shell>::type shell )
    {
        m_Shell = shell;
    }

    qor::ref_of<Shell>::type Session::GetShell()
    {
        return m_Shell;
    }

    void Session::SetShm( qor::ref_of<SharedMemory>::type shm )
    {
        m_Shm = shm;
    }

    qor::ref_of<SharedMemory>::type Session::GetShm()
    {
        return m_Shm;
    }

    void Session::AddOutput( qor::ref_of<Output>::type output, uint32_t name )
    {
        m_Outputs[name] = output;
    }

    qor::ref_of<Output>::type Session::GetOutput( uint32_t name )
    {
        auto it = m_Outputs.find(name);
        if (it != m_Outputs.end())
        {
            return it->second;
        }
        return nullptr;
    }

    const std::vector<uint32_t> Session::GetOutputs()
    {
        std::vector<uint32_t> outputs;
        for(auto pair : m_Outputs)
        {
            outputs.push_back(pair.first);
        }
        return outputs;
    }

    void Session::AddSeat( qor::ref_of<Seat>::type seat, uint32_t name )
    {
        m_Seats[name] = seat;
    }

    qor::ref_of<Seat>::type Session::GetSeat( uint32_t name )
    {
        auto it = m_Seats.find(name);
        if (it != m_Seats.end())
        {
            return it->second;
        }
        return qor::ref_of<Seat>::type();
    }

    const std::vector<uint32_t> Session::GetSeats()
    {
        std::vector<uint32_t> seats;
        for( auto pair : m_Seats )
        {
            seats.push_back(pair.first);
        }
        return seats;
    }

    void Session::AddUnknownGlobal( uint32_t name, const char* interface, uint32_t version )
    {
        GlobalInfo info { .Name = name, .Interface = std::string(interface), .Version = version };
        m_UnknownGlobals.push_back( info );
    }

    const GlobalInfo* Session::GetGlobal( const std::string& interface )
    {
        for ( auto& global : m_UnknownGlobals )
        {
            if ( global.Interface == interface )
            {
                return &global;
            }
        }
        return nullptr;
    }

    qor::ref_of<Registry>::type Session::GetRegistry()
    {
        return m_Registry;
    }

    qor::ref_of<Display>::type Session::GetDisplay()
    {
        return m_Display;
    }
    
