        SharedMemoryPool& operator=(const SharedMemoryPool&) = delete;        
        SharedMemoryPool(SharedMemoryPool&& rhs) noexcept;
        SharedMemoryPool& operator=(SharedMemoryPool&& rhs) noexcept;
        virtual const char* Tag() const{return TagName;}
