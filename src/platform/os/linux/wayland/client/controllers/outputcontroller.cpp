    OutputController::OutputController(ref_of<Output>::type output) : m_output(output)
    {
        if(m_output)
        {
            connect(*m_output, &Output::GeometryEvent, *this, &OutputController::OnGeometry);
            connect(*m_output, &Output::DoneEvent, *this, &OutputController::OnDone);
            connect(*m_output, &Output::ModeEvent, *this, &OutputController::OnMode);
            connect(*m_output, &Output::ScaleEvent, *this, &OutputController::OnScale);            
        }
        else
        {
            continuable("No output specified for new OutputController.");
        }
    }

    OutputController::~OutputController()
    {
        if(m_output)
        {
            disconnect(*m_output, &Output::ScaleEvent, *this, &OutputController::OnScale);
            disconnect(*m_output, &Output::ModeEvent, *this, &OutputController::OnMode);
            disconnect(*m_output, &Output::DoneEvent, *this, &OutputController::OnDone);
            disconnect(*m_output, &Output::GeometryEvent, *this, &OutputController::OnGeometry);
        }
    }

