#ifndef QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMPOPUP
#define QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMPOPUP

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "sdk/using_framework.h"
#include "sdk/platform/os/linux/wayland.h"
#include "sharedmem.h"

using namespace qor::platform::nslinux;

class CustomPopup : public wl::XDGPopupWindow
{
public:
    CustomPopup(qor::ref_of<wl::XDGSession>::type session, XDGWindow* parent, ref_of<wl::XDGPositioner>::type positioner) : wl::XDGPopupWindow(session, parent, positioner), m_session(session)
    {
        DoConfiguration();
        Resize();
        DrawFrame();
    }

    virtual ~CustomPopup()
    {
    }

    void Resize()
    {
        int stride = m_width * 4;
        int size = 2 * stride * m_height;  // bytes
        shared_mem = std::make_shared<shared_mem_t>(size);
        
        m_shmPool = m_session->GetShm()->CreatePool(shared_mem->get_fd(), size);
        for(unsigned int c = 0; c < 2; c++)
        {
            buffer[c] = m_shmPool->CreateBuffer(c*m_width*m_height*4, m_width, m_height, stride, wl::WL_SHM_FORMAT_XRGB8888);
            buffer[c]->AddListener(buffer_listener[c], nullptr);//Attach a listener to the Buffer to free it when the compositor is done with it.
        }        
    }

    void DrawFrame(uint32_t serial = 0)
    {
        int stride = m_width * 4;

        // Draw background
        size_t total_pixels = m_width * m_height;
        pixels = reinterpret_cast<uint8_t*>(shared_mem->get_mem());
        
        uint32_t* pixel_ptr = (uint32_t*)pixels + (m_curBuffer * total_pixels);
        for (int y = 0; y < m_height; ++y) 
        {
            for (int x = 0; x < m_width; ++x) 
            {
                if ((x + y / 8 * 8) % 16 < 8) 
                {
                    pixel_ptr[y * m_width + x] = 0xFF666666 - (x % 100);
                } 
                else 
                {
                    pixel_ptr[y * m_width + x] = 0xFFEEEEEE;
                }
            }
        }        
        
        //Get the pixels out of the door by:        
        m_xdgSurface->BaseSurface()->Attach(buffer[m_curBuffer], 0, 0);     //attaching them to the surface
        m_xdgSurface->BaseSurface()->Damage( 0, 0, m_width, m_height);      //marking the whole surface as needing redrawing        
        if(++m_curBuffer > 1)
        {
            m_curBuffer = 0;
        }
        m_frameCallback = m_xdgSurface->BaseSurface()->Frame();             //Request a callback on the next Frame
        m_frameCallback.m_done = bind_mem_fn(&CustomPopup::DrawFrame, this);
        m_frameCallback.AddDefaultListener();

        m_xdgSurface->BaseSurface()->Commit();                              //Commit the surface
    }

    virtual void OnConfigure(int32_t x, int32_t y, int32_t width, int32_t height)
    {
        SetWidth(width);
        SetHeight(height);
    }

    virtual void OnXDGSurfaceConfigured()
    {
        Resize();
        DrawFrame();
    }

    virtual void OnXDGTopLevelConfigured(int32_t new_width, int32_t new_height, struct wl_array* states)
    {
        if (new_width == 0 || new_height == 0)
        {
            return;
        }

        if (m_width != new_width || m_height != new_height)
        {
            Resize();
            m_width = new_width;
            m_height = new_height;
        }
    }

    uint8_t* pixels;
    wl::BufferListener buffer_listener[2];       
    ref_of<wl::XDGSession>::type m_session;
    ref_of<wl::XDGPositioner>::type m_positioner;    
    wl::Callback m_frameCallback;        
    ref_of<wl::SharedMemoryPool>::type m_shmPool;
    ref_of<wl::Buffer>::type buffer[2];    
    unsigned int m_curBuffer;
    std::shared_ptr<shared_mem_t> shared_mem; 

};

#endif//QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMPOPUP