#ifndef QOR_PP_H_LINUX_WAYLAND_EXAMPLES_SHAREDMEM
#define QOR_PP_H_LINUX_WAYLAND_EXAMPLES_SHAREDMEM

#include <stdexcept>
#include <iostream>
#include <array>
#include <memory>
#include <sstream>
#include <ctime>
#include <algorithm>
#include <sys/mman.h>
#include <sys/stat.h>
#include <sys/syscall.h>
#include <fcntl.h>
#include <unistd.h>

// shared memory helper class
class shared_mem_t
{
private:
    std::string name;
    int fd = 0;
    size_t len = 0;
    void *mem = nullptr;
 
public:
    shared_mem_t()
    {
    }
 
    shared_mem_t(size_t size) : len(size)
    {
        // Very simple shared memory wrapper
        std::stringstream ss;
        std::srand(std::time(nullptr));
        ss << '/' << std::rand();
        name = ss.str();

        fd = syscall(SYS_memfd_create, name, 0);
        ftruncate(fd, size);

        // map memory
        mem = mmap(nullptr, len, PROT_READ | PROT_WRITE, MAP_SHARED, fd, 0);
        if(mem == MAP_FAILED)
        throw std::runtime_error("mmap failed.");
    }
 
    ~shared_mem_t() noexcept(false)
    {
        if(fd)
        {
            if(munmap(mem, len) < 0)
                throw std::runtime_error("munmap failed.");
            if(close(fd) < 0)
                throw std::runtime_error("close failed.");
        }
    }
 
    int get_fd()
    {
        return fd;
    }
 
    void *get_mem()
    {
        return mem;
    }
};

#endif//QOR_PP_H_LINUX_WAYLAND_EXAMPLES_SHAREDMEM