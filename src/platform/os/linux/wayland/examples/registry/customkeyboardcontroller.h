#ifndef QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMKEYBOARDCONTROLLER
#define QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMKEYBOARDCONTROLLER

#include "sdk/using_framework.h"
#include "sdk/platform/os/linux/wayland.h"
#include "customwindow.h"

class customKeyboardController : public wl::KeyboardController
{
public:

    customKeyboardController(wl::XDGSession* session) : wl::KeyboardController(session->GetKeyboard()), m_session(session)
    {        
    }

    virtual ~customKeyboardController() = default;

    virtual void OnKey(wl::Keyboard*, uint32_t serial, uint32_t time, uint32_t key, uint32_t state)
    {
        if(m_session)
        {
            if(key == 1 && state == 1)
            {
                m_session->End();
            }
        }
    }

protected:

    wl::XDGSession* m_session;    
};

#endif//QOR_PP_H_LINUX_WAYLAND_EXAMPLES_CUSTOMKEYBOARDCONTROLLER