#include "src/configuration/configuration.h"
#include "src/qor/error/error.h"

#include "src/platform/os/linux/wayland/client/output.h"

#include "feedbacklistener.h"
#include "../feedback.h"

namespace qor{ namespace platform { namespace nslinux{ namespace wl{

    FeedbackListener::FeedbackListener()
    {
        this->discarded = [](void* data, struct wp_presentation_feedback* pres_feedback)
        {
            Feedback* feedback = Feedback::FeedbackFrom(pres_feedback);
            if(feedback)
            {
                feedback->OnDiscarded(data);
            }
        };

        this->presented = [](void* data, struct wp_presentation_feedback* pres_feedback, uint32_t tvSecHi, uint32_t tvSecLo, uint32_t nSec,
        uint32_t refresh, uint32_t seqHi, uint32_t seqLo, uint32_t flags)
        {
            Feedback* feedback = Feedback::FeedbackFrom(pres_feedback);
            if(feedback)
            {
                feedback->OnPresented(data, tvSecHi, tvSecLo, nSec, refresh, seqHi, seqLo, flags);
            }
        };

        this->sync_output = [](void* data, struct wp_presentation_feedback* pres_feedback, wl_output* o)
        {
            Feedback* feedback = Feedback::FeedbackFrom(pres_feedback);
            Output* output = Output::OutputFrom(o);
            if(feedback)
            {
                feedback->OnSyncOutput(data, output);
            }
        };
    }
        
}}}}//qor::platform::nslinux::wl
