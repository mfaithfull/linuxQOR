    const char* const Feedback::TagName = "QOR::PLATFORM::NSLINUX::WL::FEEDBACK";

    Feedback* Feedback::FeedbackFrom(struct wp_presentation_feedback* feedback)
    {
        if(!feedback)
        {
            return nullptr;
        }
        Feedback* result = reinterpret_cast<Feedback*>(wp_presentation_feedback_get_user_data(feedback));
        if(result && result->Tag() == TagName)
        {
            return result;
        }
        else if(result)
        {
            continuable("Wayland wp_presentation_feedback user data tag mismatch");
        }
        return new Feedback(feedback);
    }

    Feedback::Feedback(struct wp_presentation_feedback* feedback) : m_feedback(feedback)
    {
        if(m_feedback)
        {
            wp_presentation_feedback_set_user_data(m_feedback, this);
        }
        else
        {
            continuable("Feedback created with null wp_presentation_feedback pointer");
        }
    }

    Feedback::Feedback(Feedback&& rhs) noexcept : m_feedback(rhs.m_feedback)
    {
        rhs.m_feedback = nullptr;
        if (m_feedback)
        {
            wp_presentation_feedback_set_user_data(m_feedback, this);
        }
    }

    Feedback& Feedback::operator=(Feedback&& rhs) noexcept
    {
        if (this != &rhs)
        {
            if (m_feedback)
            {
                wp_presentation_feedback_destroy(m_feedback);
            }
            m_feedback = rhs.m_feedback;
            rhs.m_feedback = nullptr;
            if (m_feedback)
            {
                wp_presentation_feedback_set_user_data(m_feedback, this);
            }
        }
        return *this;
    }

    Feedback::~Feedback()
    {
        if(m_feedback)
        {
            wp_presentation_feedback_destroy(m_feedback);
        }
    }

    struct wp_presentation_feedback* Feedback::Use() const
    {
        if(!m_feedback)
        {
            warning("Using Feedback with null wp_presentation_feedback pointer");
        }
        return m_feedback;
    }

    uint32_t Feedback::Version() const
    {
        if(!m_feedback)
        {
            warning("Getting version of Feedback with null wp_presentation_feedback pointer");
            return 0;
        }
        return wp_presentation_feedback_get_version(m_feedback);
    }

    int Feedback::AddListener(const FeedbackListener& listener, void* context)
    {
        return wp_presentation_feedback_add_listener(m_feedback, &listener, context);
    }

