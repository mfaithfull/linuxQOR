    // Make the specified window and its video mode active on its monitor    //
    void WindowImpl::AcquireMonitorWin32()
    {
        if (!TheSession().acquiredMonitorCount)
        {
            SetThreadExecutionState(ES_CONTINUOUS | ES_DISPLAY_REQUIRED);

            // HACK: When mouse trails are enabled the cursor becomes invisible when
            //       the OpenGL ICD switches to page flipping
            SystemParametersInfoW(SPI_GETMOUSETRAILS, 0, &TheSession().mouseTrailSize, 0);
            SystemParametersInfoW(SPI_SETMOUSETRAILS, 0, 0, 0);
        }

        if (!GetMonitorWin32()->GetWindowWin32())
        {
            TheSession().acquiredMonitorCount++;
        }

        GetMonitorWin32()->SetVideoModeWin32(m_pOuter->GetVideoMode());
        qor::gl::Monitor::InputMonitorWindow(m_pOuter->GetMonitor(), m_pOuter);
    }
    
    // Remove the window and restore the original video mode    
    void WindowImpl::ReleaseMonitorWin32()
    {
        if (GetMonitorWin32()->GetWindowWin32() != this)
        {
            return;
        }

        TheSession().acquiredMonitorCount--;
        if (!TheSession().acquiredMonitorCount)
        {
            SetThreadExecutionState(ES_CONTINUOUS);

            // HACK: Restore mouse trail length saved in acquireMonitor
            SystemParametersInfoW(SPI_SETMOUSETRAILS, TheSession().mouseTrailSize, 0, 0);
        }

        qor::gl::Monitor::InputMonitorWindow(m_pOuter->GetMonitor(), nullptr);
        GetMonitorWin32()->RestoreVideoModeWin32();
    }
    
    // Manually maximize the window, for when SW_MAXIMIZE cannot be used    
    void WindowImpl::MaximizeWindowManuallyWin32()
    {
        qor::platform::nswindows::Rect rect;
        DWORD style;
        qor::platform::nswindows::MonitorInfo mi = { sizeof(mi) };

        WindowController::ToMonitor(MONITOR_DEFAULTTONEAREST)->GetInfo(mi);

        rect = mi.rcWork;

        if (m_pOuter->GetMaxWidth() != qor::gl::DONT_CARE && m_pOuter->GetMaxHeight() != qor::gl::DONT_CARE)
        {
            rect.m_right = std::min(rect.m_right, rect.m_left + m_pOuter->GetMaxWidth());
            rect.m_bottom = std::max(rect.m_bottom, rect.m_top + m_pOuter->GetMaxHeight());
        }

        style = WindowController::GetWindowLongT(GWL_STYLE);
        style |= WS_MAXIMIZE;
        WindowController::SetWindowLongT(GWL_STYLE, style);

        if (m_pOuter->Decorated())
        {
            const DWORD exStyle = WindowController::GetWindowLongT(GWL_EXSTYLE);

            if (Session::IsWindows10Version1607OrGreaterWin32())
            {
                const UINT dpi = WindowController::GetDPI();
                WindowController::AdjustRectForDpi(rect, style, FALSE, exStyle, dpi);
                OffsetRect(reinterpret_cast<LPRECT>(&rect), 0, GetSystemMetricsForDpi(SM_CYCAPTION, dpi));
            }
            else
            {
                WindowController::AdjustRect(rect, style, FALSE, exStyle);
                OffsetRect(reinterpret_cast<LPRECT>(&rect), 0, GetSystemMetrics(SM_CYCAPTION));
            }

            rect.m_bottom = std::min(rect.m_bottom, mi.rcWork.m_bottom);
        }

        WindowController::SetPosTop(rect.m_left, rect.m_top, rect.m_right - rect.m_left, rect.m_bottom - rect.m_top, SWP_NOACTIVATE | SWP_NOZORDER | SWP_FRAMECHANGED);
    }    

