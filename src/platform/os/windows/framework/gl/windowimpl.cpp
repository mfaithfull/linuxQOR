    // Returns the window style for the specified window
    unsigned long WindowImpl::GetWindowStyleWin32() const
    {
        DWORD style = WS_CLIPSIBLINGS | WS_CLIPCHILDREN;

        if (m_pOuter->GetMonitor())
        {
            style |= WS_POPUP;
        }
        else
        {
            style |= WS_SYSMENU | WS_MINIMIZEBOX;

            if (m_pOuter->Decorated())
            {
                style |= WS_CAPTION;

                if (m_pOuter->Resizeable())
                {
                    style |= WS_MAXIMIZEBOX | WS_THICKFRAME;
                }
            }
            else
            {
                style |= WS_POPUP;
            }
        }

        return style;
    }
    
    // Returns the extended window style
    unsigned long WindowImpl::GetWindowExStyleWin32() const
    {
        DWORD style = WS_EX_APPWINDOW;

        if (m_pOuter->GetMonitor() || m_pOuter->Floating())
        {
            style |= WS_EX_TOPMOST;
        }

        return style;
    }
    
    // Returns the image whose area most closely matches the desired one    
    static const qor::gl::Image* ChooseImage(int count, const qor::gl::Image* images, int width, int height)
    {
        int i, leastDiff = INT_MAX;
        const qor::gl::Image* closest = nullptr;

        for (i = 0;  i < count;  i++)
        {
            const int currDiff = abs(images[i].width * images[i].height - width * height);
            if (currDiff < leastDiff)
            {
                closest = images + i;
                leastDiff = currDiff;
            }
        }
        return closest;
    }

    // Creates an RGBA icon or cursor
    static HICON createIcon(const qor::gl::Image* image, int xhot, int yhot, bool icon)
    {
        int i;
        HDC dc;
        HICON handle;
        HBITMAP color, mask;
        BITMAPV5HEADER bi;
        ICONINFO ii;
        unsigned char* target = NULL;
        unsigned char* source = image->pixels;

        ZeroMemory(&bi, sizeof(bi));
        bi.bV5Size        = sizeof(bi);
        bi.bV5Width       = image->width;
        bi.bV5Height      = -image->height;
        bi.bV5Planes      = 1;
        bi.bV5BitCount    = 32;
        bi.bV5Compression = BI_BITFIELDS;
        bi.bV5RedMask     = 0x00ff0000;
        bi.bV5GreenMask   = 0x0000ff00;
        bi.bV5BlueMask    = 0x000000ff;
        bi.bV5AlphaMask   = 0xff000000;

        dc = GetDC(NULL);
        color = CreateDIBSection(dc,
                                (BITMAPINFO*) &bi,
                                DIB_RGB_COLORS,
                                (void**) &target,
                                NULL,
                                (DWORD) 0);
        ReleaseDC(NULL, dc);

        if (!color)
        {
            Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to create RGBA bitmap");
            return nullptr;
        }

        mask = CreateBitmap(image->width, image->height, 1, 1, NULL);
        if (!mask)
        {
            Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to create mask bitmap");
            DeleteObject(color);
            return nullptr;
        }

        for (i = 0;  i < image->width * image->height;  i++)
        {
            target[0] = source[2];
            target[1] = source[1];
            target[2] = source[0];
            target[3] = source[3];
            target += 4;
            source += 4;
        }

        ZeroMemory(&ii, sizeof(ii));
        ii.fIcon    = icon;
        ii.xHotspot = xhot;
        ii.yHotspot = yhot;
        ii.hbmMask  = mask;
        ii.hbmColor = color;

        handle = CreateIconIndirect(&ii);

        DeleteObject(color);
        DeleteObject(mask);

        if (!handle)
        {
            if (icon)
            {
                Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to create icon");
            }
            else
            {
                Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to create cursor");
            }
        }

        return handle;
    }
    
    // Enforce the content area aspect ratio based on which edge is being dragged
    void WindowImpl::ApplyAspectRatioWin32(int edge, qor::platform::nswindows::Rect* area)
    {
        RECT frame = {0};
        const float ratio = (float) m_pOuter->GetNumer() / (float) m_pOuter->GetDenom();
        const DWORD style = GetWindowStyleWin32();
        const DWORD exStyle = GetWindowExStyleWin32();

        if (Session::IsWindows10BuildOrGreaterWin32(14393))
        {
            AdjustWindowRectExForDpi(&frame, style, FALSE, exStyle, WindowController::GetDPI());
        }
        else
        {
            AdjustWindowRectEx(&frame, style, FALSE, exStyle);
        }

        if (edge == WMSZ_LEFT  || edge == WMSZ_BOTTOMLEFT || edge == WMSZ_RIGHT || edge == WMSZ_BOTTOMRIGHT)
        {
            area->m_bottom = area->m_top + (frame.bottom - frame.top) + (int) (((area->m_right - area->m_left) - (frame.right - frame.left)) / ratio);
        }
        else if (edge == WMSZ_TOPLEFT || edge == WMSZ_TOPRIGHT)
        {
            area->m_top = area->m_bottom - (frame.bottom - frame.top) - (int) (((area->m_right - area->m_left) - (frame.right - frame.left)) / ratio);
        }
        else if (edge == WMSZ_TOP || edge == WMSZ_BOTTOM)
        {
            area->m_right = area->m_left + (frame.right - frame.left) + (int) (((area->m_bottom - area->m_top) - (frame.bottom - frame.top)) * ratio);
        }
    }
    
    // Updates the cursor image according to its cursor mode
    void WindowImpl::UpdateCursorImageWin32()
    {
        if (m_pOuter->GetCursorMode() == qor::gl::CURSOR_NORMAL || m_pOuter->GetCursorMode() == qor::gl::CURSOR_CAPTURED)
        {
            SetCursor((HCURSOR)(m_pOuter->GetCursor() ? GetCursorWin32()->GetHandle() : LoadCursorW(NULL, (WCHAR*)IDC_ARROW)));
        }
        else
        {
            // NOTE: Via Remote Desktop, setting the cursor to NULL does not hide it.
            // HACK: When running locally, it is set to NULL, but when connected via Remote
            //       Desktop, this is a transparent cursor.
            SetCursor(TheSession().blankCursor);
        }
    }
