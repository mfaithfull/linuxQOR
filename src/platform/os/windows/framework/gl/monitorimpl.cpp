    void MonitorImpl::GetPos(int* xpos, int* ypos)
    {
        DEVMODEW dm;
        ZeroMemory(&dm, sizeof(dm));
        dm.dmSize = sizeof(dm);

        EnumDisplaySettingsExW(adapterName, ENUM_CURRENT_SETTINGS, &dm, EDS_ROTATEDMODE);

        if (xpos)
            *xpos = dm.dmPosition.x;
        if (ypos)
            *ypos = dm.dmPosition.y;
    }

    void MonitorImpl::GetContentScale(float* xscale, float* yscale)
    {
        GetHMONITORContentScaleWin32((HMONITOR)(handle), xscale, yscale);
    }

    void MonitorImpl::GetWorkarea(int* xpos, int* ypos, int* width, int* height)
    {
        MONITORINFO mi = { sizeof(mi) };
        GetMonitorInfoW((HMONITOR)(handle), &mi);

        if (xpos)
            *xpos = mi.rcWork.left;
        if (ypos)
            *ypos = mi.rcWork.top;
        if (width)
            *width = mi.rcWork.right - mi.rcWork.left;
        if (height)
            *height = mi.rcWork.bottom - mi.rcWork.top;
    }

    qor::gl::VidMode* MonitorImpl::GetVideoModes(int* count)
    {
        int modeIndex = 0, size = 0;
        qor::gl::VidMode* result = nullptr;

        *count = 0;

        for (;;)
        {
            int i;
            qor::gl::VidMode mode;
            DEVMODEW dm;

            ZeroMemory(&dm, sizeof(dm));
            dm.dmSize = sizeof(dm);

            if (!EnumDisplaySettingsW(adapterName, modeIndex, &dm))
            {
                break;
            }

            modeIndex++;

            // Skip modes with less than 15 BPP
            if (dm.dmBitsPerPel < 15)
            {
                continue;
            }

            mode.width  = dm.dmPelsWidth;
            mode.height = dm.dmPelsHeight;
            mode.refreshRate = dm.dmDisplayFrequency;
            m_pOuter->SplitBPP(dm.dmBitsPerPel, &mode.redBits, &mode.greenBits, &mode.blueBits);

            for (i = 0;  i < *count;  i++)
            {
                if (m_pOuter->CompareVideoModes(result + i, &mode) == 0)
                    break;
            }

            // Skip duplicate modes
            if (i < *count)
                continue;

            if (modesPruned)
            {
                // Skip modes not supported by the connected displays
                if (::ChangeDisplaySettingsExW(adapterName, &dm, NULL, CDS_TEST, NULL) != DISP_CHANGE_SUCCESSFUL)
                {
                    continue;
                }
            }

            if (*count == size)
            {
                size += 128;
                delete [] result;
                result = new qor::gl::VidMode[size];
            }

            (*count)++;
            result[*count - 1] = mode;
        }

        if (!*count)
        {
            // HACK: Report the current mode if no valid modes were found
            result = new qor::gl::VidMode[1];
            GetVideoModeWin32(result);
            *count = 1;
        }

        return result;
    }

    int MonitorImpl::GetVideoMode(qor::gl::VidMode* mode)
    {
        DEVMODEW dm;
        ZeroMemory(&dm, sizeof(dm));
        dm.dmSize = sizeof(dm);

        if (!EnumDisplaySettingsW(adapterName, ENUM_CURRENT_SETTINGS, &dm))
        {
            qor::gl::Session::InputError(qor::gl::PLATFORM_ERROR, "Win32: Failed to query display settings");
            return 0;
        }

        mode->width  = dm.dmPelsWidth;
        mode->height = dm.dmPelsHeight;
        mode->refreshRate = dm.dmDisplayFrequency;
        m_pOuter->SplitBPP(dm.dmBitsPerPel, &mode->redBits, &mode->greenBits, &mode->blueBits);
        return 1;
    }

    int MonitorImpl::GetGammaRamp(qor::gl::GammaRamp* ramp)
    {
        HDC dc;
        WORD values[3][256];

        dc = ::CreateDCW(L"DISPLAY", adapterName, NULL, NULL);
        GetDeviceGammaRamp(dc, values);
        DeleteDC(dc);

        AllocGammaArrays(ramp, 256);

        memcpy(ramp->red,   values[0], sizeof(values[0]));
        memcpy(ramp->green, values[1], sizeof(values[1]));
        memcpy(ramp->blue,  values[2], sizeof(values[2]));

        return 1;
    }

    void MonitorImpl::SetGammaRamp(const qor::gl::GammaRamp* ramp)
    {
        HDC dc;
        WORD values[3][256];

        if (ramp->size != 256)
        {
            qor::gl::Session::InputError(qor::gl::PLATFORM_ERROR, "Win32: Gamma ramp size must be 256");
            return;
        }

        memcpy(values[0], ramp->red,   sizeof(values[0]));
        memcpy(values[1], ramp->green, sizeof(values[1]));
        memcpy(values[2], ramp->blue,  sizeof(values[2]));

        dc = ::CreateDCW(L"DISPLAY", adapterName, NULL, NULL);
        ::SetDeviceGammaRamp(dc, values);
        ::DeleteDC(dc);
    }


