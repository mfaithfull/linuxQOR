    bool MonitorImpl::GetVideoModeWin32(qor::gl::VidMode* mode)
    {
        DEVMODEW dm;
        ZeroMemory(&dm, sizeof(dm));
        dm.dmSize = sizeof(dm);
        if (!EnumDisplaySettingsW(adapterName, ENUM_CURRENT_SETTINGS, &dm))
        {
            qor::gl::Session::InputError(qor::gl::PLATFORM_ERROR, "Win32: Failed to query display settings");
            return false;
        }
        mode->width  = dm.dmPelsWidth;
        mode->height = dm.dmPelsHeight;
        mode->refreshRate = dm.dmDisplayFrequency;
        m_pOuter->SplitBPP(dm.dmBitsPerPel, &mode->redBits, &mode->greenBits, &mode->blueBits);
        return true;
    }

    void MonitorImpl::GetHMONITORContentScaleWin32(void* handle, float* xscale, float* yscale)
    {
        UINT xdpi, ydpi;

        if (xscale)
            *xscale = 0.f;
        if (yscale)
            *yscale = 0.f;

        /*TODO:
        if (IsWindows8Point1OrGreater())
        {
            if (GetDpiForMonitor(handle, MDT_EFFECTIVE_DPI, &xdpi, &ydpi) != S_OK)
            {
                qor::gl::Session::InputError(qor::gl::PLATFORM_ERROR, "Win32: Failed to query monitor DPI");
                return;
            }
        }
        else*/
        {
            const HDC dc = ::GetDC(NULL);
            xdpi = ::GetDeviceCaps(dc, LOGPIXELSX);
            ydpi = ::GetDeviceCaps(dc, LOGPIXELSY);
            ::ReleaseDC(NULL, dc);
        }

        if (xscale)
            *xscale = xdpi / (float) USER_DEFAULT_SCREEN_DPI;
        if (yscale)
            *yscale = ydpi / (float) USER_DEFAULT_SCREEN_DPI;
    }

