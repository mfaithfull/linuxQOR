#include "src/configuration/configuration.h"

#include "displaydevices.h"
#include "src/platform/os/windows/common/stringconv.h"
#include "src/platform/os/windows/api_layer/user/user32.h"

using namespace qor::nswindows::api;

namespace qor{ namespace platform { namespace nswindows{

    unsigned int DisplayDevices::Enumerate(bool(fDisplay)(DisplayDevice&))
    {
        unsigned int countDevices = 0;
        //Make sure we have a primary display device
        DisplayDevice displayDevice = {0};
        unsigned long devNumber = 0;
        unsigned long flags = EDD_GET_DEVICE_INTERFACE_NAME;
        bool hasPrimaryDisplay = User32::EnumDisplayDevicesT(nullptr, devNumber, reinterpret_cast<PDISPLAY_DEVICE>(&displayDevice), flags) ? true : false;

        if(hasPrimaryDisplay)
        {
            countDevices++;
            while(fDisplay(displayDevice) && User32::EnumDisplayDevicesT(nullptr, ++devNumber, reinterpret_cast<PDISPLAY_DEVICE>(&displayDevice), flags) ? true : false)
            {
                countDevices++;  
            }
        }
        return countDevices;
    }

    unsigned int DisplayDevices::EnumerateDisplaySettings(const TCHAR* deviceName, bool(fMode)(DeviceMode&))
    {        
        unsigned long modeNumber = 0;
        unsigned long flags = 0;
        DeviceMode displayMode = {0};

        while(User32::EnumDisplaySettingsExT(deviceName, modeNumber++, reinterpret_cast<LPDEVMODE>(&displayMode), flags) ? true : false)
        {
            fMode(displayMode);
        }
        return modeNumber;
    }

    long DisplayDevices::ChangeSettings(const TCHAR* lpszDeviceName, DeviceMode* deviceMode, unsigned long flags, void* param)
    {
        return User32::ChangeDisplaySettingsExT(lpszDeviceName, reinterpret_cast<::LPDEVMODE>(deviceMode), nullptr, flags, param);
    }
    
    
}}}//qor::platform::nswindows
