#include "src/configuration/configuration.h"

#include "dialog.h"

#include "src/platform/os/windows/api_layer/user/user32.h"

using namespace qor::nswindows::api;

#undef MessageBox
#undef DialogBoxIndirectParam

namespace qor{ namespace platform { namespace nswindows{
    
    long long qor_pp_compiler_stdcallconvention Dialog::DlgProc(void* hDlg, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        switch(msg)
        {
            case WM_INITDIALOG:
                {
                    Dialog* dlg = reinterpret_cast<Dialog*>(lParam);
                    if(dlg != nullptr)
                    {
                        User32::SetPropT((HWND)(hDlg), TEXT("QOR"), (HANDLE)(dlg));
                        return dlg->OnInit();
                    }                    
                }
                break;
            case WM_COMMAND:
                {
                    unsigned int wId = LOWORD(wParam);
                    if (wId == IDOK || wId == IDCANCEL)
                    {
                        Dialog* dlg = reinterpret_cast<Dialog*>(User32::GetPropT((HWND)(hDlg), TEXT("QOR")));
                        User32::EndDialog((HWND)(hDlg), dlg ? dlg->OnEnd(wId) : wId);
                        return 1;
                    }                    
                }
                break;
            case WM_CLOSE:
                    Dialog* dlg = reinterpret_cast<Dialog*>(User32::GetPropT((HWND)(hDlg), TEXT("QOR")));
                    User32::EndDialog((HWND)(hDlg), dlg ? dlg->OnEnd(IDCANCEL) : IDCANCEL);
                    return 1;
                break;     
        }
        return 0;
    }

    Dialog::Dialog(ref_of<Window>::type parent, const DlgTemplateEx* t)
    {       
        HINSTANCE instance = Kernel32::GetModuleHandleW(nullptr);        
        m_result = User32::DialogBoxIndirectParamT(
            instance, 
            reinterpret_cast<LPCDLGTEMPLATE>(t), 
            (HWND)(parent.IsNotNull() ? parent->GetHandle().Use() : nullptr), 
            (DLGPROC)(Dialog::DlgProc), reinterpret_cast<long long>(this));
    }

    int Dialog::WriteString(const char* multiByteSource, wchar_t* wideStringDest)
    {
        return Kernel32::MultiByteToWideChar(CP_ACP, 0, multiByteSource, -1, wideStringDest, 50);
    }

    int Dialog::OnInit()
    {
        return 1;
    }

    int Dialog::OnEnd(int id)
    {
        return id;
    }

}}}//qor::platform::nswindows
