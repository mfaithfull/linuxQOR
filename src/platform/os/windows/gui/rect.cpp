#include "src/configuration/configuration.h"

#include "rect.h"
#include "src/platform/os/windows/common/stringconv.h"
#include "src/platform/os/windows/api_layer/user/user32.h"

using namespace qor::nswindows::api;

namespace qor{ namespace platform { namespace nswindows{

    bool Rect::CopyFrom(const Rect& src)
    {
        return User32::CopyRect(reinterpret_cast<LPRECT>(this), reinterpret_cast<const RECT*>(&src)) ? true : false;
    }

    bool Rect::Equal(const Rect& cmp) const
    {
        return User32::EqualRect(reinterpret_cast< const RECT*>(this), reinterpret_cast< const RECT*>(&cmp)) ? true : false;
    }

    bool Rect::Inflate(int dx, int dy)
    {
        return User32::InflateRect(reinterpret_cast<LPRECT>(this), dx, dy) ? true : false;
    }

    Rect Rect::FromIntersection(const Rect& rc1, const Rect& rc2)
    {
        Rect r;
        User32::IntersectRect(reinterpret_cast<LPRECT>(&r), reinterpret_cast<const RECT*>(&rc1), reinterpret_cast<const RECT*>(&rc2));
        return r;
    }

    bool Rect::IsEmpty() const
    {
        return User32::IsRectEmpty(reinterpret_cast<const RECT*>(this)) ? true : false;
    }

    bool Rect::Offset(int dx, int dy)
    {
        return User32::OffsetRect(reinterpret_cast<LPRECT>(this), dx, dy) ? true : false;
    }

    bool Rect::IsPointInRect(const Point& pt) const
    {
        const POINT* p = reinterpret_cast<const POINT*>(&pt);
        return User32::PtInRect(reinterpret_cast<const RECT*>(this), *p) ? true : false;
    }

    bool Rect::Set(int left, int top, int right, int bottom)
    {
        return User32::SetRect(reinterpret_cast<LPRECT>(this), left, top, right, bottom) ? true : false;
    }

    bool Rect::SetEmpty()
    {
        return User32::SetRectEmpty(reinterpret_cast<LPRECT>(this)) ? true : false;
    }

    Rect Rect::FromSubtraction(const Rect& rc1, const Rect& rc2)
    {
        Rect r;
        User32::SubtractRect(reinterpret_cast<LPRECT>(&r), reinterpret_cast< const RECT*>(&rc1), reinterpret_cast<const RECT*>(&rc2));
        return r;
    }

    Rect Rect::FromUnion(const Rect& rc1, const Rect& rc2)
    {
        Rect r;
        User32::UnionRect(reinterpret_cast<LPRECT>(&r), reinterpret_cast<const RECT*>(&rc1), reinterpret_cast<const RECT*>(&rc2));
        return r;
    }

}}}//qor::platform::nswindows
