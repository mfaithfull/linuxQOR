    bool MessageHandler::RouteMessage()
    {
        Message message;
        if (!Get(&message, 0, 0, 0))
        {
            return false;//Quit message received
        }

        Translate(&message);
        Dispatch(&message);

        return true;
    }
    
    bool MessageHandler::RouteMessage(AcceleratorTable& acceleratorTable)
    {
        Message message;
        if (!Get(&message, 0, 0, 0))
        {
            return false;//Quit message received
        }
        
        Window window(PrimitiveHandle(message.hwnd));
        if(!acceleratorTable.Translate(window, message))
        {
            Translate(&message);
            Dispatch(&message);
        }
        return true;
    }
        
    int MessageHandler::MessageLoop(AcceleratorTable& acceleratorTable)
    {
        Message message;
        bool bIdle = true;//For tracking the idle time state
        long lIdleCount = 0;

        //Acquire and dispatch messages until a WM_QUIT message is received.
        do
        {
            //Check to see if we can do idle work
            while (bIdle && !Peek(&message, 0, 0, 0, MessageHandler::PMNoRemove))
            {
                // call OnIdle while in Idle state
                if (!OnIdle(lIdleCount++))
                {
                    bIdle = false;
                }
            }

            //Pump messages while available
            do
            {
                // pump message, but quit on WM_QUIT
                if (!RouteMessage(acceleratorTable))
                {
                    return 0;
                }

                if (IsIdleMessage(&message))
                {
                    bIdle = true;
                    lIdleCount = 0;
                }

            } while (Peek(&message, 0, 0, 0, MessageHandler::PMNoRemove));

            bIdle = true;
            lIdleCount = 0;

        } while (true);

    }
    
