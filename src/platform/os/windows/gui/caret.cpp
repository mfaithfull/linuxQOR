#include "src/configuration/configuration.h"

#include "caret.h"
#include "src/platform/os/windows/common/stringconv.h"
#include "src/platform/os/windows/api_layer/user/user32.h"

using namespace qor::nswindows::api;

namespace qor{ namespace platform { namespace nswindows{

    bool Caret::Create(Window& window, Handle* bitmap, int width, int height)
    {    
        return User32::CreateCaret((HWND)(window.GetHandle().Use()), (HBITMAP)(bitmap ? bitmap->Use() : nullptr), width, height) ? true : false;
    }

    bool Caret::Destroy()
    {
        return User32::DestroyCaret() ? true : false;
    }

    unsigned int Caret::GetBlinkTime()
    {
        return User32::GetCaretBlinkTime();
    }

    bool Caret::SetBlinkTime(unsigned int umseconds)
    {
        return User32::SetCaretBlinkTime(umseconds);
    }

    bool Caret::GetPosition(Point& pt)
    {
        return User32::GetCaretPos(reinterpret_cast<LPPOINT>(&pt)) ? true : false;
    }

    bool Caret::SetPosition(int x, int y)
    {
        return User32::SetCaretPos(x, y) ? true : false;
    }

    bool Caret::Show(Handle& hWnd)
    {
        return User32::ShowCaret((HWND)hWnd.Use()) ? true : false;
    }

    bool Caret::Hide(Handle& hWnd)
    {
        return User32::HideCaret((HWND)hWnd.Use()) ? true : false;
    }

}}}//qor::platform::nswindows
