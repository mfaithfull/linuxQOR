#include "src/configuration/configuration.h"

#include "buttoncontroller.h"
#include "../perthread.h"

#include "src/platform/os/windows/common/stringconv.h"
#include "src/platform/os/windows/api_layer/user/user32.h"

using namespace qor::nswindows::api;

#define WINDOW_HANDLE (HWND)(m_window->GetHandle().Use())
namespace qor{ namespace platform { namespace nswindows{

    ButtonController::ButtonController() : WindowController(){}
    ButtonController::ButtonController(ref_of<Button>::type button) : WindowController(button.AsRef<Window>())
    {
        button->SetController(this);
    }

    ref_of<Bitmap>::type ButtonController::GetBitmap() const
    {
        return new_ref<Bitmap>(PrimitiveHandle((void*)(User32::SendMessageT(WINDOW_HANDLE, BM_GETIMAGE, IMAGE_BITMAP, 0))), false);
    }

    ref_of<Bitmap>::type ButtonController::SetBitmap(ref_of<Bitmap>::type bitmap) const
    {
        LPARAM lparam = reinterpret_cast<LPARAM>(bitmap->GetHandle().Use());
        return new_ref<Bitmap>(PrimitiveHandle((void*)(User32::SendMessageT(WINDOW_HANDLE, BM_SETIMAGE, IMAGE_BITMAP, lparam))), false);
    }

    unsigned int ButtonController::GetButtonStyle() const
    {
        return static_cast<unsigned int>(GetStyle() & 0xFFFF);
    }

    void ButtonController::SetButtonStyle(unsigned long style, bool redraw) const
    {
        User32::SendMessageT(WINDOW_HANDLE, BM_SETSTYLE, static_cast<WPARAM>(style), static_cast<LPARAM>(redraw));
    }

    int ButtonController::GetCheck() const
    {
        return static_cast<int>(User32::SendMessageT(WINDOW_HANDLE, BM_GETCHECK, 0, 0));
    }

    void ButtonController::SetCheck(int checkState) const
    {
        User32::SendMessageT(WINDOW_HANDLE, BM_SETCHECK, static_cast<WPARAM>(checkState), 0);
    }

    ref_of<Cursor>::type ButtonController::GetCursor() const
    {
        return new_ref<Cursor>(PrimitiveHandle(reinterpret_cast<void*>(User32::SendMessageT(WINDOW_HANDLE, BM_GETIMAGE, IMAGE_CURSOR, 0))));
    }

    ref_of<Cursor>::type ButtonController::SetCursor(ref_of<Cursor>::type cursor) const
    {
        return new_ref<Cursor>(PrimitiveHandle(reinterpret_cast<void*>(User32::SendMessageT(WINDOW_HANDLE, BM_SETIMAGE, IMAGE_CURSOR, reinterpret_cast<LPARAM>(cursor->GetHandle().Use())))));
    }

    ref_of<Icon>::type ButtonController::GetIcon() const
    {
        return new_ref<Icon>(PrimitiveHandle(reinterpret_cast<void*>(User32::SendMessageT(WINDOW_HANDLE, BM_GETIMAGE, IMAGE_ICON, 0))));
    }

    ref_of<Icon>::type ButtonController::SetIcon(ref_of<Icon>::type icon) const
    {
        return new_ref<Icon>(PrimitiveHandle(reinterpret_cast<void*>(User32::SendMessageT(WINDOW_HANDLE, BM_SETIMAGE, IMAGE_ICON, reinterpret_cast<LPARAM>(icon->GetHandle().Use())))));
    }

    unsigned int ButtonController::GetState() const
    {
        return static_cast<unsigned int>(User32::SendMessageT(WINDOW_HANDLE, BM_GETSTATE, 0, 0));
    }

    void ButtonController::SetState(unsigned int state) const
    {
        User32::SendMessageT(WINDOW_HANDLE, BM_SETSTATE, static_cast<WPARAM>(state), 0);
    }

    unsigned short ButtonController::GetId() const
    {
        return static_cast<unsigned short>(User32::GetDlgCtrlID(WINDOW_HANDLE));        
    }

}}}//qor::platform::nswindows

#undef WINDOW_HANDLE