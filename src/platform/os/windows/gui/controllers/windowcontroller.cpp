    unsigned short WindowController::TileChildren(unsigned int how, Rect& rc, std::vector<ref_of<Window>::type>& children)
    {
        std::vector<HWND> vecChildren;
        vecChildren.reserve(children.size());
        for( auto child : children)
        {
            vecChildren.push_back((HWND)(child->GetHandle().Use()));
        }
        return User32::TileWindows(WINDOW_HANDLE, how, reinterpret_cast<RECT*>(&rc), static_cast<UINT>(vecChildren.size()), vecChildren.data());
    }

