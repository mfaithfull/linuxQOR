    unsigned short WindowController::CascadeWindows(unsigned int how, const Rect* rect, std::vector<ref_of<Window>::type>& childWindows)
    {            
        std::vector<HWND> vecKids;
        vecKids.reserve(childWindows.size());
        for(auto r : childWindows)
        {
            vecKids.push_back((HWND)(r->GetHandle().Use()));
        }
        unsigned int countKids = static_cast<unsigned int>(vecKids.size());
        return User32::CascadeWindows(WINDOW_HANDLE, how, (const ::RECT*)(&rect), countKids, vecKids.data());
    }

