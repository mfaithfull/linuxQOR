#include "src/configuration/configuration.h"

#include "mouse.h"
#include "src/platform/os/windows/common/stringconv.h"
#include "src/platform/os/windows/api_layer/user/user32.h"

using namespace qor::nswindows::api;

namespace qor{ namespace platform { namespace nswindows{

    Handle Mouse::GetCapture()
    {
        return Handle(User32::GetCapture());
    }

    unsigned int Mouse::GetDoubleClickTime()
    {
        return User32::GetDoubleClickTime();
    }

    int Mouse::GetMovePointsEx(unsigned int size, MouseMovePoint* pt, MouseMovePoint* pointBuffer, int pointCount, unsigned long resolution)
    {
        return User32::GetMouseMovePointsEx(size, reinterpret_cast<LPMOUSEMOVEPOINT>(pt), reinterpret_cast<LPMOUSEMOVEPOINT>(pointBuffer), pointCount, resolution);
    }

    void Mouse::_event(unsigned long flags, unsigned long dx, unsigned long dy, unsigned long data, unsigned long long extraInfo)
    {
        User32::mouse_event(flags, dx, dy, data, extraInfo);
    }

    bool Mouse::ReleaseCapture()
    {
        return User32::ReleaseCapture() ? true : false;
    }

    bool Mouse::SetDoubleClickTime(unsigned int interval)
    {
        return User32::SetDoubleClickTime(interval) ? true : false;
    }

    bool Mouse::SwapMouseButton(bool fSwap)
    {
        return User32::SwapMouseButton(fSwap) ? true : false;
    }

    bool Mouse::TrackMouseEvent(struct TrackMouseEvent* eventTrack)
    {
        return User32::TrackMouseEvent(reinterpret_cast<LPTRACKMOUSEEVENT>(eventTrack)) ? true : false;
    }

    static HWND GetCapture(void);
    static UINT GetDoubleClickTime(void);
    static int GetMouseMovePointsEx(UINT cbSize, LPMOUSEMOVEPOINT lppt, LPMOUSEMOVEPOINT lpptBuf, int nBufPoints, DWORD resolution);
    static VOID mouse_event(DWORD dwFlags, DWORD dx, DWORD dy, DWORD dwData, ULONG_PTR dwExtraInfo);
    static BOOL ReleaseCapture(void);

    static BOOL SetDoubleClickTime(UINT uInterval);
    static BOOL SwapMouseButton(BOOL fSwap);
    static BOOL TrackMouseEvent(LPTRACKMOUSEEVENT lpEventTrack);

}}}//qor::platform::nswindows