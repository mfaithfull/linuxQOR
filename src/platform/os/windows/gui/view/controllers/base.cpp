    BaseWindowController::BaseWindowController() : m_nextHookController(nullptr)
    {
    }

    bool BaseWindowController::ProcessHook(Window& Window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {   //If the Window has been hooked defer to the hook controller for processing first
        return m_nextHookController ? m_nextHookController->ProcessMessage(Window, lResult, msg, wParam, lParam) : false;
    }

    bool BaseWindowController::ProcessMessage(Window& Window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
			bool bProcessed = ProcessHook(Window, lResult, msg, wParam, lParam);

			//Pass the message through filters until it's processed
			bProcessed =
				(m_timer.IsNotNull() && m_timer->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				(m_owner.IsNotNull() && m_owner->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				(m_style.IsNotNull() && m_style->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				(m_baseText.IsNotNull() && m_baseText->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				(m_user.IsNotNull() && m_user->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				(m_deviceChange.IsNotNull() && m_deviceChange->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				(m_menu.IsNotNull() && m_menu->ProcessMessage(Window, lResult, msg, wParam, lParam)) ||
				HandleMessage(Window, lResult, msg, wParam, lParam);

			return bProcessed;
    }

    //Handle the base messages here.
    bool BaseWindowController::HandleMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bHandled = false;

        switch (msg)
        {
        case wmNull:
            //Deliberately ignore WM_NULL as per spec
            bHandled = true;
            break;

        case wmCreate:
        {
            CreateStruct* pCreateStruct = reinterpret_cast<CreateStruct*>(lParam);
            lResult = OnCreate(window, pCreateStruct);
            bHandled = true;
        }
        break;

        case wmDestroy:
        {
            lResult = OnDestroy(window, msg, wParam, lParam);
            bHandled = true;
        }
        break;

        case wmActivate:
        {
            lResult = OnActivate(window, msg, wParam, lParam);
            bHandled = true;
        }
        break;

        case wmMouseActivate:
        {
            lResult = OnMouseActivate(window, msg, wParam, lParam);
            bHandled = true;
        }
        break;

        case wmEnable:
        {
            bool bEnable = wParam ? true : false;
            OnEnable(window, bEnable);
            lResult = 0;
            bHandled = true;
        }
        break;

        case wmClose:
        {
            OnClose(window);
            lResult = 0;
            bHandled = true;
        }
        break;

        case 0x0017/*WM_SYSTEMERROR*/:
        {
            OnSystemError(window, lResult, msg, wParam, lParam);
            lResult = 0;
            bHandled = true;
        }
        break;

        case wmSysCommand:
        {
            OnSysCommand(window, msg, wParam, lParam);
            lResult = 0;
            bHandled = true;
        }
        break;

        };

        return bHandled;
    }

    BaseWindowController* BaseWindowController::HookController(BaseWindowController* controller)
    {
        BaseWindowController* pResult = nullptr;
        if (m_nextHookController == nullptr)
        {
            m_nextHookController = controller;
        }
        else
        {
            pResult = m_nextHookController->HookController(controller);
        }
        return pResult;
    }

