    bool NonClientRenderingController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmNCCalcSize:
        {
            OnNCCalcSize(window, lResult, wParam, lParam);
            bProcessed = true;
        }
        break;

        case wmNCPaint:
        {
            OnNCPaint(window, lResult, wParam, lParam);
            bProcessed = true;
        }
        break;

        case wmNCHitTest:
        {
            OnNCHitTest(window, lResult, wParam, lParam);
            bProcessed = true;
        }
        break;

        case wmSize:
        {
            OnNCSize(window, lResult, wParam, lParam);
        }
        break;

        case wmDWMCompositionChanged:
        {

        }
        break;

        case wmDWMNonClientRenderingChanged:
        {
            
        }
        break;

        }

        return bProcessed;
    }

