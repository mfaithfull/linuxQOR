#include "src/configuration/configuration.h"

#include "iconized.h"
#include "../../messages.h"

namespace qor{ namespace platform { namespace nswindows{ namespace gui{ namespace view{

    IconizedController::IconizedController() : BaseWindowPartController()
    {
    }

    bool IconizedController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmQueryOpen:
        {
            lResult = (OnQueryOpen(window) ? 1 : 0);
            bProcessed = true;
        }
        break;

        case wmPaintIcon:
        {
            bProcessed = true;
        }
        break;

        case wmIconEraseBkgnd:
        {
            bProcessed = true;
        }
        break;

        case wmQueryDragIcon:
        {
            PrimitiveHandle iconHandle = OnQueryDragIcon(window);
            lResult = reinterpret_cast<long long>(iconHandle.Use());
            bProcessed = true;
        }
        break;
        }

        return bProcessed;
    }

    bool IconizedController::OnQueryOpen(Window& window)
    {
        return true;
    }

    PrimitiveHandle IconizedController::OnQueryDragIcon(Window& window)
    {
        return PrimitiveHandle(0);
    }

}}}}}//qor::platform::nswindows::gui::view

