    bool OwnerController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmEnterMenuLoop:
        {
            bool bTrack = wParam ? true : false;
            OnEnterMenuLoop(window, bTrack);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmExitMenuLoop:
        {
            bool bShortcut = wParam ? true : false;
            OnExitMenuLoop(window, bShortcut);
            bProcessed = true;
        }
        break;
        case wmEnterIdle:
        {
            Window causeWindow(PrimitiveHandle((void*)(lParam)));
            OnEnterIdle(window, wParam, causeWindow);
            lResult = 0;
            bProcessed = true;
        }
        break;
        }

        return bProcessed;

    }

