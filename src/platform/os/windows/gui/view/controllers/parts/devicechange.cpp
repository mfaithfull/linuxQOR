#include "src/configuration/configuration.h"

#include "devicechange.h"
#include "../../messages.h"

namespace qor{ namespace platform { namespace nswindows{ namespace gui{ namespace view{

    DeviceChangeController::DeviceChangeController() : BaseWindowPartController()
    {
    }

    bool DeviceChangeController::ProcessMessage(Window& Window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(Window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmDeviceChange:
        {
            lResult = OnDeviceChange(Window, wParam, lParam);
            bProcessed = true;
        }
        break;
        }

        return bProcessed;
    }

    long long DeviceChangeController::OnDeviceChange(Window& Window, unsigned long long wParam, long long lParam)
    {
        return Window.DefWindowProcT(wmDeviceChange, wParam, lParam);
    }

}}}}}//qor::platform::nswindows::gui::view
    