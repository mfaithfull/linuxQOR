    bool MenuController::ProcessMessage(Window& Wnd, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(Wnd, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmContextMenu:
        {
            Window wnd(PrimitiveHandle((void*)(wParam)));
            unsigned short wXPos = LoWord(lParam);
            unsigned short wYPos = HiWord(lParam);
            OnContextMenu(Wnd, wnd, wXPos, wYPos);
            bProcessed = true;
        }
        break;
        case wmInitMenu:
        {
            Menu menu(PrimitiveHandle((void*)(wParam)));
            OnInitMenu(Wnd, menu);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmInitMenuPopup:
        {
            Menu menu(PrimitiveHandle((void*)(wParam)));
            unsigned short wPos = LoWord(lParam);
            int bWindowMenu = static_cast<int>(HiWord(lParam));
            OnInitMenuPopup(Wnd, menu, wPos, bWindowMenu ? true : false);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmUninitMenuPopup:
        {
            Menu menu(PrimitiveHandle((void*)(wParam)));
            unsigned short wDestroyedMenu = HiWord(lParam);
            OnDestroyedMenuPopup(Wnd, menu, wDestroyedMenu);
            bProcessed = true;
        }
        break;
        case wmMenuSelect:
        {
            unsigned short wIndex = LoWord(wParam);
            unsigned short wFlags = HiWord(wParam);
            Menu menu(PrimitiveHandle((void*)(lParam)));
            OnMenuSelect(Wnd, menu, wIndex, wFlags);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmMenuChar:
        {
            unsigned short wCharCode = LoWord(wParam);
            unsigned short wMenuType = HiWord(wParam);
            Menu menu(PrimitiveHandle((void*)(lParam)));
            lResult = OnMenuChar(Wnd, menu, wCharCode, wMenuType);
            bProcessed = true;
        }
        break;
        case wmNextMenu:
        {
            MDINextMenu* pNextMenu = reinterpret_cast<MDINextMenu*>(lParam);

            Menu menuIn(PrimitiveHandle((void*)(pNextMenu->hmenuIn)));
            Menu menuNext(PrimitiveHandle((void*)(pNextMenu->hmenuNext)));

            OnNextMenu(Wnd, wParam, menuIn, menuNext);
            bProcessed = true;
        }
        break;
        case wmMenuGetObject:
        {
            MenuGetObjectInfo* pMenuObjectInfo = reinterpret_cast<MenuGetObjectInfo*>(lParam);
            lResult = OnGetObject(Wnd, pMenuObjectInfo);
            bProcessed = true;
        }
        break;
        case wmMenuRButtonUp:
        {
            Menu menu(PrimitiveHandle((void*)(lParam)));
            OnRButtonUp(Wnd, menu, wParam);
            bProcessed = true;
        }
        break;
        case wmMenuCommand:
        {
            Menu menu(PrimitiveHandle((void*)(lParam)));

            unsigned short wItemID = static_cast<unsigned short>(-1);
            unsigned long long iIndex = 0;
/*
            if (TheSystem().Info().As< windows::CSystemInfo >()->IsWindows98orME())
            {
                wItemID = LoWord(wParam);
                iIndex = static_cast<int>(HiWord(wParam));
            }
            else
            {*/
                iIndex = wParam;
            //}

            OnMenuCommand(Wnd, menu, iIndex, wItemID);

            bProcessed = true;
        }
        break;
        case wmMenuDrag:
        {
            Menu menu(PrimitiveHandle((void*)(lParam)));
            lResult = OnMenuDrag(Wnd, menu, static_cast<unsigned short>(wParam));
            bProcessed = true;
        }
        break;
        case wmCommand:
        {
            unsigned short wCode = HiWord(wParam);

            if (wCode == 0)
            {
                unsigned short wMenuID = LoWord(wParam);
                OnCommand(Wnd, wMenuID);
                lResult = 0;
                bProcessed = true;
            }
        }
        break;
        }

        return bProcessed;

    }

