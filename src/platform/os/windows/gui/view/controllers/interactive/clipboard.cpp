    bool ClipboardController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmRenderFormat:
        {
            OnRenderFormat(window, wParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmRenderAllFormats:
        {
            OnRenderAllFormats(window);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmDestroyClipboard:
        {
            OnDestroyData(window);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmPaintClipboard:
        {
            Window viewer(PrimitiveHandle((void*)(wParam)));
            PaintStruct* paintStruct = reinterpret_cast<PaintStruct*>(lParam);
            OnPaintClipboard(window, viewer, paintStruct);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmVScrollClipboard:
        {
            Window viewer(PrimitiveHandle((void*)(wParam)));
            unsigned short wEvent = LoWord(lParam);
            unsigned short wPos = HiWord(lParam);

            OnVSCrollClipboard(window, viewer, wEvent, wPos);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmSizeClipboard:
        {
            Window viewer(PrimitiveHandle((void*)(wParam)));
            Rect* rect = reinterpret_cast<Rect*>(lParam);
            OnSizeClipboard(window, viewer, rect);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmAskCBFormatName:
        {
            char* pszBuffer = reinterpret_cast<char*>(lParam);
            OnAskCBFormatName(window, pszBuffer, wParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmHScrollClipboard:
        {
            Window viewer(PrimitiveHandle((void*)(wParam)));
            unsigned short wEvent = LoWord(lParam);
            unsigned short wPos = HiWord(lParam);

            OnHScrollClipboard(window, viewer, wEvent, wPos);
            lResult = 0;
            bProcessed = true;
        }
        break;
        }

        return bProcessed;
    }

