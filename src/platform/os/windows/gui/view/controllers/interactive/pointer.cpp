    bool PointerController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmMouseMove:
        {
            OnMouseMove(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmLButtonDown:
        {
            OnLButtonDown(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmLButtonUp:
        {
            OnLButtonUp(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmLButtonDblClk:
        {
            OnLButtonDblClk(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmRButtonDown:
        {
            OnRButtonDown(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmRButtonUp:
        {
            OnRButtonUp(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmRButtonDblClk:
        {
            OnRButtonDblClk(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmMButtonDown:
        {
            OnMButtonDown(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmMButtonUp:
        {
            OnMButtonUp(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmMButtonDblClk:
        {
            OnMButtonDblClk(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmMouseWheel:
        {
            OnMouseWheel(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmCaptureChanged:
        {
            Window otherWindow(PrimitiveHandle((void*)(lParam)));
            OnCaptureChanged(window, otherWindow);
            lResult = 0;
            bProcessed = true;
        }
        break;

        case wmMouseHover:
        {
            OnMouseHover(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmMouseLeave:
        {
            OnMouseLeave(window);
            lResult = 0;
            bProcessed = true;
        }
        break;
        }

        return bProcessed;
    }

