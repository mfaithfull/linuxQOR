#include "src/configuration/configuration.h"

#include "scroll.h"
#include "../../messages.h"

namespace qor{ namespace platform { namespace nswindows{ namespace gui{ namespace view{

    ScrollController::ScrollController() : BaseWindowPartController()
    {
    }

    bool ScrollController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmVScroll:
        {
            OnVScroll(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        case wmHScroll:
        {
            OnHScroll(window, wParam, lParam);
            lResult = 0;
            bProcessed = true;
        }
        break;
        }

        return bProcessed;
    }
    
    void ScrollController::OnVScroll(Window& window, unsigned long long wParam, long long lParam)
    {
        window.DefWindowProcT(wmVScroll, wParam, lParam);
    }

    void ScrollController::OnHScroll(Window& window, unsigned long long wParam, long long lParam)
    {
        window.DefWindowProcT(wmHScroll, wParam, lParam);
    }

}}}}}//qor::platform::nswindows::gui::view
    