    bool WinCtrlParentController::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        bool bProcessed = ProcessHook(window, lResult, msg, wParam, lParam);

        switch (msg)
        {
        case wmCtlColorMsgBox:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window msgBox(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColourMsgBox(window, dc, msgBox).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmCtlColorEdit:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window edit(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColour(window, dc, edit, wmCtlColorEdit).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmCtlColorListBox:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window listBox(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColour(window, dc, listBox, wmCtlColorListBox).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmCtlColorBtn:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window button(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColour(window, dc, button,wmCtlColorBtn).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmCtlColorDlg:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window dlg(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColourDialog(window, dc, dlg).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmCtlColorScrollbar:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window scrollBar(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColour(window, dc, scrollBar, wmCtlColorStatic).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmCtlColorStatic:
        {
            DeviceContext dc(PrimitiveHandle((void*)(wParam)));
            Window staticWindow(PrimitiveHandle((void*)(lParam)));
            lResult = reinterpret_cast<long long>(OnCtlColour(window, dc, staticWindow, wmCtlColorStatic).GetHandle().Use());
            bProcessed = true;
        }
        break;
        case wmDrawItem:
        {
            DrawItemStruct* itemStruct = reinterpret_cast<DrawItemStruct*>(lParam);
            OnDrawItem(window, wParam, itemStruct);
            lResult = 1;
            bProcessed = true;
        }
        break;
        case wmCompareItem:
        {
            lResult = OnCompareItem(window, wParam, reinterpret_cast<ComparisonItem*>(lParam));
            bProcessed = true;
        }
        break;
        case wmMeasureItem:
        {
            MeasureItemStruct* itemStruct = reinterpret_cast<MeasureItemStruct*>(lParam);
            OnMeasureItem(window, wParam, itemStruct);
            lResult = 1;
            bProcessed = true;
        }
        break;
        case wmDeleteItem:
        {
            DeleteItemStruct* itemStruct = reinterpret_cast<DeleteItemStruct*>(lParam);
            OnDeleteItem(window, wParam, itemStruct);
            lResult = 1;
            bProcessed = true;
        }
        break;
        case wmVKeyToItem:
        {
            unsigned short wVKey = LoWord(wParam);
            unsigned short wPos = HiWord(wParam);
            Window listBox(PrimitiveHandle((void*)(lParam)));
            lResult = OnVKeyToItem(window, wVKey, wPos, listBox);
            bProcessed = true;
        }
        break;
        case wmCharToItem:
        {
            unsigned short wKey = LoWord(wParam);
            unsigned short wPos = HiWord(wParam);
            Window listBox(PrimitiveHandle((void*)(lParam)));
            lResult = OnCharToItem(window, wKey, wPos, listBox);
            bProcessed = true;
        }
        break;
        }

        return bProcessed;
    }
    
    Brush WinCtrlParentController::OnCtlColourMsgBox(Window& window, DeviceContext& dc, Window& msgBox)
    {        
        return Brush(PrimitiveHandle((void*)window.DefWindowProcT(wmCtlColorMsgBox, (unsigned long long)dc.GetHandle().Use(), (long long)msgBox.GetHandle().Use())));
    }
    
    Brush WinCtrlParentController::OnCtlColour(Window& window, DeviceContext& dc, Window& child, unsigned int msg)
    {   
       return Brush(PrimitiveHandle((void*)window.DefWindowProcT(msg, (unsigned long long)(dc.GetHandle().Use()), (long long)(child.GetHandle().Use()))));
    }

    
    Brush WinCtrlParentController::OnCtlColourDialog(Window& window, DeviceContext& dc, Window& dlg)
    {        
        return Brush(PrimitiveHandle((void*)(window.DefWindowProcT(wmCtlColorDlg, (unsigned long long)(dc.GetHandle().Use()), (long long)(dlg.GetHandle().Use())))));
    }

