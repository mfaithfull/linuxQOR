    bool ParentController::HandleMessage(Window& window, long long& lResult, unsigned int uMsg, unsigned long long wParam, long long lParam)
    {
        bool bHandled = false;

        switch (uMsg)
        {
        case wmParentNotify:
        {
            unsigned short wEvent = LoWord(wParam);
            unsigned short wID = HiWord(wParam);
            OnParentNotify(window, wEvent, wID, lParam);
            lResult = 0;
            bHandled = true;
        }
        break;
        case wmNotifyFormat:
        {
            Window fromWindow(PrimitiveHandle((void*)(wParam)));
            lResult = OnNotifyFormat(window, fromWindow, lParam);
            bHandled = true;
        }
        break;
        case wmNotify:
        {
            NotificationMessageHeader* pHdr = reinterpret_cast<NotificationMessageHeader*>(lParam);
            lResult = OnNotify(window, wParam, pHdr);
            bHandled = true;
        }
        break;
        case wmCommand:
        {
            unsigned short wCode = HiWord(wParam);
            unsigned short wCtrlID = LoWord(wParam);
            Window ctrl(PrimitiveHandle((void*)(lParam)));
            OnCommand(window, wCtrlID, wCode, ctrl);
            lResult = 0;
            bHandled = true;
        }
        break;
        }

        return bHandled;
    }

    void ParentController::OnParentNotify(Window& window, unsigned short wEvent, unsigned short wID, long long lParam)
    {
        switch (wEvent)
        {
        case wmCreate:
        {
            Window child(PrimitiveHandle((void*)(lParam)));
            OnChildCreate(window, wID, child);
        }
        break;
        case wmDestroy:
        {
            Window child(PrimitiveHandle((void*)(lParam)));
            OnChildDestroy(window, wID, child);
        }
        break;
        case wmLButtonDown:
        {
            unsigned short wXPos = LoWord(lParam);
            unsigned short wYPos = HiWord(lParam);
            OnChildLButtonDown(window, wXPos, wYPos);
        }
        break;
        case wmMButtonDown:
        {
            unsigned short wXPos = LoWord(lParam);
            unsigned short wYPos = HiWord(lParam);
            OnChildMButtonDown(window, wXPos, wYPos);
        }
        break;
        case wmRButtonDown:
        {
            unsigned short wXPos = LoWord(lParam);
            unsigned short wYPos = HiWord(lParam);
            OnChildRButtonDown(window, wXPos, wYPos);
        }
        break;
        case wmXButtonDown:
        {
            unsigned short wXPos = LoWord(lParam);
            unsigned short wYPos = HiWord(lParam);
            OnChildXButtonDown(window, wID, wXPos, wYPos);
        }
        break;
        default:
            wEvent = wEvent;
            break;
        }
        
    }

