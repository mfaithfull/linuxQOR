#include "iconized.h"
#include "../../messages.h"

namespace qor{ namespace platform { namespace nswindows{ namespace gui{ namespace view{

    bool IconizedHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(!ProcessHook(window, lResult, msg, wParam, lParam))
        {
            switch (msg)
            {
                case wmQueryOpen:
                {
                    lResult = OnQueryOpen(window) ? 1 : 0;
                    return true;
                }
                case wmPaintIcon:
                {   //Old win 3.x message
                    return true;
                }
                case wmIconEraseBkgnd:
                {
                    //Old win 3.x message
                    return true;
                }
                case wmQueryDragIcon:
                {
                    PrimitiveHandle iconHandle = OnQueryDragIcon(window);
                    lResult = reinterpret_cast<long long>(iconHandle.Use());
                    return true;
                }
            }
            return false;
        }
        return true;
    }

    bool IconizedHandler::OnQueryOpen(Window& window)
    {
        //No activation or focus changes allowed here
        return true;//Can the iconized window be restored?
    }

    PrimitiveHandle IconizedHandler::OnQueryDragIcon(Window& window)
    {
        return PrimitiveHandle(0);
    }

}}}}}//qor::platform::nswindows::gui::view

