    bool RenderingHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(!ProcessHook(window, lResult, msg, wParam, lParam))
        {
            switch (msg)
            {
                case wmPaint:
                {
                    if(OnPaint(window))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmSetRedraw:
                {
                    bool bRedraw = ((wParam == 1) ? true : false);
                    if(OnSetRedraw(window, bRedraw))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmEraseBackground:
                {
                    DeviceContext dc(PrimitiveHandle((void*)(wParam)));
                    if(OnEraseBackground(window, dc))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmSysColorChange:
                {
                    if(OnSysColourChange(window))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmShowWindow:
                {
                    bool bShow = wParam ? true : false;
                    if(OnShow(window, bShow, lParam))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmGetMinMaxInfo:
                {
                    MinMaxInfo* minMaxInfo = reinterpret_cast<MinMaxInfo*>(lParam);
                    if(OnGetMinMaxInfo(window, minMaxInfo))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmWindowPosChanging:
                {
                    WindowPos* pWindowPos = reinterpret_cast<WindowPos*>(lParam);
                    if(OnWindowPosChanging(window, pWindowPos))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmWindowPosChanged:
                {
                    WindowPos* pWindowPos = reinterpret_cast<WindowPos*>(lParam);
                    if(OnWindowPosChanged(window, pWindowPos))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmDisplayChange:
                {
                    unsigned short wBitsPP = static_cast<unsigned short>(wParam);
                    unsigned short hRes = LoWord(lParam);
                    unsigned short vRes = HiWord(lParam);
                    OnDisplayChange(window, wBitsPP, hRes, vRes);
                    return true;
                }
                case wmGetIcon:
                {
                    /*ICON_BIG
                    1
                    Retrieve the large icon for the window.
                    ICON_SMALL
                    0
                    Retrieve the small icon for the window.
                    ICON_SMALL2
                    2*/
                    unsigned int icon = static_cast<unsigned int>(wParam);
                    long long dpi = lParam;
                    lResult = OnGetIcon(window, icon, dpi);
                    return true;
                }
                case wmSetIcon:
                {
                    Icon icon(PrimitiveHandle((void*)(lParam)));
                    unsigned long long size = wParam;
                    lResult = OnSetIcon(window, size, icon);
                    return true;
                }
                case wmSyncPaint:
                {
                    if(OnSyncPaint(window))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmSizing:
                {
                    Rect* rect = reinterpret_cast<Rect*>(lParam);
                    if(OnSizing(window, wParam, rect))
                    {
                        lResult = 1;
                    }
                    return true;
                }
                case wmMoving:
                {
                    Rect* rect = reinterpret_cast<Rect*>(lParam);
                    if(OnMoving(window, rect))
                    {
                        lResult = 1;
                    }
                    return true;
                }
                case wmQueryNewPalette:
                {
                    lResult = OnQueryNewPalette(window) ? 1 : 0;
                    return true;
                }
                case wmPaletteIsChanging:
                {
                    Window win(PrimitiveHandle((void*)(wParam)));
                    if(OnPaletteChanging(win))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmPaletteChanged:
                {
                    Window win(PrimitiveHandle((void*)(wParam)));
                    OnPaletteChanged(win);
                    return true;
                }
                case wmPrint:
                {
                    DeviceContext dc(PrimitiveHandle((void*)(wParam)));
                    if(OnPrint(window, dc, lParam))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmPrintClient:
                {
                    DeviceContext dc(PrimitiveHandle((void*)(wParam)));
                    if(OnPrintClient(window, dc, lParam))
                    {
                        lResult = 0;
                    }
                    return true;
                }
                case wmSize:
                {
                    OnSize(window, wParam, lParam);
                    lResult = 0;
                    return true;
                }
                case wmMove:
                {
                    OnMove(window, LoWord(lParam), HiWord(lParam));
                    lResult = 0;
                    return true;
                }
            }
            return false;
        }
        return true;
    }

    void RenderingHandler::OnSize(Window& window, unsigned long long wParam, long long lParam)
    {
        window.DefWindowProcT(wmSize, wParam, lParam);
    }

    void RenderingHandler::OnMove(Window& window, unsigned short wXPos, unsigned short wYPos)
    {
        window.DefWindowProcT(wmMove, 0, MakeLong(wXPos, wYPos));
    }

