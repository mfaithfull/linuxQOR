    bool DesktopWindowManagerHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(!ProcessHook(window, lResult, msg, wParam, lParam))
        {
            switch (msg)
            {
                case wmDWMCompositionChanged://Informs all top-level windows that DWM composition has been enabled or disabled.
                {
                    lResult = OnCompositionChanged(window) ? 0 : window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                case wmDWMNonClientRenderingChanged://Sent when the non-client area rendering policy has changed.
                {
                    bool enabled = wParam ? true : false;
                    lResult = OnNonClientRenderingChanged(window, enabled) ? 0 : window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                case wmDWMColorizationColourChanged://Informs all top-level windows that the colorization color has changed.
                {
                    lResult = OnColorizationColourChanged(window, wParam, lParam) ? 0 : window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                case wmDWMWindowMaximizedChange://Sent when a DWM composed window is maximized.
                {
                    bool maximized = wParam ? true : false;
                    lResult = OnWindowMaximizedChange(window, maximized) ? 0 : window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                case wmDWMSendIconicThumbnail://Instructs a window to provide a static bitmap to use as a thumbnail representation of that window.
                {
                    int xMax = HiWord(lParam);
                    int yMax = LoWord(lParam);

                    lResult = OnSendIconicThumbnail(window, xMax, yMax) ? 0 : window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                case wmDWMSendIconicLivePreviewBitmap://Instructs a window to provide a static bitmap to use as a live preview (also known as a Peek preview) of that window.
                {
                    lResult = OnSendIconicLivePreviewBitmap(window) ? 0 : window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                return false;
            }
        }
        return true;
    }

