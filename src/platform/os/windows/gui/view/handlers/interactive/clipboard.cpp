    bool ClipboardHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(ProcessHook(window, lResult, msg, wParam, lParam))
        {
            return true;
        }

        switch (msg)
        {
            case wmRenderFormat:
            {
                if(OnRenderFormat(window, wParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmRenderAllFormats:
            {
                if(OnRenderAllFormats(window))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmDestroyClipboard:
            {
                if(OnDestroyData(window))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmPaintClipboard:
            {
                Window viewer(PrimitiveHandle((void*)(wParam)));
                PaintStruct* paintStruct = reinterpret_cast<PaintStruct*>(lParam);
                if(OnPaintClipboard(window, viewer, paintStruct))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmVScrollClipboard:
            {
                Window viewer(PrimitiveHandle((void*)(wParam)));
                unsigned short wEvent = LoWord(lParam);
                unsigned short wPos = HiWord(lParam);

                if(OnVSCrollClipboard(window, viewer, wEvent, wPos))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmSizeClipboard:
            {
                Window viewer(PrimitiveHandle((void*)(wParam)));
                Rect* rect = reinterpret_cast<Rect*>(lParam);
                if(OnSizeClipboard(window, viewer, rect))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmAskCBFormatName:
            {
                char* pszBuffer = reinterpret_cast<char*>(lParam);
                if(OnAskCBFormatName(window, pszBuffer, wParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmHScrollClipboard:
            {
                Window viewer(PrimitiveHandle((void*)(wParam)));
                unsigned short wEvent = LoWord(lParam);
                unsigned short wPos = HiWord(lParam);

                if(OnHScrollClipboard(window, viewer, wEvent, wPos))
                {
                    lResult = 0;
                }
                return true;
            }
        }
        return false;
    }

