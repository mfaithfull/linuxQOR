    bool PointerHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(ProcessHook(window, lResult, msg, wParam, lParam))
        {
            return true;
        }

        switch (msg)
        {
            case wmMouseMove:
            {
                if(OnMouseMove(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmLButtonDown:
            {
                if(OnLButtonDown(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmLButtonUp:
            {
                if(OnLButtonUp(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmLButtonDblClk:
            {
                if(OnLButtonDblClk(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmRButtonDown:
            {
                if(OnRButtonDown(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmRButtonUp:
            {
                if(OnRButtonUp(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmRButtonDblClk:
            {
                if(OnRButtonDblClk(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmMButtonDown:
            {
                if(OnMButtonDown(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmMButtonUp:
            {
                if(OnMButtonUp(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmMButtonDblClk:
            {
                if(OnMButtonDblClk(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmMouseWheel:
            {
                if(OnMouseWheel(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmCaptureChanged:
            {
                Window otherWindow(PrimitiveHandle((void*)(lParam)));
                if(OnCaptureChanged(window, otherWindow))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmMouseHover:
            {
                if(OnMouseHover(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmMouseLeave:
            {
                if(OnMouseLeave(window))
                {
                    lResult = 0;
                }
                return true;
            }
        }

        return false;
    }

