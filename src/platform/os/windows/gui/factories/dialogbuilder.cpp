namespace qor{ namespace platform { namespace nswindows{

    DialogItemViewModel::DialogItemViewModel(short x, short y, short width, short height, unsigned long style, unsigned long exStyle, unsigned short id) :
        m_xPos(x), m_yPos(y), m_width(width), m_height(height), m_style(style), m_ExStyle(exStyle), m_id(id)
    {
    }

    StaticDialogItemViewModel::StaticDialogItemViewModel(const std::wstring& message, short x, short y, short width, short height, unsigned long style, unsigned long exStyle, unsigned short id, unsigned short creationData) :
        m_message(message), m_creationData(creationData), DialogItemViewModel(x, y, width, height, style, exStyle, id)
    {        
    }

    ButtonDialogItemViewModel::ButtonDialogItemViewModel(const std::wstring& message, short x, short y, short width, short height, unsigned long style, unsigned long exStyle, unsigned short id, unsigned short creationData) :
        m_message(message), m_creationData(creationData), DialogItemViewModel(x, y, width, height, style, exStyle, id)
    {
    }

    unsigned int DialogBuilder::AddStatic(const std::wstring& message, short x, short y, short width, short height, unsigned long style, unsigned long exStyle, unsigned short id, unsigned short creationData)
    {
        if(id == 0)
        {
            auto perThread = new_ref<PerThread>();
            id = perThread->NextResourceId();
        }

        m_items.push_back(new_ref<StaticDialogItemViewModel>(message, x, y, width, height, style, exStyle, id, creationData));
        return id;
    }

    unsigned int DialogBuilder::AddButton(const std::wstring& message, short x, short y, short width, short height, unsigned long style, unsigned long exStyle, unsigned short id, unsigned short creationData)
    {
        if(id == 0)
        {
            auto perThread = new_ref<PerThread>();
            id = perThread->NextResourceId();
        }
        m_items.push_back(new_ref<ButtonDialogItemViewModel>(message, x, y, width, height, style, exStyle, id, creationData));
        return id;
    }

}}}//qor::platform::nswindows

