#include "src/qor/error/error.h"
#include "../returncheck.h"

namespace qor { namespace nswindows { namespace api {

    extern void WSALastErrorHandler();

   	template< typename TNumeric >
	class CheckNullPointerIsWinsockFailure
	{
	public:
		
		virtual bool Test(TNumeric& value)
		{
			return (value == nullptr) ? false : true;
		}

		virtual void DefaultInit(TNumeric& value)
		{
            value = nullptr;
		}

		static void Check(TNumeric& value, CheckNullPointerIsWinsockFailure* pInstance)
		{
			if (!pInstance->Test(value))
			{
				WSALastErrorHandler();
			}
		}

		static void Init(TNumeric& value, CheckNullPointerIsWinsockFailure* pInstance)
		{
			pInstance->DefaultInit(value);
		}

	};

   	template< typename TNumeric >
	class CheckNonZeroIsWinsockFailure
	{
	public:
		
		virtual bool Test(TNumeric& value)
		{
			return (value == 0) ? true : false;
		}

		virtual void DefaultInit(TNumeric& value)
		{
		}

		static void Check(TNumeric& value, CheckNonZeroIsWinsockFailure* pInstance)
		{
			if (!pInstance->Test(value))
			{
				WSALastErrorHandler();
			}
		}

		static void Init(TNumeric& value, CheckNonZeroIsWinsockFailure* pInstance)
		{
			pInstance->DefaultInit(value);
		}

	};

	template< typename TFailure, TFailure iFailure >
	class TCheckWinsockFailureValue
	{
	public:

		virtual void DefaultInit(TFailure& value)
		{
			value = iFailure;
		}

		virtual bool Test(TFailure& value)
		{
			return (value == iFailure) ? false : true;
		}

		static void Check(TFailure& value, TCheckWinsockFailureValue* pInstance)
		{
			if (!pInstance->Test(value))
			{
                WSALastErrorHandler();
			}
		}

		static void Init(TFailure& value, TCheckWinsockFailureValue* pInstance)
		{
			pInstance->DefaultInit(value);
		}

	};

}}}//qor:nswindows::api
