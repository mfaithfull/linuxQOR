    template< class TInterface >
    class CComPtr : public COObjectBase
    {
    public:

        CComPtr() : m_p(0)
        {
        }

        CComPtr(TInterface* p) : m_p(p)
        {
        }

        CComPtr(const CComPtr< TInterface >& src)
        {
            *this = src;
        }

        CComPtr& operator = (const CComPtr< TInterface >& src)
        {
            if (src.m_p != m_p)
            {
                if (m_p)
                {
                    m_p->Release();
                }
                m_p = src.m_p;
                m_p->AddRef();
            }

            return *this;
        }

        virtual ~CComPtr()
        {
            if (m_p)
            {
                m_p->Release();
                m_p = nullptr;
            }
        }

        operator TInterface*() const
        {
            return m_p;
        }

        TInterface* operator -> () const
        {
            return m_p;
        }

        TInterface** operator &()
        {
            return &m_p;
        }

        bool IsNull(void)
        {
            return m_p == 0 ? true : false;
        }

    protected:

        TInterface* m_p;

    };

