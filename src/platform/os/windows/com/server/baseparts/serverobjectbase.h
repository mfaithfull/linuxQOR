#ifndef QOR_PP_H_WINDOWS_COM_SERVER_BASE_SERVEROBJECTBASE
#define QOR_PP_H_WINDOWS_COM_SERVER_BASE_SERVEROBJECTBASE

#include "coobjectbase.h"
#include "unknownimplbase.h"

namespace qor{ namespace platform { namespace nswindows{

    template< class TInterface = IUnknown >
    class ServerObjectBase : public COObjectBase, public IUnknownImplBase< TInterface >
    {
    public:

        ServerObjectBase() = default;
        virtual ~ServerObjectBase() = default;
        
        TInterface* DefaultInterface(void)
        {
            TInterface* pDefaultInterface = 0;
            IUnknownImplBase< TInterface >::QueryInterface(&pDefaultInterface);
            return pDefaultInterface;
        }

        long QueryClientBlanket(unsigned long* pulAuthnSvc, unsigned long* pulAuthzSvc, wchar_t** pServerPrincName, unsigned long* pulAuthnLevel, unsigned long* pulImplLevel, void* pPrivs, unsigned long* pulCapabilities);
        long TestCancel(void);

    };

}}}//qor::platform::nswindows

#endif//QOR_PP_H_WINDOWS_COM_SERVER_BASE_SERVEROBJECTBASE

