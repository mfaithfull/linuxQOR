    bool ParentHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(ProcessHook(window, lResult, msg, wParam, lParam))
        {
            return true;
        }

        return /*TODO: WinCtrlParent().ProcessMessage(Window, lResult, uMsg, wParam, lParam) ||*/
            HandleMessage(window, lResult, msg, wParam, lParam);
    }
    
    bool ParentHandler::HandleMessage(Window& window, long long& lResult, unsigned int uMsg, unsigned long long wParam, long long lParam)
    {
        switch (uMsg)
        {
            case wmParentNotify:
            {
                unsigned short wEvent = LoWord(wParam);
                unsigned short wID = HiWord(wParam);
                if(OnParentNotify(window, wEvent, wID, lParam))
                {
                    lResult = 0;
                }
                else
                {
                    lResult = window.DefWindowProcT(uMsg, wParam, lParam);
                }
                return true;
            }
            case wmNotifyFormat:
            {
                Window fromWindow(PrimitiveHandle((void*)(wParam)));
                lResult = OnNotifyFormat(window, fromWindow, lParam);
                return true;
            }
            case wmNotify:
            {
                NotificationMessageHeader* pHdr = reinterpret_cast<NotificationMessageHeader*>(lParam);
                lResult = OnNotify(window, wParam, pHdr);
                return true;
            }
        }

        return false;
    }

    bool ParentHandler::OnParentNotify(Window& window, unsigned short wEvent, unsigned short wID, long long lParam)
    {
        switch (wEvent)
        {
            case wmCreate:
            {
                Window child(PrimitiveHandle((void*)(lParam)));
                return OnChildCreate(window, wID, child);
            }
            case wmDestroy:
            {
                Window child(PrimitiveHandle((void*)(lParam)));
                return OnChildDestroy(window, wID, child);
            }
            case wmLButtonDown:
            {
                unsigned short wXPos = LoWord(lParam);
                unsigned short wYPos = HiWord(lParam);
                return OnChildLButtonDown(window, wXPos, wYPos);
            }
            case wmMButtonDown:
            {
                unsigned short wXPos = LoWord(lParam);
                unsigned short wYPos = HiWord(lParam);
                return OnChildMButtonDown(window, wXPos, wYPos);
            }
            case wmRButtonDown:
            {
                unsigned short wXPos = LoWord(lParam);
                unsigned short wYPos = HiWord(lParam);
                return OnChildRButtonDown(window, wXPos, wYPos);
            }
            case wmXButtonDown:
            {
                unsigned short wXPos = LoWord(lParam);
                unsigned short wYPos = HiWord(lParam);
                return OnChildXButtonDown(window, wID, wXPos, wYPos);
            }
        }
        return false;        
    }

