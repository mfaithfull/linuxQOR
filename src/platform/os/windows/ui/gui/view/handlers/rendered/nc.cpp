#include "src/configuration/configuration.h"

#include "nc.h"
#include "../../messages.h"

namespace qor{ namespace platform { namespace win{ namespace gui{ namespace view{

    bool NonClientHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(!ProcessHook(window, lResult, msg, wParam, lParam))
        {
            switch (msg)
            {
                case wmNCCreate:
                {
                    CreateStruct* create = reinterpret_cast<CreateStruct*>(lParam);
                    lResult = OnNCCreate(window, create) ? 1 : 0;                    
                    return true;
                }
                case wmNCActivate:
                {
                    bool active = wParam ? true : false;
                    bool repaint = lParam != -1ll;
                    void* prevWindow = repaint ? (void*)lParam : nullptr;
                    OnNCActivate(window, active, repaint, prevWindow) ? 1 : 0;                    
                    lResult = window.DefWindowProcT(msg, wParam, lParam);
                    return true;
                }
                case wmNCDestroy:
                {
                    lResult = OnNCDestroy(window) ? 1 : 0;
                    return true;
                }
            }
            return false;
        }
        return true;
    }

    bool NonClientHandler::OnNCCreate(Window& window, CreateStruct* create)
    {
        return true;        
    }

    bool NonClientHandler::OnNCActivate(Window& window, bool active, bool repaint, void* prevWindow)
    {
        return true;
    }

    bool NonClientHandler::OnNCDestroy(Window& window)
    {
        return false;
    }

}}}}}//qor::platform::win::gui::view

