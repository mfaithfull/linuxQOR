    bool NCPointerHandler::ProcessMessage(Window& window, long long& lResult, unsigned int msg, unsigned long long wParam, long long lParam)
    {
        if(ProcessHook(window, lResult, msg, wParam, lParam))
        {
            return true;
        }

        switch (msg)
        {
            case wmNCMouseMove:
            {
                if(OnNCMouseMove(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCLButtonDown:
            {
                if(OnNCLButtonDown(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCLButtonUp:
            {
                if(OnNCLButtonUp(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCLButtonDblClk:
            {
                if(OnNCLButtonDblClk(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCRButtonDown:
            {
                if(OnNCRButtonDown(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCRButtonUp:
            {
                if(OnNCRButtonUp(window, wParam, lParam))
                {
                   lResult = 0;
                }
                return true;
            }
            case wmNCRButtonDblClk:
            {
                if(OnNCRButtonDblClk(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCMButtonDown:
            {
                if(OnNCMButtonDown(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
            case wmNCMButtonUp:
            {
                if(OnNCMButtonUp(window, wParam, lParam))
                {
                   lResult = 0;
                }
                return true;
            }
            case wmNCMButtonDblClk:
            {
                if(OnNCMButtonDblClk(window, wParam, lParam))
                {
                    lResult = 0;
                }
                return true;
            }
        }

        return false;
    }

