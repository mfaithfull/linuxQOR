    void HorizontalLayout::Place(Size position, Size extent)
    {
        long currentSize = 0;
        long minSize = 0;
        long maxSize = 0;
        long flexSize = 0;
        long flexCount = 0;

        for(auto item : m_items)
        {
            minSize += item->GetMinSize().m_x;
            maxSize += item->GetMaxSize().m_x;
            item->TmpSize().m_x = item->GetMinSize().m_x;
            currentSize += item->GetMinSize().m_x;
            if(item->GetMaxSize().m_x > item->GetMinSize().m_x)
            {
                flexSize += item->GetMinSize().m_x;
                flexCount++;
            }
        }
        if(minSize > extent.m_x)
        {
            //Overflow
        }
        if(minSize < extent.m_x)
        {
            long remaining = extent.m_x - minSize;
            while(remaining > 0 && flexCount > 0)   //Distribute remaining space among remaining flexible items
            {
                long totalDistributed = 0;
                for(auto& item : m_items)
                {
                    if(item->GetMaxSize().m_x > item->TmpSize().m_x)
                    {
                        long offset = std::max( 1L, std::min((item->GetMaxSize().m_x - item->TmpSize().m_x), remaining / (flexSize / item->TmpSize().m_x)));
                        totalDistributed += offset;
                        item->TmpSize().m_x += offset;
                        if(item->GetMaxSize().m_x == item->TmpSize().m_x)
                        {
                            flexCount--;
                        }
                    }
                    if(remaining - totalDistributed == 0)
                    {
                        break;
                    }
                }
                remaining -= totalDistributed;
                flexSize += totalDistributed;
            };
            if(remaining > 0)
            {
                //Underflow
                //We could space out the items or align them beginning or end or middle
            }
        }

        long placementY = 0;
        long placementX = 0;
        Size itemPos{0};
        Size itemExt{0};

        if(m_direction == forward)//Top to bottom for vertical
        {            
            for(auto& item : m_items)
            {
                itemPos.m_x = placementX;
                itemPos.m_y = placementY;
                itemExt.m_x = item->TmpSize().m_x;
                itemExt.m_y = extent.m_y;
                item->Place(itemPos, itemExt);
                placementX += itemExt.m_x;
            }
        }
        else
        {
            placementX = extent.m_x;
            for(auto& item : m_items)
            {
                itemPos.m_y = placementY;
                placementX -= itemExt.m_x;
                itemPos.m_x = placementX;
                itemExt.m_y = extent.m_y;
                itemExt.m_x = item->TmpSize().m_x;
                item->Place(itemPos, itemExt);
            }
        }               
    }

