namespace qor{ namespace platform { namespace win{

    void MenuBuilder::SetHelpId(unsigned long helpId)
    {
        m_helpId = helpId;
    }

    unsigned int MenuBuilder::AddItem(const tstring& item, unsigned long type, unsigned long state, unsigned short flags)
    {
        ref_of<PerThread>::type perThread = new_ref<PerThread>();
        std::pair<MenuExTemplateItem, tstring> itemData;
        itemData.second = item;
        itemData.first.dwState = state;
        itemData.first.dwType = type;
        itemData.first.wFlags = flags;
        itemData.first.uId = perThread->NextResourceId();
        m_items.emplace_back(itemData);
        return itemData.first.uId;
    }

    size_t MenuBuilder::CalcBufferNeeded()
    {
        size_t bufferByteCount = sizeof(MenuExTemplateHeader);
        for( auto item : m_items )
        {
            bufferByteCount += sizeof(MenuExTemplateItem);
            bufferByteCount += item.second.size() * sizeof(TCHAR);
            while(bufferByteCount % sizeof(unsigned long) != 0)
            {
                bufferByteCount++;
            }
        }
        return bufferByteCount;
    }

    ref_of<Menu>::type MenuBuilder::Build()
    {
        ref_of<Menu>::type menu;
        size_t bufferByteCount = CalcBufferNeeded();
        byte* buffer = new byte[bufferByteCount];
        byte* pointer = buffer;
        MenuExTemplateHeader header;
        header.helpId = m_helpId;
        memcpy(pointer, &header, sizeof(MenuExTemplateHeader));
        pointer += sizeof(MenuExTemplateHeader);
        for( auto item : m_items )
        {
            size_t itemSize = sizeof(MenuExTemplateItem);
            memcpy(pointer, &item.first, itemSize);
            pointer += itemSize;
            for( TCHAR c : item.second)
            {
                memcpy(pointer, &c, sizeof(TCHAR));
                itemSize += sizeof(TCHAR);
                pointer += sizeof(TCHAR);
            }
            while(itemSize % sizeof(unsigned long) != 0)
            {
                memset(pointer++, 0, 1);
                itemSize++;                
            }
        }
        menu = new_ref<Menu>(PrimitiveHandle(User32::LoadMenuIndirectT(reinterpret_cast<const MENUTEMPLATE*>(buffer))));
        return menu;
    }

}}}//qor::platform::win

