    int ListBoxController::SelItemRange(bool isSelected, int firstItem, int lastItem) const
    {
        if(isSelected)
        {
            WPARAM wparam = static_cast<WPARAM>(firstItem);
            LPARAM lparam = static_cast<LPARAM>(lastItem);
            return static_cast<int>(User32::SendMessageT(WINDOW_HANDLE, LB_SELITEMRANGEEX, wparam, lparam));
        }
        else
        {
            WPARAM wparam = static_cast<WPARAM>(lastItem);
            LPARAM lparam = static_cast<LPARAM>(firstItem);
            return static_cast<int>(User32::SendMessageT(WINDOW_HANDLE, LB_SELITEMRANGEEX, wparam, lparam));
        }
    }

