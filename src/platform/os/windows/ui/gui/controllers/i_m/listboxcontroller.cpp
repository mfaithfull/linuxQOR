    unsigned int ListBoxController::ItemFromPoint(Point pt, int& isOutside ) const
    {
        DWORD result = static_cast<DWORD>(User32::SendMessageT(WINDOW_HANDLE, LB_ITEMFROMPOINT, 0, MAKELPARAM(pt.m_x, pt.m_y)));
        isOutside = !!HIWORD(result);
        return LOWORD(result);
    }

