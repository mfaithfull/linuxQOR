    const char* Platform::GetScancodeName(int scancode)
    {
        if (scancode < 0 || scancode > (KF_EXTENDED | 0xff))
        {
            qor::gl::Session::InputError(qor::gl::INVALID_VALUE, "Invalid scancode %i", scancode);
            return nullptr;
        }

        const int key = TheSession().keycodes[scancode];

        if (key == qor::gl::KEY_UNKNOWN)
        {
            return nullptr;
        }

        return TheSession().keynames[key];

    }

