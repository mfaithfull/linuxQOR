    void Platform::SetClipboardString(const char* string)
    {
        int characterCount, tries = 0;
        HANDLE object;
        WCHAR* buffer;

        characterCount = MultiByteToWideChar(CP_UTF8, 0, string, -1, NULL, 0);
        if (!characterCount)
            return;

        object = GlobalAlloc(GMEM_MOVEABLE, characterCount * sizeof(WCHAR));
        if (!object)
        {
            Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to allocate global handle for clipboard");
            return;
        }

        buffer = (WCHAR*)GlobalLock(object);
        if (!buffer)
        {
            Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to lock global handle");
            GlobalFree(object);
            return;
        }

        MultiByteToWideChar(CP_UTF8, 0, string, -1, buffer, characterCount);
        GlobalUnlock(object);

        // NOTE: Retry clipboard opening a few times as some other application may have it
        //       open and also the Windows Clipboard History reads it after each update
        while (!OpenClipboard(TheSession().helperWindowHandle))
        {
            Sleep(1);
            tries++;

            if (tries == 3)
            {
                Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to open clipboard");
                GlobalFree(object);
                return;
            }
        }

        EmptyClipboard();
        SetClipboardData(CF_UNICODETEXT, object);
        CloseClipboard();

    }

    const char* Platform::GetClipboardString(void)
    {
        HANDLE object;
        WCHAR* buffer;
        int tries = 0;

        // NOTE: Retry clipboard opening a few times as some other application may have it
        //       open and also the Windows Clipboard History reads it after each update
        while (!OpenClipboard(TheSession().helperWindowHandle))
        {
            Sleep(1);
            tries++;

            if (tries == 3)
            {
                Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to open clipboard");
                return NULL;
            }
        }

        object = GetClipboardData(CF_UNICODETEXT);
        if (!object)
        {
            Session::InputErrorWin32(qor::gl::FORMAT_UNAVAILABLE, "Win32: Failed to convert clipboard to string");
            CloseClipboard();
            return NULL;
        }

        buffer = (WCHAR*)GlobalLock(object);
        if (!buffer)
        {
            Session::InputErrorWin32(qor::gl::PLATFORM_ERROR, "Win32: Failed to lock global handle");
            CloseClipboard();
            return NULL;
        }

        qor::gl::Session::free(TheSession().clipboardString);
        TheSession().clipboardString = Session::CreateUTF8FromWideStringWin32(buffer);

        GlobalUnlock(object);
        CloseClipboard();

        return TheSession().clipboardString;

    }

    int Platform::InitJoysticks(void)
    {
        if (TheSession().dinput8.instance)
        {
            if (FAILED(DirectInput8Create(TheSession().instance,
                                        DIRECTINPUT_VERSION,
                                        IID_IDirectInput8W,
                                        (void**) &TheSession().dinput8.api,
                                        NULL)))
            {
                qor::gl::Session::InputError(qor::gl::PLATFORM_ERROR, "Win32: Failed to create interface");
                return false;
            }
        }

        DetectJoystickConnectionWin32();
        return true;
    }

