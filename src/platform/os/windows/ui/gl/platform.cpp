    int Platform::PollJoystick(qor::gl::Joystick* joystick, int mode)
    {
        joystickWin32* js = dynamic_cast<joystickWin32*>(joystick);

        if (js->device)
        {
            int i, ai = 0, bi = 0, pi = 0;
            HRESULT result;
            DIJOYSTATE state = {0};

            IDirectInputDevice8_Poll(js->device);
            result = IDirectInputDevice8_GetDeviceState(js->device, sizeof(state), &state);
            if (result == DIERR_NOTACQUIRED || result == DIERR_INPUTLOST)
            {
                IDirectInputDevice8_Acquire(js->device);
                IDirectInputDevice8_Poll(js->device);
                result = IDirectInputDevice8_GetDeviceState(js->device, sizeof(state), &state);
            }

            if (FAILED(result))
            {
                delete js;
                return false;
            }

            if (mode == qor::gl::POLL_PRESENCE)
                return true;

            for (i = 0;  i < js->objectCount;  i++)
            {
                const void* data = (char*) &state + js->objects[i].offset;

                switch (js->objects[i].type)
                {
                    case TYPE_AXIS:
                    case TYPE_SLIDER:
                    {
                        const float value = (*((LONG*) data) + 0.5f) / 32767.5f;
                        qor::gl::Input::InputJoystickAxis(js, ai, value);
                        ai++;
                        break;
                    }

                    case TYPE_BUTTON:
                    {
                        const char value = (*((BYTE*) data) & 0x80) != 0;
                        qor::gl::Input::InputJoystickButton(js, bi, value);
                        bi++;
                        break;
                    }

                    case TYPE_POV:
                    {
                        const int states[9] =
                        {
                            qor::gl::HAT_UP,
                            qor::gl::HAT_RIGHT_UP,
                            qor::gl::HAT_RIGHT,
                            qor::gl::HAT_RIGHT_DOWN,
                            qor::gl::HAT_DOWN,
                            qor::gl::HAT_LEFT_DOWN,
                            qor::gl::HAT_LEFT,
                            qor::gl::HAT_LEFT_UP,
                            qor::gl::HAT_CENTERED
                        };

                        // Screams of horror are appropriate at this point
                        int stateIndex = LOWORD(*(DWORD*) data) / (45 * DI_DEGREES);
                        if (stateIndex < 0 || stateIndex > 8)
                            stateIndex = 8;

                        qor::gl::Input::InputJoystickHat(js, pi, states[stateIndex]);
                        pi++;
                        break;
                    }
                }
            }
        }
        else
        {
            int i, dpad = 0;
            DWORD result;
            XINPUT_STATE xis;
            const WORD buttons[10] =
            {
                XINPUT_GAMEPAD_A,
                XINPUT_GAMEPAD_B,
                XINPUT_GAMEPAD_X,
                XINPUT_GAMEPAD_Y,
                XINPUT_GAMEPAD_LEFT_SHOULDER,
                XINPUT_GAMEPAD_RIGHT_SHOULDER,
                XINPUT_GAMEPAD_BACK,
                XINPUT_GAMEPAD_START,
                XINPUT_GAMEPAD_LEFT_THUMB,
                XINPUT_GAMEPAD_RIGHT_THUMB
            };

            result = XInputGetState(js->index, &xis);
            if (result != ERROR_SUCCESS)
            {
                if (result == ERROR_DEVICE_NOT_CONNECTED)
                {
                    delete js;
                }

                return false;
            }

            if (mode == qor::gl::POLL_PRESENCE)
            {
                return true;
            }

            qor::gl::Input::InputJoystickAxis(js, 0, (xis.Gamepad.sThumbLX + 0.5f) / 32767.5f);
            qor::gl::Input::InputJoystickAxis(js, 1, -(xis.Gamepad.sThumbLY + 0.5f) / 32767.5f);
            qor::gl::Input::InputJoystickAxis(js, 2, (xis.Gamepad.sThumbRX + 0.5f) / 32767.5f);
            qor::gl::Input::InputJoystickAxis(js, 3, -(xis.Gamepad.sThumbRY + 0.5f) / 32767.5f);
            qor::gl::Input::InputJoystickAxis(js, 4, xis.Gamepad.bLeftTrigger / 127.5f - 1.f);
            qor::gl::Input::InputJoystickAxis(js, 5, xis.Gamepad.bRightTrigger / 127.5f - 1.f);

            for (i = 0;  i < 10;  i++)
            {
                const char value = (xis.Gamepad.wButtons & buttons[i]) ? 1 : 0;
                qor::gl::Input::InputJoystickButton(js, i, value);
            }

            if (xis.Gamepad.wButtons & XINPUT_GAMEPAD_DPAD_UP)
                dpad |= qor::gl::HAT_UP;
            if (xis.Gamepad.wButtons & XINPUT_GAMEPAD_DPAD_RIGHT)
                dpad |= qor::gl::HAT_RIGHT;
            if (xis.Gamepad.wButtons & XINPUT_GAMEPAD_DPAD_DOWN)
                dpad |= qor::gl::HAT_DOWN;
            if (xis.Gamepad.wButtons & XINPUT_GAMEPAD_DPAD_LEFT)
                dpad |= qor::gl::HAT_LEFT;

            // Treat invalid combinations as neither being pressed
            // while preserving what data can be preserved
            if ((dpad & qor::gl::HAT_RIGHT) && (dpad & qor::gl::HAT_LEFT))
                dpad &= ~(qor::gl::HAT_RIGHT | qor::gl::HAT_LEFT);
            if ((dpad & qor::gl::HAT_UP) && (dpad & qor::gl::HAT_DOWN))
                dpad &= ~(qor::gl::HAT_UP | qor::gl::HAT_DOWN);

            qor::gl::Input::InputJoystickHat(js, 0, dpad);
        }

        return true;
    }

