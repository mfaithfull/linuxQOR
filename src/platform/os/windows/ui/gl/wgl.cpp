    bool WGL::Init()
    {
        GetExtensionsStringEXT = (PFNWGLGETEXTENSIONSSTRINGEXTPROC) GetProcAddress("wglGetExtensionsStringEXT");
        GetExtensionsStringARB = (PFNWGLGETEXTENSIONSSTRINGARBPROC) GetProcAddress("wglGetExtensionsStringARB");
        CreateContextAttribsARB = (PFNWGLCREATECONTEXTATTRIBSARBPROC) GetProcAddress("wglCreateContextAttribsARB");
        SwapIntervalEXT = (PFNWGLSWAPINTERVALEXTPROC) GetProcAddress("wglSwapIntervalEXT");
        GetPixelFormatAttribivARB = (PFNWGLGETPIXELFORMATATTRIBIVARBPROC) GetProcAddress("wglGetPixelFormatAttribivARB");

        void* dc = nullptr;
        void* prc = nullptr;
        void* rc = nullptr;
        void* pdc = nullptr;

        //HDC dc = GetDC(_glfw.win32.helperWindowHandle);

        PixelFormatDescriptor pfd;        
        pfd.dwFlags = PFD_DRAW_TO_WINDOW | PFD_SUPPORT_OPENGL | PFD_DOUBLEBUFFER;
        pfd.iPixelType = PFD_TYPE_RGBA;
        pfd.cColorBits = 24;

        if (!GDI32::SetPixelFormat((HDC)dc, GDI32::ChoosePixelFormat((HDC)dc, reinterpret_cast<const PIXELFORMATDESCRIPTOR *>(&pfd)), reinterpret_cast<const PIXELFORMATDESCRIPTOR *>(&pfd)))
        {
            continuable("WGL: Failed to set pixel format for dummy context");
            return false;
        }

        rc = CreateContext(dc);
        if (!rc)
        {
            continuable("WGL: Failed to create dummy context");
            return false;
        }

        pdc = GetCurrentDC();
        prc = GetCurrentContext();

        if (!MakeCurrent(dc, rc))
        {
            MakeCurrent(pdc, prc);
            DeleteContext(rc);
            continuable("WGL: Failed to make dummy context current");
            return false;
        }

        ARB_multisample = ExtensionSupported("WGL_ARB_multisample");
        ARB_framebuffer_sRGB = ExtensionSupported("WGL_ARB_framebuffer_sRGB");
        EXT_framebuffer_sRGB = ExtensionSupported("WGL_EXT_framebuffer_sRGB");
        ARB_create_context = ExtensionSupported("WGL_ARB_create_context");
        ARB_create_context_profile = ExtensionSupported("WGL_ARB_create_context_profile");
        EXT_create_context_es2_profile = ExtensionSupported("WGL_EXT_create_context_es2_profile");
        ARB_create_context_robustness = ExtensionSupported("WGL_ARB_create_context_robustness");
        ARB_create_context_no_error = ExtensionSupported("WGL_ARB_create_context_no_error");
        EXT_swap_control = ExtensionSupported("WGL_EXT_swap_control");
        EXT_colorspace = ExtensionSupported("WGL_EXT_colorspace");
        ARB_pixel_format = ExtensionSupported("WGL_ARB_pixel_format");
        ARB_context_flush_control = ExtensionSupported("WGL_ARB_context_flush_control");

        MakeCurrent(pdc, prc);
        DeleteContext(rc);

        return true;
    }

    void* WGL::CreateContext(void* hdc)
    {
        return GDI32::wglCreateContext((HDC)(hdc));
    }

    bool WGL::DeleteContext(void* hglrc)
    {
        return GDI32::wglDeleteContext((HGLRC)(hglrc)) ? true : false;
    }
    
    void* WGL::GetProcAddress(const char* procName)
    {
        return GDI32::wglGetProcAddress(procName);
    }

    void* WGL::GetCurrentDC()
    {
        return GDI32::wglGetCurrentDC();
    }

    void* WGL::GetCurrentContext()
    {
        return GDI32::wglGetCurrentContext();
    }

    bool WGL::MakeCurrent(void* hdc, void* hglrc)
    {
        return GDI32::wglMakeCurrent((HDC)(hdc), (HGLRC)(hglrc)) ? true : false;
    }

    bool WGL::ShareLists(void* hglrc1, void* hglrc2)
    {
        return GDI32::wglShareLists((HGLRC)(hglrc1), (HGLRC)(hglrc2)) ? true : false;
    }

    // Searches an extension string for the specified extension    
    bool StringInExtensionString(const char* string, const char* extensions)
    {
        const char* start = extensions;
        for (;;)
        {
            const char* where;
            const char* terminator;
            where = strstr(start, string);
            if (!where)
            {
                return false;
            }
            terminator = where + strlen(string);
            if (where == start || *(where - 1) == ' ')
            {
                if (*terminator == ' ' || *terminator == '\0')
                {
                    break;
                }
            }
            start = terminator;
        }
        return true;
    }

    bool WGL::ExtensionSupported(const char* extension)
    {
        const char* extensions = NULL;

        if (GetExtensionsStringARB)
            extensions = GetExtensionsStringARB(GetCurrentDC());
        else if (GetExtensionsStringEXT)
            extensions = GetExtensionsStringEXT();

        if (!extensions)
            return false;

        return StringInExtensionString(extension, extensions);
    }

