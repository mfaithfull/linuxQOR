    FileWriter::FileWriter(ref_of<IFile>::type file)
    {
        m_file = file;
    }

    FileWriter::FileWriter(FileIndex index, OpenFor use)
    {
        auto fileSystem = ThePlatform(qor_shared)->GetSubsystem<FileSystem>();
        m_file = fileSystem->Open(index, use);
    }
    
    void FileWriter::WriteLine(const std::string& line, const std::string& end) const
    {        
        m_file->Write((byte*)line.data(), line.size());
        m_file->Write((byte*)end.data(), end.size());
    }

    void FileWriter::WriteByte(const byte& b) const
    {        
        m_file->Write((byte*)&b, 1);        
    }

    void FileWriter::WriteChar(const char& c) const
    {        
        m_file->Write((byte*)&c, 1);
    }

    void FileWriter::WriteBool(const bool& b) const
    {
        m_file->Write((byte*)&b, 1);
    }
        
    void FileWriter::WriteInt16(const int16_t& i, arch::Endian endian) const
    {
        if(endian == arch::host || (endian == qor::arch::endian))
        {
            m_file->Write((byte*)&i, 2);
        }
        else
        {
            byte* p = (byte*)&i;
            m_file->Write( p + 1, 1);
            m_file->Write( p, 1);
        }
    }

    void FileWriter::WriteUInt16(const uint16_t& ui, arch::Endian endian) const
    {
        byte* p = (byte*)&ui;
        if(endian == arch::host || (endian == qor::arch::endian))
        {
            m_file->Write(p, 2);
        }
        else
        {            
            m_file->Write( p + 1, 1);
            m_file->Write( p, 1);
        }
    }

    void FileWriter::WriteInt32(const int32_t& i, arch::Endian endian) const
    {
        byte* p = (byte*)&i;
        if(endian == arch::host || (endian == qor::arch::endian))
        {
            m_file->Write(p, 4);
        }
        else
        {            
            m_file->Write( p + 3, 1);
            m_file->Write( p + 2, 1);
            m_file->Write( p + 1, 1);
            m_file->Write( p, 1);
        }
    }

