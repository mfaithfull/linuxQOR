    void FileWriter::WriteInt64(const int64_t& i, arch::Endian endian) const
    {
        byte* p = (byte*)&i;
        if(endian == arch::host || (endian == qor::arch::endian))
        {
            m_file->Write(p, 8);
        }
        else
        {            
            m_file->Write( p + 7, 1);
            m_file->Write( p + 6, 1);
            m_file->Write( p + 5, 1);
            m_file->Write( p + 4, 1);
            m_file->Write( p + 3, 1);
            m_file->Write( p + 2, 1);
            m_file->Write( p + 1, 1);
            m_file->Write( p, 1);
        }
    }

