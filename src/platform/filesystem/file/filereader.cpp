    int16_t FileReader::ReadInt16(arch::Endian endian)
    {
        int16_t s = 0;
        if(endian == arch::host || (endian == qor::arch::endian))
        {            
            m_file->Read((byte*)&s, 2);            
        }
        else
        {            
            m_file->Read(((byte*)&s) + 1, 1);
            m_file->Read((byte*)&s, 1);
        }
        return s;
    }

