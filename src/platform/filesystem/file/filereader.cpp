    int32_t FileReader::ReadInt32(arch::Endian endian)
    {
        int32_t i = 0;
        if(endian == arch::host || (endian == qor::arch::endian))
        {            
            m_file->Read((byte*)&i, 4);
        }
        else
        {   
            byte data[4];
            m_file->Read(data, 4);
            byte* p = reinterpret_cast<byte*>(&i);
            for(unsigned int l = 0; l < 4; ++l)
            {
                *p++ = data[3 - l];
            }
        }
        return i;
    }

