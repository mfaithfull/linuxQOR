#include "src/configuration/configuration.h"

#include "filereader.h"
#include "src/platform/platform.h"
#include "src/platform/filesystem/filesystem.h"

namespace qor{ namespace platform{

    FileReader::FileReader(ref_of<IFile>::type file)
    {
        m_file = file;
    }

    FileReader::FileReader(FileIndex index, OpenFor use)
    {
        auto fileSystem = ThePlatform(qor_shared)->GetSubsystem<FileSystem>();
        m_file = fileSystem->Open(index, use);
    }

    std::string FileReader::ReadLine()
    {
        std::string line;
        char c = '\0';
        while( c != 0xA && m_file->Read((byte*)&c,1))
        {
            if( c != 0xD )
            {
                line.push_back(c);
            }
        }
        return line;
    }

    byte FileReader::ReadByte()
    {
        byte b;
        m_file->Read(&b, 1);
        return b;
    }

    char FileReader::ReadChar()
    {
        char c;
        m_file->Read((byte*)&c, 1);
        return c;
    }

}}//qor::platform