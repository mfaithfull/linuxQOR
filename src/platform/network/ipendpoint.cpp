#include "src/configuration/configuration.h"
#include "src/framework/thread/thread.h"
#include "ipendpoint.h"

namespace qor{ namespace network{

	std::string IPEndpoint::to_string() const
	{
		return is_ipv4() ? m_ipv4.to_string() : m_ipv6.to_string();
	}

	std::optional<IPEndpoint>
		IPEndpoint::from_string(std::string_view string) noexcept
	{
		if (auto ipv4 = IPv4Endpoint::from_string(string); ipv4)
		{
			return *ipv4;
		}

		if (auto ipv6 = IPv6Endpoint::from_string(string); ipv6)
		{
			return *ipv6;
		}

		return std::nullopt;
	}

}}//qor::network
