#ifndef QOR_PP_H_PLATFORM_NETWORK_IPV4ENDPOINT
#define QOR_PP_H_PLATFORM_NETWORK_IPV4ENDPOINT

#include "ipv4address.h"

namespace qor { namespace network {

	class IPv4Endpoint
	{
	public:

		// Construct to 0.0.0.0:0
		IPv4Endpoint() noexcept : m_address() , m_port(0)
		{
		}

		explicit IPv4Endpoint(IPv4Address address, std::uint16_t port = 0) noexcept : m_address(address) , m_port(port)
		{
		}

		const IPv4Address& address() const noexcept 
		{ 
			return m_address; 
		}

		std::uint16_t port() const noexcept 
		{ 
			return m_port; 
		}

		std::string to_string() const;

		static std::optional<IPv4Endpoint> from_string(std::string_view string) noexcept;

	private:

		IPv4Address m_address;
		std::uint16_t m_port;

	};

	inline bool operator==(const IPv4Endpoint& a, const IPv4Endpoint& b)
	{
		return a.address() == b.address() &&
			a.port() == b.port();
	}

	inline bool operator!=(const IPv4Endpoint& a, const IPv4Endpoint& b)
	{
		return !(a == b);
	}

	inline bool operator<(const IPv4Endpoint& a, const IPv4Endpoint& b)
	{
		return a.address() < b.address() ||
			(a.address() == b.address() && a.port() < b.port());
	}

	inline bool operator>(const IPv4Endpoint& a, const IPv4Endpoint& b)
	{
		return b < a;
	}

	inline bool operator<=(const IPv4Endpoint& a, const IPv4Endpoint& b)
	{
		return !(b < a);
	}

	inline bool operator>=(const IPv4Endpoint& a, const IPv4Endpoint& b)
	{
		return !(a < b);
	}

}}//qor::network

#endif//QOR_PP_H_PLATFORM_NETWORK_IPV4ENDPOINT
