    void AddressInfoToWindows(const network::AddressInfo& addressinfo, ADDRINFOA& info)
    {
        memset(&info, 0, sizeof(struct addrinfo));
        AddressInfoFlagsToWindows( addressinfo.flags, info.ai_flags);
        info.ai_family = Socket::AddressFamilyToWindows(addressinfo.family);
        info.ai_socktype = Socket::TypeToWindows(addressinfo.socktype, false);
        info.ai_protocol = Socket::ProtocolToWindows(addressinfo.protocol);
    }

    void AddressInfoFromWindows(const addrinfo& info, network::AddressInfo& addressinfo)
    {
        addressinfo.flags = info.ai_flags;
        addressinfo.socktype = Socket::TypeFromWindows(info.ai_socktype);
        addressinfo.protocol = Socket::ProtocolFromWindows(info.ai_protocol);
        addressinfo.family = Socket::AddressFamilyFromWindows(info.ai_family);
        addressinfo.canonname = info.ai_canonname == nullptr ? "" : std::string(info.ai_canonname);
        addressinfo.address.sa_family = addressinfo.family;
        memcpy(addressinfo.address.sa.sa_data, info.ai_addr->sa_data, info.ai_addrlen);
    }

    ref_of<network::Socket>::type Sockets::CreateSocket(const network::sockets::eAddressFamily AF, const network::sockets::eType Type, const network::sockets::eProtocol Protocol) const
    {
        ref_of<network::Socket>::type socket = new_ref<Socket>(AF, Type, Protocol).AsRef<network::Socket>();
        return socket;
    }

    ref_of<network::Socket>::type Sockets::CreateAsyncSocket(const network::sockets::eAddressFamily AF, const network::sockets::eType Type, const network::sockets::eProtocol Protocol, qor::framework::AsyncIOContext* ioContext) const
    {
        ref_of<network::Socket>::type socket = CreateSocket(AF,Type,Protocol); 
        if( ioContext != nullptr)
        {
            ioContext->Enroll(socket());
        }
        return socket;
    }

    void Sockets::Setup()
    {
        WORD wVersionRequested;
        WSADATA wsaData;
        int err;        
        wVersionRequested = MAKEWORD(2, 2);

        err = WS2::WSAStartup(wVersionRequested, &wsaData);
        if (err != 0) 
        {
            serious("Could not find a suitable Windows sockets library.");
        }
    }

    void Sockets::Shutdown()
    {
        int err = WS2::WSACleanup();
    }

    int Sockets::GetAddressInfo(const std::string& node, const std::string& service, const network::AddressInfo& hints, std::vector<network::AddressInfo>& results) const
    {
        struct addrinfo hintinfo;
        AddressInfoToWindows(hints, hintinfo);

        struct addrinfo *res = nullptr;

        auto result = WS2::getaddrinfo(node.empty() ? nullptr : node.c_str(), service.empty() ? nullptr : service.c_str(), &hintinfo, &res);
        
        if(result != 0)
        {      
            return -1;
        }

        for (auto rp = res; rp; rp = rp->ai_next)
        {
            auto socket = CreateSocket(
                Socket::AddressFamilyFromWindows(rp->ai_family), 
                Socket::TypeFromWindows(rp->ai_socktype),
                Socket::ProtocolFromWindows(rp->ai_protocol));
            if(socket.IsNull())
            {
                continue;
            }
         
            network::AddressInfo resultAddressInfo;

            AddressInfoFromWindows(*rp, resultAddressInfo);
            
            results.emplace_back(resultAddressInfo);
        }

        WS2::freeaddrinfo(res);
        return results.size();
    }

