    {
        //TODO: thread local window context
        Window* previous = (Window*)(TheSession().GetPlatform().PlatformGetTls(&TheSession().contextSlot));

        if (this && context.client == NO_API)
        {
            continuable("Cannot make current with a window that has no OpenGL or OpenGL ES context");
            return;
        }

        if (previous)
        {
            if (context.source != previous->context.source)
            {
                previous->context.MakeCurrent(nullptr);
            }
        }

        context.MakeCurrent(this);
    }

