    void Window::SetIcon(int count, const Image* images)
    {
        int i;
        qor_pp_assert_that(count >= 0);
        qor_pp_assert_that(count == 0 || images != nullptr);        
        qor_pp_assert_that(this != nullptr);

        if (count < 0)
        {
            continuable("Invalid image count for window icon");
            return;
        }

        for (i = 0; i < count; i++)
        {
            qor_pp_assert_that(images[i].pixels != nullptr);

            if (images[i].width <= 0 || images[i].height <= 0)
            {
                continuable("Invalid image dimensions for window icon");
                return;
            }
        }

        m_impl->SetIcon(count, images);        
    }

