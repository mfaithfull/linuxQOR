    void Window::SetSizeLimits(int minwidth, int minheight, int maxwidth, int maxheight)
    {
        qor_pp_assert_that(this != nullptr);

        if (minwidth != DONT_CARE && minheight != DONT_CARE)
        {
            if (minwidth < 0 || minheight < 0)
            {
                continuable("Invalid window minimum size {0:i}x{1:i}", minwidth, minheight);
                return;
            }
        }

        if (maxwidth != DONT_CARE && maxheight != DONT_CARE)
        {
            if (maxwidth < 0 || maxheight < 0 || maxwidth < minwidth || maxheight < minheight)
            {
                continuable("Invalid window maximum {0:i}x{1:i}", maxwidth, maxheight);
                return;
            }
        }

        m_minwidth  = minwidth;
        m_minheight = minheight;
        m_maxwidth  = maxwidth;
        m_maxheight = maxheight;

        if (m_monitor || !m_resizable)
        {
            return;
        }

        m_impl->SetSizeLimits(minwidth, minheight, maxwidth, maxheight);
    }

