    Window::Window(int width, int height, const std::string& title, Monitor* monitor, Window* share)
    {
        FBconfig fbconfig;
        Ctxconfig ctxconfig;
        Wndconfig wndconfig;
        qor_pp_assert_that(width > 0);
        qor_pp_assert_that(height > 0);

        fbconfig  = TheSession().hints.framebuffer;
        ctxconfig = TheSession().hints.context;
        wndconfig = TheSession().hints.window;

        wndconfig.width   = width;
        wndconfig.height  = height;
        ctxconfig.share   = share;

        if (!ctxconfig.IsValid())
        {
            fatal("Context configuration is invalid when trying to construct a qor::gl Window.");
        }
        
        next = TheSession().windowListHead;
        TheSession().windowListHead = this;

        m_videoMode.width       = width;
        m_videoMode.height      = height;
        m_videoMode.redBits     = fbconfig.redBits;
        m_videoMode.greenBits   = fbconfig.greenBits;
        m_videoMode.blueBits    = fbconfig.blueBits;
        m_videoMode.refreshRate = TheSession().hints.refreshRate;

        monitor          = monitor;
        m_resizable        = wndconfig.resizable;
        m_decorated      = wndconfig.decorated;
        m_autoIconify      = wndconfig.autoIconify;
        m_floating         = wndconfig.floating;
        focusOnShow      = wndconfig.focusOnShow;
        mousePassthrough = wndconfig.mousePassthrough;
        m_cursorMode       = CURSOR_NORMAL;

        doublebuffer = fbconfig.doublebuffer;

        m_minwidth    = DONT_CARE;
        m_minheight   = DONT_CARE;
        m_maxwidth    = DONT_CARE;
        m_maxheight   = DONT_CARE;
        m_numer       = DONT_CARE;
        m_denom       = DONT_CARE;
        this->title = title;

        m_impl = new_ref<WindowPimpl>(this, wndconfig, ctxconfig, fbconfig);
        //if (!TheSession().platform.CreateWindow(window, &wndconfig, &ctxconfig, &fbconfig))

    }

    Window* Window::Create(int width, int height, const std::string& title, Monitor* monitor, Window* share)
    {
        FBconfig fbconfig;
        Ctxconfig ctxconfig;
        Wndconfig wndconfig;
        Window* window = nullptr;

        qor_pp_assert_that(width >= 0);
        qor_pp_assert_that(height >= 0);

        REQUIRE_INIT_OR_RETURN(nullptr);

        if (width <= 0 || height <= 0)
        {
            continuable("Invalid window size {0:i}x{1:i}", width, height);
            return nullptr;
        }

        fbconfig  = TheSession().hints.framebuffer;
        ctxconfig = TheSession().hints.context;
        wndconfig = TheSession().hints.window;

        wndconfig.width   = width;
        wndconfig.height  = height;
        ctxconfig.share   = (Window*) share;

        if (!ctxconfig.IsValid())
        {
            return nullptr;
        }

        window = (Window*)Session::alloc(1, sizeof(Window));
        window->next = TheSession().windowListHead;
        TheSession().windowListHead = window;

        window->m_videoMode.width       = width;
        window->m_videoMode.height      = height;
        window->m_videoMode.redBits     = fbconfig.redBits;
        window->m_videoMode.greenBits   = fbconfig.greenBits;
        window->m_videoMode.blueBits    = fbconfig.blueBits;
        window->m_videoMode.refreshRate = TheSession().hints.refreshRate;

        window->m_monitor          = (Monitor*) monitor;
        window->m_resizable        = wndconfig.resizable;
        window->m_decorated      = wndconfig.decorated;
        window->m_autoIconify      = wndconfig.autoIconify;
        window->m_floating         = wndconfig.floating;
        window->focusOnShow      = wndconfig.focusOnShow;
        window->mousePassthrough = wndconfig.mousePassthrough;
        window->m_cursorMode       = CURSOR_NORMAL;

        window->doublebuffer = fbconfig.doublebuffer;

        window->m_minwidth    = DONT_CARE;
        window->m_minheight   = DONT_CARE;
        window->m_maxwidth    = DONT_CARE;
        window->m_maxheight   = DONT_CARE;
        window->m_numer       = DONT_CARE;
        window->m_denom       = DONT_CARE;
        window->title       = title;

        if (!TheSession().GetPlatform().CreateWindow(window, &wndconfig, &ctxconfig, &fbconfig))
        {
            window->Destroy();
            return nullptr;
        }

        return window;
    }

    void Window::DefaultHints(void)
    {
        REQUIRE_INIT();

        // The default is OpenGL with minimum version 1.0
        memset(&TheSession().hints.context, 0, sizeof(TheSession().hints.context));
        TheSession().hints.context.client = OPENGL_API;
        TheSession().hints.context.source = NATIVE_CONTEXT_API;
        TheSession().hints.context.major  = 1;
        TheSession().hints.context.minor  = 0;

        // The default is a focused, visible, resizable window with decorations
        memset(&TheSession().hints.window, 0, sizeof(TheSession().hints.window));
        TheSession().hints.window.resizable    = true;
        TheSession().hints.window.visible      = true;
        TheSession().hints.window.decorated    = true;
        TheSession().hints.window.focused      = true;
        TheSession().hints.window.autoIconify  = true;
        TheSession().hints.window.centerCursor = true;
        TheSession().hints.window.focusOnShow  = true;
        TheSession().hints.window.xpos         = ANY_POSITION;
        TheSession().hints.window.ypos         = ANY_POSITION;
        TheSession().hints.window.scaleFramebuffer = true;

        // The default is 24 bits of color, 24 bits of depth and 8 bits of stencil,
        // double buffered
        memset(&TheSession().hints.framebuffer, 0, sizeof(TheSession().hints.framebuffer));
        TheSession().hints.framebuffer.redBits      = 8;
        TheSession().hints.framebuffer.greenBits    = 8;
        TheSession().hints.framebuffer.blueBits     = 8;
        TheSession().hints.framebuffer.alphaBits    = 8;
        TheSession().hints.framebuffer.depthBits    = 24;
        TheSession().hints.framebuffer.stencilBits  = 8;
        TheSession().hints.framebuffer.doublebuffer = true;

        // The default is to select the highest available refresh rate
        TheSession().hints.refreshRate = DONT_CARE;
        
    }

