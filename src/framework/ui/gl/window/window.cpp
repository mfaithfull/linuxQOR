    void Window::InputWindowFocus(Window* window, bool focused)
    {
        qor_pp_assert_that(window != nullptr);        

        if (window->callbacks.focus)
        {
            window->callbacks.focus(window, focused);
        }

        if (!focused)
        {
            int key, button;

            for (key = 0;  key <= KEY_LAST;  key++)
            {
                if (window->keys[key] == Input::PRESS)
                {
                    const int scancode = TheSession().GetPlatform().GetKeyScancode(key);
                    Input::InputKey(window, key, scancode, Input::RELEASE, 0);
                }
            }

            for (button = 0;  button <= Input::MOUSE_BUTTON_LAST;  button++)
            {
                if (window->mouseButtons[button] == Input::PRESS)
                {
                    Input::InputMouseClick(window, button, Input::RELEASE, 0);
                }
            }
        }

    }

