    bool Ctxconfig::IsValid()
    {
        if(this == nullptr)
        {
            return false;
        }

        if (source != NATIVE_CONTEXT_API && source != EGL_CONTEXT_API && source != OSMESA_CONTEXT_API)
        {
            Session::InputError(INVALID_ENUM, "Invalid context creation API 0x%08X", source);
            return false;
        }

        if (client != NO_API && client != OPENGL_API && client != OPENGL_ES_API)
        {
            Session::InputError(INVALID_ENUM, "Invalid client API 0x%08X", client);
            return false;
        }

        if (share)
        {
            if (client == NO_API || share->context.client == NO_API)
            {
                Session::InputError(NO_WINDOW_CONTEXT, nullptr);
                return false;
            }

            if (source != share->context.source)
            {
                Session::InputError(INVALID_ENUM, "Context creation APIs do not match between contexts");
                return false;
            }
        }

        if (client == OPENGL_API)
        {
            if ((major < 1 || minor < 0) ||
                (major == 1 && minor > 5) ||
                (major == 2 && minor > 1) ||
                (major == 3 && minor > 3))
            {
                // OpenGL 1.0 is the smallest valid version
                // OpenGL 1.x series ended with version 1.5
                // OpenGL 2.x series ended with version 2.1
                // OpenGL 3.x series ended with version 3.3
                // For now, let everything else through

                Session::InputError(INVALID_VALUE, "Invalid OpenGL version %i.%i", major, minor);
                return false;
            }

            if (profile)
            {
                if (profile != OPENGL_CORE_PROFILE && profile != OPENGL_COMPAT_PROFILE)
                {
                    Session::InputError(INVALID_ENUM, "Invalid OpenGL profile 0x%08X", profile);
                    return false;
                }

                if (major <= 2 || (major == 3 && minor < 2))
                {
                    // Desktop OpenGL context profiles are only defined for version 3.2 and above
                    Session::InputError(INVALID_VALUE, "Context profiles are only defined for OpenGL version 3.2 and above");
                    return false;
                }
            }

            if (forward && major <= 2)
            {
                // Forward-compatible contexts are only defined for OpenGL version 3.0 and above
                Session::InputError(INVALID_VALUE, "Forward-compatibility is only defined for OpenGL version 3.0 and above");
                return false;
            }
        }
        else if (client == OPENGL_ES_API)
        {
            if (major < 1 || minor < 0 ||
                (major == 1 && minor > 1) ||
                (major == 2 && minor > 0))
            {
                // OpenGL ES 1.0 is the smallest valid version
                // OpenGL ES 1.x series ended with version 1.1
                // OpenGL ES 2.x series ended with version 2.0
                // For now, let everything else through

                Session::InputError(INVALID_VALUE, "Invalid OpenGL ES version %i.%i", major, minor);
                return false;
            }
        }

        if (robustness)
        {
            if (robustness != NO_RESET_NOTIFICATION && robustness != LOSE_CONTEXT_ON_RESET)
            {
                Session::InputError(INVALID_ENUM, "Invalid context robustness mode 0x%08X", robustness);
                return false;
            }
        }

        if (release)
        {
            if (release != RELEASE_BEHAVIOR_NONE && release != RELEASE_BEHAVIOR_FLUSH)
            {
                Session::InputError(INVALID_ENUM, "Invalid context release behavior 0x%08X", release);
                return false;
            }
        }

        return true;
    }
