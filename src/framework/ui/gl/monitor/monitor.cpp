    void Monitor::AllocGammaArrays(GammaRamp* ramp, unsigned int size)
    {
        ramp->red = (unsigned short*)Session::alloc(size, sizeof(unsigned short));
        ramp->green = (unsigned short*)Session::alloc(size, sizeof(unsigned short));
        ramp->blue = (unsigned short*)Session::alloc(size, sizeof(unsigned short));
        ramp->size = size;
    }

    void Monitor::FreeGammaArrays(GammaRamp* ramp)
    {
        Session::free(ramp->red);
        Session::free(ramp->green);
        Session::free(ramp->blue);
        memset(ramp, 0, sizeof(GammaRamp));
    }

    int Monitor::RefreshVideoModes(Monitor* monitor)
    {
        int modeCount;
        VidMode* modes;

        if (monitor->modes)
            return 1;

        modes = TheSession().GetPlatform().GetVideoModes(monitor, &modeCount);
        if (!modes)
            return 0;

        qsort(modes, modeCount, sizeof(VidMode), (_CoreCrtNonSecureSearchSortCompareFunction)&Monitor::CompareVideoModes);

        Session::free(monitor->modes);
        monitor->modes = modes;
        monitor->modeCount = modeCount;

        return 1;
    }

    const VidMode* Monitor::ChooseVideoMode(Monitor* monitor,const VidMode* desired)
    {
        int i;
        unsigned int sizeDiff, leastSizeDiff = UINT_MAX;
        unsigned int rateDiff, leastRateDiff = UINT_MAX;
        unsigned int colorDiff, leastColorDiff = UINT_MAX;
        const VidMode* current;
        const VidMode* closest = NULL;

        if (!RefreshVideoModes(monitor))
        {
            return nullptr;
        }

        for (i = 0;  i < monitor->modeCount;  i++)
        {
            current = monitor->modes + i;

            colorDiff = 0;

            if (desired->redBits != DONT_CARE)
                colorDiff += abs(current->redBits - desired->redBits);
            if (desired->greenBits != DONT_CARE)
                colorDiff += abs(current->greenBits - desired->greenBits);
            if (desired->blueBits != DONT_CARE)
                colorDiff += abs(current->blueBits - desired->blueBits);

            sizeDiff = abs((current->width - desired->width) *
                        (current->width - desired->width) +
                        (current->height - desired->height) *
                        (current->height - desired->height));

            if (desired->refreshRate != DONT_CARE)
                rateDiff = abs(current->refreshRate - desired->refreshRate);
            else
                rateDiff = UINT_MAX - current->refreshRate;

            if ((colorDiff < leastColorDiff) ||
                (colorDiff == leastColorDiff && sizeDiff < leastSizeDiff) ||
                (colorDiff == leastColorDiff && sizeDiff == leastSizeDiff && rateDiff < leastRateDiff))
            {
                closest = current;
                leastSizeDiff = sizeDiff;
                leastRateDiff = rateDiff;
                leastColorDiff = colorDiff;
            }
        }

        return closest;

    }

    int Monitor::CompareVideoModes(const VidMode* fp, const VidMode* sp)
    {
        const VidMode* fm = fp;
        const VidMode* sm = sp;
        const int fbpp = fm->redBits + fm->greenBits + fm->blueBits;
        const int sbpp = sm->redBits + sm->greenBits + sm->blueBits;
        const int farea = fm->width * fm->height;
        const int sarea = sm->width * sm->height;

        // First sort on color bits per pixel
        if (fbpp != sbpp)
            return fbpp - sbpp;

        // Then sort on screen area
        if (farea != sarea)
            return farea - sarea;

        // Then sort on width
        if (fm->width != sm->width)
            return fm->width - sm->width;

        // Lastly sort on refresh rate
        return fm->refreshRate - sm->refreshRate;

    }

    void Monitor::SplitBPP(int bpp, int* red, int* green, int* blue)
    {
        int delta;

        // We assume that by 32 the user really meant 24
        if (bpp == 32)
        {
            bpp = 24;
        }
        
        // Convert "bits per pixel" to red, green & blue sizes
        *red = *green = *blue = bpp / 3;
        delta = bpp - (*red * 3);
        if (delta >= 1)
        {
            *green = *green + 1;
        }
        
        if (delta == 2)
        {
            *red = *red + 1;
        }
    }

