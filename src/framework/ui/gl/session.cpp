    void Session::InputError(int code, const char* format, ...)
    {                
        if (format)
        {
            va_list vl;
            va_start(vl, format);
            continuable(format, vl);
            va_end(vl);
        }
        else
        {
            switch(code)
            {
                case NOT_INITIALIZED:
                    continuable("The GLFW library is not initialized");
                    break;
                case NO_CURRENT_CONTEXT:
                    continuable("There is no current context");
                    break;
                case INVALID_ENUM:
                    continuable("Invalid argument for enum parameter");
                    break;
                case INVALID_VALUE:
                    continuable("Invalid value for parameter");
                    break;
                case OUT_OF_MEMORY:
                    continuable("Out of memory");
                    break;
                case API_UNAVAILABLE:
                    continuable("The requested API is unavailable");
                    break;
                case VERSION_UNAVAILABLE:
                    continuable("The requested API version is unavailable");
                    break;
                case PLATFORM_ERROR:
                    continuable("A platform-specific error occurred");
                    break;
                case FORMAT_UNAVAILABLE:
                    continuable("The requested format is unavailable");
                    break;
                case NO_WINDOW_CONTEXT:
                    continuable("The specified window has no context");
                    break;
                case CURSOR_UNAVAILABLE:
                    continuable("The specified cursor shape is unavailable");
                    break;
                case FEATURE_UNAVAILABLE:
                    continuable("The requested feature cannot be implemented for this platform");
                    break;
                case FEATURE_UNIMPLEMENTED:
                    continuable("The requested feature has not yet been implemented for this platform");
                    break;
                case PLATFORM_UNAVAILABLE:
                    continuable("The requested platform is unavailable");
                    break;
                default:
                    serious("ERROR: UNKNOWN ERROR");
            }
        }
    }

