    void* Session::alloc(size_t count, size_t size)
    {
        return new byte[count * size];
    }

    void* Session::realloc(void* block, size_t size)
    {
        byte* newAlloc = new byte[size];
        memcpy(newAlloc, block, size);
        delete block;
        return newAlloc;
    }

    void Session::free(void* block)
    {
        delete block;
    }

