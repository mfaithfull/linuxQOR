    const char* Input::GetKeyName(int key, int scancode)
    {
        RequireInitOrReturn(nullptr);

        if (key != KEY_UNKNOWN)
        {
            if (key < KEY_SPACE || key > KEY_LAST)
            {
                Session::InputError(INVALID_ENUM, "Invalid key %i", key);
                return nullptr;
            }

            if (key != KEY_KP_EQUAL &&
                (key < KEY_KP_0 || key > KEY_KP_ADD) &&
                (key < KEY_APOSTROPHE || key > KEY_WORLD_2))
            {
                return nullptr;
            }

            scancode = TheSession().GetPlatform().GetKeyScancode(key);
        }

        return TheSession().GetPlatform().GetScancodeName(scancode);

    }

