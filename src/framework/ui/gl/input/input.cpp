    void Input::InitGamepadMappings(void)
    {
        size_t i;
        const size_t count = sizeof(DefaultMappings) / sizeof(char*);
        TheSession().mappings = (Mapping*)Session::alloc(count, sizeof(Mapping));

        for (i = 0;  i < count;  i++)
        {
            if (ParseMapping(&TheSession().mappings[TheSession().mappingCount], DefaultMappings[i]))
                TheSession().mappingCount++;
        }
    }

    Joystick* Input::AllocJoystick(const char* name, const char* guid, int axisCount, int buttonCount, int hatCount)
    {
        int jid;
        Joystick* js;

        for (jid = 0;  jid <= Joystick::JOYSTICK_LAST;  jid++)
        {
            if (!TheSession().joysticks[jid].allocated)
                break;
        }

        if (jid > Joystick::JOYSTICK_LAST)
        {
            return nullptr;
        }

        js = TheSession().joysticks + jid;
        js->allocated   = 1;
        js->axes        = (float*)Session::alloc(axisCount, sizeof(float));
        js->buttons     = (unsigned char*)Session::alloc(buttonCount + (size_t) hatCount * 4, 1);
        js->hats        = (unsigned char*)Session::alloc(hatCount, 1);
        js->axisCount   = axisCount;
        js->buttonCount = buttonCount;
        js->hatCount    = hatCount;

        strncpy(js->name, name, sizeof(js->name) - 1);
        strncpy(js->guid, guid, sizeof(js->guid) - 1);
        js->mapping = FindValidMapping(js);

        return js;
    }

    void Input::FreeJoystick(Joystick* js)
    {
        Session::free(js->axes);
        Session::free(js->buttons);
        Session::free(js->hats);
        memset(js, 0, sizeof(Joystick));
    }

