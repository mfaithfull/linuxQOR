    bool Input::UpdateGamepadMappings(const char* string)
    {
        int jid;
        const char* c = string;

        qor_pp_assert_that(string != NULL);

        RequireInitOrReturn(false);

        while (*c)
        {
            if ((*c >= '0' && *c <= '9') ||
                (*c >= 'a' && *c <= 'f') ||
                (*c >= 'A' && *c <= 'F'))
            {
                char line[1024];

                const size_t length = strcspn(c, "\r\n");
                if (length < sizeof(line))
                {
                    Mapping mapping = {{0}};

                    memcpy(line, c, length);
                    line[length] = '\0';

                    if (ParseMapping(&mapping, line))
                    {
                        Mapping* previous = FindMapping(mapping.guid);
                        if (previous)
                            *previous = mapping;
                        else
                        {
                            TheSession().mappingCount++;
                            TheSession().mappings = (Mapping*) Session::realloc(TheSession().mappings, sizeof(Mapping) * TheSession().mappingCount);
                            TheSession().mappings[TheSession().mappingCount - 1] = mapping;
                        }
                    }
                }

                c += length;
            }
            else
            {
                c += strcspn(c, "\r\n");
                c += strspn(c, "\r\n");
            }
        }

        for (jid = 0;  jid <= Joystick::JOYSTICK_LAST;  jid++)
        {
            Joystick* js = TheSession().joysticks + jid;
            if (js->connected)
            {
                js->mapping = FindValidMapping(js);
            }
        }

        return true;
    }

