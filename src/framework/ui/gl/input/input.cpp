    int Input::GetKey(Window* handle, int key)
    {
        RequireInitOrReturn(RELEASE);
        Window* window = handle;
        qor_pp_assert_that(window != nullptr);

        if (key < KEY_SPACE || key > KEY_LAST)
        {
            Session::InputError(INVALID_ENUM, "Invalid key %i", key);
            return RELEASE;
        }

        if (window->keys[key] == STICK)
        {
            // Sticky mode: release key now
            window->keys[key] = RELEASE;
            return PRESS;
        }

        return (int) window->keys[key];
    }

    int Input::GetMouseButton(Window* handle, int button)
    {
        RequireInitOrReturn(RELEASE);

        Window* window = handle;
        qor_pp_assert_that(window != nullptr);

        if (button < MOUSE_BUTTON_1 || button > MOUSE_BUTTON_LAST)
        {
            Session::InputError(INVALID_ENUM, "Invalid mouse button %i", button);
            return RELEASE;
        }

        if (window->mouseButtons[button] == STICK)
        {
            // Sticky mode: release mouse button now
            window->mouseButtons[button] = RELEASE;
            return PRESS;
        }

        return (int) window->mouseButtons[button];

    }

    void Input::GetCursorPos(Window* handle, double* xpos, double* ypos)
    {
        if (xpos)
        {
            *xpos = 0;
        }
        if (ypos)
        {
            *ypos = 0;
        }

        RequireInit();

        Window* window = handle;
        qor_pp_assert_that(window != nullptr);

        if (window->GetCursorMode() == CURSOR_DISABLED)
        {
            if (xpos)
            {
                *xpos = window->virtualCursorPosX;
            }
            if (ypos)
            {
                *ypos = window->virtualCursorPosY;
            }
        }
        else
        {
            TheSession().GetPlatform().GetCursorPos(window, xpos, ypos);
        }
    }

    void Input::SetCursorPos(Window* handle, double xpos, double ypos)
    {
        RequireInit();

        Window* window = handle;
        qor_pp_assert_that(window != nullptr);

        if (xpos != xpos || xpos < -DBL_MAX || xpos > DBL_MAX ||
            ypos != ypos || ypos < -DBL_MAX || ypos > DBL_MAX)
        {
            Session::InputError(INVALID_VALUE, "Invalid cursor position %f %f", xpos, ypos);
            return;
        }

        if (!TheSession().GetPlatform().WindowFocused(window))
        {
            return;
        }

        if (window->GetCursorMode() == CURSOR_DISABLED)
        {
            // Only update the accumulated position if the cursor is disabled
            window->virtualCursorPosX = xpos;
            window->virtualCursorPosY = ypos;
        }
        else
        {
            // Update system cursor position
            TheSession().GetPlatform().SetCursorPos(window, xpos, ypos);
        }

    }

    Cursor* Input::CreateCursor(const Image* image, int xhot, int yhot)
    {
        Cursor* cursor;

        qor_pp_assert_that(image != nullptr);
        qor_pp_assert_that(image->pixels != nullptr);

        RequireInitOrReturn(nullptr);

        if (image->width <= 0 || image->height <= 0)
        {
            Session::InputError(INVALID_VALUE, "Invalid image dimensions for cursor");
            return NULL;
        }

        cursor = (Cursor*)Session::alloc(1, sizeof(Cursor));
        cursor->next = TheSession().cursorListHead;
        TheSession().cursorListHead = cursor;

        if (!TheSession().GetPlatform().CreateCursor(cursor, image, xhot, yhot))
        {
            DestroyCursor(cursor);
            return nullptr;
        }

        return cursor;
    }

    Cursor* Input::CreateStandardCursor(int shape)
    {
        Cursor* cursor;

        RequireInitOrReturn(nullptr);

        if (shape != ARROW_CURSOR &&
            shape != IBEAM_CURSOR &&
            shape != CROSSHAIR_CURSOR &&
            shape != POINTING_HAND_CURSOR &&
            shape != RESIZE_EW_CURSOR &&
            shape != RESIZE_NS_CURSOR &&
            shape != RESIZE_NWSE_CURSOR &&
            shape != RESIZE_NESW_CURSOR &&
            shape != RESIZE_ALL_CURSOR &&
            shape != NOT_ALLOWED_CURSOR)
        {
            Session::InputError(INVALID_ENUM, "Invalid standard cursor 0x%08X", shape);
            return nullptr;
        }

        cursor = (Cursor*)Session::alloc(1, sizeof(Cursor));
        cursor->next = TheSession().cursorListHead;
        TheSession().cursorListHead = cursor;

        if (!TheSession().GetPlatform().CreateStandardCursor(cursor, shape))
        {
            DestroyCursor(cursor);
            return nullptr;
        }

        return cursor;
    }

    void Input::DestroyCursor(Cursor* handle)
    {
        RequireInit();

        Cursor* cursor = handle;

        if (cursor == nullptr)
        {
            return;
        }

        // Make sure the cursor is not being used by any window
        {
            Window* window;

            for (window = TheSession().windowListHead;  window;  window = window->next)
            {
                if (window->m_cursor == cursor)
                {
                    SetCursor(window, nullptr);
                }
            }
        }

        TheSession().GetPlatform().DestroyCursor(cursor);

        // Unlink cursor from global linked list
        {
            Cursor** prev = &TheSession().cursorListHead;

            while (*prev != cursor)
            {
                prev = &((*prev)->next);
            }

            *prev = cursor->next;
        }

        Session::free(cursor);

    }

