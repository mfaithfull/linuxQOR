    int Input::GetInputMode(Window* handle, int mode)
    {
        RequireInitOrReturn(0);
        Window* window = handle;
        qor_pp_assert_that(window != nullptr);

        switch (mode)
        {
            case CURSOR:
                return window->GetCursorMode();
            case STICKY_KEYS:
                return window->stickyKeys;
            case STICKY_MOUSE_BUTTONS:
                return window->stickyMouseButtons;
            case LOCK_KEY_MODS:
                return window->lockKeyMods;
            case RAW_MOUSE_MOTION:
                return window->GetRawMouseMotion();
            case UNLIMITED_MOUSE_BUTTONS:
                return window->disableMouseButtonLimit;
        }

        Session::InputError(INVALID_ENUM, "Invalid input mode 0x%08X", mode);
        return 0;
    }

    void Input::SetInputMode(Window* handle, int mode, int value)
    {
        RequireInit();

        Window* window = handle;
        qor_pp_assert_that(window != nullptr);

        switch (mode)
        {
            case CURSOR:
            {
                if (value != CURSOR_NORMAL &&
                    value != CURSOR_HIDDEN &&
                    value != CURSOR_DISABLED &&
                    value != CURSOR_CAPTURED)
                {
                    Session::InputError(INVALID_ENUM, "Invalid cursor mode 0x%08X", value);
                    return;
                }

                if (window->GetCursorMode() == value)
                    return;

                window->m_cursorMode = value;

                TheSession().GetPlatform().GetCursorPos(window, &window->virtualCursorPosX, &window->virtualCursorPosY);
                TheSession().GetPlatform().SetCursorMode(window, value);
                return;
            }

            case STICKY_KEYS:
            {
                value = value ? true : false;
                if (window->stickyKeys == value)
                    return;

                if (!value)
                {
                    int i;

                    // Release all sticky keys
                    for (i = 0;  i <= KEY_LAST;  i++)
                    {
                        if (window->keys[i] == STICK)
                            window->keys[i] = RELEASE;
                    }
                }

                window->stickyKeys = value;
                return;
            }

            case STICKY_MOUSE_BUTTONS:
            {
                value = value ? true : false;
                if (window->stickyMouseButtons == value)
                    return;

                if (!value)
                {
                    int i;

                    // Release all sticky mouse buttons
                    for (i = 0;  i <= MOUSE_BUTTON_LAST;  i++)
                    {
                        if (window->mouseButtons[i] == STICK)
                            window->mouseButtons[i] = RELEASE;
                    }
                }

                window->stickyMouseButtons = value;
                return;
            }

            case LOCK_KEY_MODS:
            {
                window->lockKeyMods = value ? true : false;
                return;
            }

            case RAW_MOUSE_MOTION:
            {
                if (!TheSession().GetPlatform().RawMouseMotionSupported())
                {
                    Session::InputError(PLATFORM_ERROR, "Raw mouse motion is not supported on this system");
                    return;
                }

                value = value ? true : false;
                if (window->GetRawMouseMotion() == value)
                    return;

                window->m_rawMouseMotion = value;
                TheSession().GetPlatform().SetRawMouseMotion(window, value);
                return;
            }

            case UNLIMITED_MOUSE_BUTTONS:
            {
                window->disableMouseButtonLimit = value ? true : false;
                return;
            }
        }

        Session::InputError(INVALID_ENUM, "Invalid input mode 0x%08X", mode);

    }

