    void Input::InputJoystickHat(Joystick* js, int hat, char value)
    {
        int base;

        qor_pp_assert_that(js != NULL);
        qor_pp_assert_that(hat >= 0);
        qor_pp_assert_that(hat < js->hatCount);

        // Valid hat values only use the least significant nibble
        qor_pp_assert_that((value & 0xf0) == 0);
        // Valid hat values do not have both bits of an axis set
        qor_pp_assert_that((value & HAT_LEFT) == 0 || (value & HAT_RIGHT) == 0);
        qor_pp_assert_that((value & HAT_UP) == 0 || (value & HAT_DOWN) == 0);

        base = js->buttonCount + hat * 4;

        js->buttons[base + 0] = (value & 0x01) ? PRESS : RELEASE;
        js->buttons[base + 1] = (value & 0x02) ? PRESS : RELEASE;
        js->buttons[base + 2] = (value & 0x04) ? PRESS : RELEASE;
        js->buttons[base + 3] = (value & 0x08) ? PRESS : RELEASE;

        js->hats[hat] = value;

    }

