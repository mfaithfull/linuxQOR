    void Input::InputMouseClick(Window* window, int button, int action, int mods)
    {
        qor_pp_assert_that(window != nullptr);
        qor_pp_assert_that(button >= 0);
        qor_pp_assert_that(action == PRESS || action == RELEASE);
        qor_pp_assert_that(mods == (mods & MOD_MASK));

        if (button < 0 || (!window->disableMouseButtonLimit && button > MOUSE_BUTTON_LAST))
        {
            return;
        }

        if (!window->lockKeyMods)
        {
            mods &= ~(MOD_CAPS_LOCK | MOD_NUM_LOCK);
        }

        if (button <= MOUSE_BUTTON_LAST)
        {
            if (action == RELEASE && window->stickyMouseButtons)
                window->mouseButtons[button] = STICK;
            else
                window->mouseButtons[button] = (char) action;
        }

        if (window->callbacks.mouseButton)
        {
            window->callbacks.mouseButton(window, button, action, mods);
        }
    }

