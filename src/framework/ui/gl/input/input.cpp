    void Input::InputKey(Window* window, int key, int scancode, int action, int mods)
    {
        qor_pp_assert_that(window != nullptr);
        qor_pp_assert_that(key >= 0 || key == KEY_UNKNOWN);
        qor_pp_assert_that(key <= KEY_LAST);
        qor_pp_assert_that(action == PRESS || action == RELEASE);
        qor_pp_assert_that(mods == (mods & MOD_MASK));

        if (key >= 0 && key <= KEY_LAST)
        {
            bool repeated = false;

            if (action == RELEASE && window->keys[key] == RELEASE)
            {
                return;
            }

            if (action == PRESS && window->keys[key] == PRESS)
            {
                repeated = true;
            }

            if (action == RELEASE && window->stickyKeys)
            {
                window->keys[key] = STICK;
            }
            else
            {
                window->keys[key] = (char) action;
            }

            if (repeated)
            {
                action = REPEAT;
            }
        }

        if (!window->lockKeyMods)
        {
            mods &= ~(MOD_CAPS_LOCK | MOD_NUM_LOCK);
        }

        if (!window->callbacks.key.IsNull())
        {
            window->callbacks.key(window, key, scancode, action, mods);
        }        
    }

    void Input::InputChar(Window* window, uint32_t codepoint, int mods, int plain)
    {
        qor_pp_assert_that(window != nullptr);
        qor_pp_assert_that(mods == (mods & MOD_MASK));
        qor_pp_assert_that(plain == 1 || plain == 0);

        if (codepoint < 32 || (codepoint > 126 && codepoint < 160))
        {
            return;
        }

        if (!window->lockKeyMods)
        {
            mods &= ~(MOD_CAPS_LOCK | MOD_NUM_LOCK);
        }

        if (window->callbacks.charmods)
        {
            window->callbacks.charmods(window, codepoint, mods);
        }

        if (plain && window->callbacks.character)
        {
            window->callbacks.character(window, codepoint);
        }        
    }

