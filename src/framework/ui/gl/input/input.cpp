    bool Input::GetGamepadState(int jid, GamepadState* state)
    {
        int i;
        Joystick* js;

        qor_pp_assert_that(jid >= Joystick::JOYSTICK_1);
        qor_pp_assert_that(jid <= Joystick::JOYSTICK_LAST);
        qor_pp_assert_that(state != NULL);

        memset(state, 0, sizeof(GamepadState));

        RequireInitOrReturn(false);

        if (jid < 0 || jid > Joystick::JOYSTICK_LAST)
        {
            Session::InputError(INVALID_ENUM, "Invalid joystick ID %i", jid);
            return false;
        }

        if (!InitJoysticks())
        {
            return false;
        }

        js = TheSession().joysticks + jid;
        if (!js->connected)
        {
            return false;
        }

        if (!TheSession().GetPlatform().PollJoystick(js, POLL_ALL))
        {
            return false;
        }

        if (!js->mapping)
        {
            return false;
        }

        for (i = 0;  i <= GAMEPAD_BUTTON_LAST;  i++)
        {
            const MapElement* e = js->mapping->buttons + i;
            if (e->type == JOYSTICK_AXIS)
            {
                const float value = js->axes[e->index] * e->axisScale + e->axisOffset;
                // HACK: This should be baked into the value transform
                // TODO: Bake into transform when implementing output modifiers
                if (e->axisOffset < 0 || (e->axisOffset == 0 && e->axisScale > 0))
                {
                    if (value >= 0.f)
                        state->buttons[i] = PRESS;
                }
                else
                {
                    if (value <= 0.f)
                        state->buttons[i] = PRESS;
                }
            }
            else if (e->type == JOYSTICK_HATBIT)
            {
                const unsigned int hat = e->index >> 4;
                const unsigned int bit = e->index & 0xf;
                if (js->hats[hat] & bit)
                    state->buttons[i] = PRESS;
            }
            else if (e->type == JOYSTICK_BUTTON)
                state->buttons[i] = js->buttons[e->index];
        }

        for (i = 0;  i <= GAMEPAD_AXIS_LAST;  i++)
        {
            const MapElement* e = js->mapping->axes + i;
            if (e->type == JOYSTICK_AXIS)
            {
                const float value = js->axes[e->index] * e->axisScale + e->axisOffset;
                state->axes[i] = fminf(fmaxf(value, -1.f), 1.f);
            }
            else if (e->type == JOYSTICK_HATBIT)
            {
                const unsigned int hat = e->index >> 4;
                const unsigned int bit = e->index & 0xf;
                if (js->hats[hat] & bit)
                    state->axes[i] = 1.f;
                else
                    state->axes[i] = -1.f;
            }
            else if (e->type == JOYSTICK_BUTTON)
            {
                state->axes[i] = js->buttons[e->index] * 2.f - 1.f;
            }
        }

        return true;
    }

