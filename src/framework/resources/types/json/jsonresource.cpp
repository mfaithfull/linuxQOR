#include "src/configuration/configuration.h"
#include "src/platform/filesystem/fileindex.h"
#include "src/platform/filesystem/folder.h"
#include "jsonresource.h"
#include "reader.h"
#include "src/components/framework/pipeline/connectors/fileconnector/fileconnector.h"

namespace qor{ namespace framework{ namespace res {

    const char* JSON::s_jsonResourceType = "JSON resource type";

    const char* JSON::StaticType(){ return s_jsonResourceType;}

    const char* JSON::Type()
    {
        return s_jsonResourceType;
    }
    
    void JSON::Name()
    {        
        std::string name = m_index.ToString();
        m_URI = "json://<ip-address>/" + name;
        Resource::Name();
    }

    void JSON::Locate()
    {        
        //Read contents of file and parse
        JSONReader reader;
        
        m_object = reader(
            qor::components::FileConnector(
                m_index,
                reader.Buffer(),
                qor::platform::WithFlags::None,
                qor::platform::ShareMode::Owner_Read,
                qor::platform::OpenFor::ReadOnly
            )
        );  
        if(m_object.IsNotNull())
        {
            Resource::Locate();
        }
        return;
    }

    void JSON::Claim()
    {        
        //Claim the JSON object resulting from the parse
        Resource::Claim();
    }

    qor::ref_of<qor::components::json::Object>::type JSON::GetObject()
    {
        return m_object;
    }

}}}//qor::framework::res