#ifndef QOR_PP_H_FRAMEWORK_RESOURCE_CONTAINER
#define QOR_PP_H_FRAMEWORK_RESOURCE_CONTAINER

#include <map>
#include <mutex>

namespace qor{ namespace framework{

    //Abstract base for resource containers
    class AnyResourceContainer
    {
        AnyResourceContainer()
        {            
        }

        virtual ~AnyResourceContainer()
        {            
        }

        virtual void AdoptResource(Resource* res) = 0;
        virtual void DisposeResource(Resource* res) = 0;

    };

    //A map container by URI for a specific resource type
    template<class ResourceType>
    class Container : public AnyResourceContainer
    {
    public:

        Container()
        {
        }

        virtual ~Container()
        {
            {
                std::lock_guard<std::recursive_mutex> guard(m_mutex);
                for(ResourceType* res : m_resources)
                {
                    delete res;
                }
                m_resources.clear();
            }
        }

        virtual void AdoptResource(Resource* res)
        {
            if(res)
            {
                std::lock_guard<std::recursive_mutex> guard(m_mutex);
                std::string uri = res->URI();
                if(m_resources.find(uri) == m_resources.end())
                {
                    m_resources.emplace(uri, dynamic_cast<ResourceType*>(res));
                }
            }
        }

        virtual void DisposeResource(Resource* res)
        {
            if(res)
            {
                std::lock_guard<std::recursive_mutex> guard(m_mutex);
                std::string uri = res->URI();
                auto it = m_resources.find(uri);
                if(it != m_resources.end())
                {
                    m_resources.erase(it);
                }
            }
        }

    private:

        std::map<std::string, ResourceType*> m_resources;
        std::recursive_mutex m_mutex;

    };

    //A by type map container for by URI resource containers
    class ByTypeContainer final
    {
    public:

        ByTypeContainer()
        {            
        }

        ~ByTypeContainer()
        {            
        }

        bool RegisterResourceType(const char* typeID, AnyResourceContainer* container)
        {
            std::lock_guard<std::recursive_mutex> guard(m_mutex);
            if(typeID != nullptr && container != nullptr && m_containers.find(typeID) == m_containers.end())
            {
                m_containers.emplace(typeID, container);
                return true;
            }
            return false;
        }

        bool UnregisterResourceType(const char* typeID)
        {
            if(typeID != nullptr )
            {
                std::lock_guard<std::recursive_mutex> guard(m_mutex);
                auto it = m_containers.find(typeID);
                if( it != m_containers.end())
                {
                    m_containers.erase(it);
                    return true;
                }
            }
            return false;
        }

        AnyResourceContainer* GetContainer(const char* typeID)
        {
            std::lock_guard<std::recursive_mutex> guard(m_mutex);
            AnyResourceContainer* result = nullptr;
            auto it = m_containers.find(typeID);
            if(it != m_containers.end())
            {
                result = it->second;
            }
            return result;
        }

    private:

        std::map<const char*, AnyResourceContainer*> m_containers;
        std::recursive_mutex m_mutex;
    };

}}//qor::framework

#endif//QOR_PP_H_FRAMEWORK_RESOURCE_CLAIMER